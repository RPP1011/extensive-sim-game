//! Python emission for compiler milestone 2.
//!
//! `EventIR` → `@dataclass` in a per-event Python module, plus an aggregate
//! `__init__.py` that re-exports every dataclass. The emitted dataclasses are
//! a structural mirror of the Rust structs — same field names, same order,
//! best-effort type mapping. Python consumers use them to unpack trace rows.

use std::collections::BTreeSet;
use std::fmt::Write;

use crate::ir::{EventIR, IrType};

pub fn emit_event_dataclass(event: &EventIR, source_file: Option<&str>) -> String {
    let mut out = String::new();
    emit_header(&mut out, source_file);
    writeln!(out, "from dataclasses import dataclass").unwrap();
    if uses_optional_or_list(event) {
        // No import needed — we use builtin `list[...]` and `T | None` syntax.
    }
    writeln!(out).unwrap();
    writeln!(out).unwrap();
    writeln!(out, "@dataclass").unwrap();
    writeln!(out, "class {}:", event.name).unwrap();
    // Synthesize implicit tick on the emitted fields list.
    let mut fields = event.fields.clone();
    if !fields.iter().any(|f| f.name == "tick") {
        fields.push(crate::ir::EventField {
            name: "tick".into(),
            ty: IrType::U32,
            span: crate::ast::Span::dummy(),
        });
    }
    if fields.is_empty() {
        writeln!(out, "    pass").unwrap();
        return out;
    }
    for f in &fields {
        let (ty, comment) = render_py_type(&f.ty);
        match comment {
            Some(c) => writeln!(out, "    {}: {}  # {}", f.name, ty, c).unwrap(),
            None => writeln!(out, "    {}: {}", f.name, ty).unwrap(),
        }
    }
    out
}

pub fn emit_events_init(events: &[EventIR]) -> String {
    let mut sorted: Vec<&EventIR> = events.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    writeln!(out, "# GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(
        out,
        "# Regenerate with `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out).unwrap();
    for e in &sorted {
        writeln!(out, "from .{} import {}", snake_case(&e.name), e.name).unwrap();
    }
    writeln!(out).unwrap();
    let names: Vec<String> = sorted.iter().map(|e| format!("\"{}\"", e.name)).collect();
    writeln!(out, "__all__ = [{}]", names.join(", ")).unwrap();
    out
}

