fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(out, "# GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "# GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "# Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "# Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
}

/// Returns (type text, optional trailing comment).
fn render_py_type(ty: &IrType) -> (String, Option<String>) {
    match ty {
        IrType::Bool => ("bool".into(), None),
        IrType::I8
        | IrType::U8
        | IrType::I16
        | IrType::U16
        | IrType::I32
        | IrType::I64
        | IrType::U32
        | IrType::U64 => ("int".into(), None),
        IrType::F32 | IrType::F64 => ("float".into(), None),
        IrType::String => ("str".into(), None),
        IrType::Vec3 => (
            "tuple[float, float, float]".into(),
            None,
        ),
        IrType::AgentId => ("int".into(), Some("AgentId".into())),
        IrType::ItemId => ("int".into(), Some("ItemId".into())),
        IrType::GroupId => ("int".into(), Some("GroupId".into())),
        IrType::QuestId => ("int".into(), Some("QuestId".into())),
        IrType::AuctionId => ("int".into(), Some("AuctionId".into())),
        IrType::EventId => ("int".into(), Some("EventId".into())),
        IrType::AbilityId => ("int".into(), Some("AbilityId".into())),
        IrType::Array(inner, cap) => {
            let (inner_ty, _) = render_py_type(inner);
            (
                format!("tuple[{}, ...]", inner_ty),
                Some(format!("len={}", cap)),
            )
        }
        IrType::Optional(inner) => {
            let (inner_ty, comment) = render_py_type(inner);
            (format!("{} | None", inner_ty), comment)
        }
        IrType::Tuple(items) => {
            let parts: Vec<String> = items.iter().map(|t| render_py_type(t).0).collect();
            (format!("tuple[{}]", parts.join(", ")), None)
        }
        IrType::SortedVec(inner, cap) => {
            let (inner_ty, _) = render_py_type(inner);
            (format!("list[{}]", inner_ty), Some(format!("SortedVec cap={}", cap)))
        }
        IrType::RingBuffer(inner, cap) => {
            let (inner_ty, _) = render_py_type(inner);
            (
                format!("list[{}]", inner_ty),
                Some(format!("RingBuffer cap={}", cap)),
            )
        }
        IrType::SmallVec(inner, cap) => {
            let (inner_ty, _) = render_py_type(inner);
            (format!("list[{}]", inner_ty), Some(format!("SmallVec cap={}", cap)))
        }
        IrType::List(inner) => {
            let (inner_ty, _) = render_py_type(inner);
            (format!("list[{}]", inner_ty), None)
        }
        IrType::EntityRef(_) => ("int".into(), Some("EntityRef".into())),
        IrType::EventRef(_) => ("int".into(), Some("EventRef".into())),
        IrType::Enum { name, .. } => ("str".into(), Some(format!("enum {}", name))),
        IrType::Named(n) => ("object".into(), Some(n.clone())),
        IrType::Unknown => ("object".into(), Some("unknown".into())),
    }
}

fn uses_optional_or_list(event: &EventIR) -> bool {
    fn walk(ty: &IrType, seen: &mut BTreeSet<&'static str>) {
        match ty {
            IrType::Optional(inner) => {
                seen.insert("opt");
                walk(inner, seen);
            }
            IrType::List(inner)
            | IrType::SortedVec(inner, _)
            | IrType::RingBuffer(inner, _)
            | IrType::SmallVec(inner, _)
            | IrType::Array(inner, _) => {
                seen.insert("list");
                walk(inner, seen);
            }
            IrType::Tuple(items) => {
                for t in items {
                    walk(t, seen);
                }
            }
            _ => {}
        }
    }
    let mut seen = BTreeSet::new();
    for f in &event.fields {
        walk(&f.ty, &mut seen);
    }
    !seen.is_empty()
}

fn snake_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 4);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            for lower in ch.to_lowercase() {
                out.push(lower);
            }
            prev_upper = true;
        } else {
            out.push(ch);
            prev_upper = false;
        }
    }
    out
}

