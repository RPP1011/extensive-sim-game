#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::Span;
    use crate::ir::{EventField, EventIR, IrType};

    fn mk_field(name: &str, ty: IrType) -> EventField {
        EventField { name: name.into(), ty, span: Span::dummy() }
    }

    fn mk_event(name: &str, fields: Vec<EventField>) -> EventIR {
        EventIR {
            name: name.into(),
            fields,
            tags: vec![],
            annotations: vec![],
            span: Span::dummy(),
        }
    }

    #[test]
    fn simple_primitives() {
        let e = mk_event("Heal", vec![mk_field("amount", IrType::F32)]);
        let out = emit_event_dataclass(&e, None);
        assert!(out.contains("@dataclass"));
        assert!(out.contains("class Heal:"));
        assert!(out.contains("amount: float"));
        // Implicit tick.
        assert!(out.contains("tick: int"));
    }

    #[test]
    fn with_niche_id() {
        let e = mk_event(
            "Damage",
            vec![
                mk_field("target", IrType::AgentId),
                mk_field("amount", IrType::F32),
            ],
        );
        let out = emit_event_dataclass(&e, Some("assets/sim/events.sim"));
        assert!(out.contains("target: int  # AgentId"));
        assert!(out.contains("# GENERATED by dsl_compiler from assets/sim/events.sim."));
    }

    #[test]
    fn with_optional() {
        let e = mk_event(
            "MaybeHit",
            vec![mk_field("target", IrType::Optional(Box::new(IrType::AgentId)))],
        );
        let out = emit_event_dataclass(&e, None);
        assert!(out.contains("target: int | None  # AgentId"));
    }

    #[test]
    fn with_array() {
        let e = mk_event(
            "MultiHit",
            vec![mk_field("targets", IrType::Array(Box::new(IrType::AgentId), 4))],
        );
        let out = emit_event_dataclass(&e, None);
        assert!(out.contains("targets: tuple[int, ...]  # len=4"));
    }

    #[test]
    fn init_file_alphabetical() {
        let e1 = mk_event("Zebra", vec![mk_field("t", IrType::U32)]);
        let e2 = mk_event("Apple", vec![mk_field("t", IrType::U32)]);
        let out = emit_events_init(&[e1, e2]);
        let a = out.find("from .apple import Apple").unwrap();
        let z = out.find("from .zebra import Zebra").unwrap();
        assert!(a < z);
        assert!(out.contains("__all__ = [\"Apple\", \"Zebra\"]"));
    }
}
