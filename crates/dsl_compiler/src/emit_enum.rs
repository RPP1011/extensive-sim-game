//! Rust + Python emission for `enum` declarations. Enums emit as
//! `#[repr(u8)]` Rust enums (pure data, no engine-side dependency) plus
//! matching Python `IntEnum` classes.

use std::fmt::Write;

use crate::ir::EnumIR;

pub fn emit_enum(e: &EnumIR, source_file: Option<&str>) -> String {
    let mut out = String::new();
    match source_file {
        Some(path) => writeln!(out, "// GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#[derive(Copy, Clone, Debug, PartialEq, Eq, Hash)]").unwrap();
    writeln!(out, "#[repr(u8)]").unwrap();
    writeln!(out, "pub enum {} {{", e.name).unwrap();
    for (i, v) in e.variants.iter().enumerate() {
        writeln!(out, "    {} = {},", v, i).unwrap();
    }
    writeln!(out, "}}").unwrap();
    out
}

pub fn emit_enum_mod(enums: &[EnumIR]) -> String {
    let mut sorted: Vec<&EnumIR> = enums.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    writeln!(out, "// GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(
        out,
        "// Regenerate with `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out).unwrap();
    for e in &sorted {
        writeln!(out, "pub mod {};", snake_case(&e.name)).unwrap();
    }
    writeln!(out).unwrap();
    for e in &sorted {
        writeln!(out, "pub use {}::{};", snake_case(&e.name), e.name).unwrap();
    }
    out
}

pub fn emit_enum_py(e: &EnumIR, source_file: Option<&str>) -> String {
    let mut out = String::new();
    match source_file {
        Some(path) => writeln!(out, "# GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "# GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "# Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "# Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "from enum import IntEnum").unwrap();
    writeln!(out).unwrap();
    writeln!(out).unwrap();
    writeln!(out, "class {}(IntEnum):", e.name).unwrap();
    if e.variants.is_empty() {
        writeln!(out, "    pass").unwrap();
        return out;
    }
    for (i, v) in e.variants.iter().enumerate() {
        writeln!(out, "    {} = {}", v, i).unwrap();
    }
    out
}

pub fn emit_enum_init(enums: &[EnumIR]) -> String {
    let mut sorted: Vec<&EnumIR> = enums.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    writeln!(out, "# GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(
        out,
        "# Regenerate with `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out).unwrap();
    for e in &sorted {
        writeln!(out, "from .{} import {}", snake_case(&e.name), e.name).unwrap();
    }
    writeln!(out).unwrap();
    let names: Vec<String> = sorted.iter().map(|e| format!("\"{}\"", e.name)).collect();
    writeln!(out, "__all__ = [{}]", names.join(", ")).unwrap();
    out
}

fn snake_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 4);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            for lower in ch.to_lowercase() {
                out.push(lower);
            }
            prev_upper = true;
        } else {
            out.push(ch);
            prev_upper = false;
        }
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::Span;

    fn mk_enum(name: &str, variants: Vec<&str>) -> EnumIR {
        EnumIR {
            name: name.into(),
            variants: variants.into_iter().map(String::from).collect(),
            annotations: vec![],
            span: Span::dummy(),
        }
    }

    #[test]
    fn repr_u8_with_explicit_ordinals() {
        let e = mk_enum("GroupBehavior", vec!["OneVsOne", "SharedPursuit"]);
        let out = emit_enum(&e, Some("assets/sim/enums.sim"));
        assert!(out.contains("// GENERATED by dsl_compiler from assets/sim/enums.sim."));
        assert!(out.contains("#[repr(u8)]"));
        assert!(out.contains("pub enum GroupBehavior {"));
        assert!(out.contains("OneVsOne = 0,"));
        assert!(out.contains("SharedPursuit = 1,"));
    }

    #[test]
    fn python_intenum_numbered() {
        let e = mk_enum("Flavour", vec!["Salty", "Sweet", "Umami"]);
        let out = emit_enum_py(&e, None);
        assert!(out.contains("class Flavour(IntEnum):"));
        assert!(out.contains("Salty = 0"));
        assert!(out.contains("Umami = 2"));
    }
}
