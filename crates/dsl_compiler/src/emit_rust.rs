//! Rust emission for compiler milestone 2.
//!
//! `EventIR` → `struct` in a per-event file, plus an aggregate `events/mod.rs`
//! that exposes the `Event` enum. The output is deliberately mechanical — no
//! macros, no helper traits, just `writeln!` into a `String`. Reviewers should
//! be able to skim the emitted output without tracing any abstractions back
//! to the emitter.

use std::collections::BTreeSet;
use std::fmt::Write;

use crate::ir::{EventIR, IrType};

/// Emit a single event file (`events/<snake_case>.rs`).
///
/// `source_file` is the DSL source path the event came from. If a single path
/// is threaded through, we stamp it into the header; otherwise the caller
/// passes `None` and we emit a generic header.
pub fn emit_event(event: &EventIR, source_file: Option<&str>) -> String {
    let mut out = String::new();
    emit_header(&mut out, source_file);
    emit_imports(&mut out, event);
    emit_struct(&mut out, event);
    out
}

/// Emit the aggregate `events/mod.rs`.
pub fn emit_events_mod(events: &[EventIR]) -> String {
    let mut sorted: Vec<&EventIR> = events.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    let mut out = String::new();
    writeln!(out, "// GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(out, "// Regenerate with `cargo run --bin xtask -- compile-dsl`.").unwrap();
    writeln!(out).unwrap();

    for e in &sorted {
        writeln!(out, "pub mod {};", snake_case(&e.name)).unwrap();
    }
    writeln!(out).unwrap();
    for e in &sorted {
        writeln!(out, "pub use {}::{};", snake_case(&e.name), e.name).unwrap();
    }
    writeln!(out).unwrap();
    writeln!(out, "use serde::{{Deserialize, Serialize}};").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]").unwrap();
    writeln!(out, "pub enum Event {{").unwrap();
    for e in &sorted {
        writeln!(out, "    {}({}),", e.name, e.name).unwrap();
    }
    writeln!(out, "}}").unwrap();
    out
}

// ---------------------------------------------------------------------------
// Internals
// ---------------------------------------------------------------------------

fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(
            out,
            "// GENERATED by dsl_compiler from {}.",
            path
        )
        .unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
}

fn emit_imports(out: &mut String, event: &EventIR) {
    let mut niche_ids: BTreeSet<&'static str> = BTreeSet::new();
    let mut needs_vec3 = false;
    let mut needs_smallvec = false;

    for f in &event.fields {
        collect_imports(&f.ty, &mut niche_ids, &mut needs_vec3, &mut needs_smallvec);
    }

    // Emit imports in alphabetical order so `rustfmt` is a no-op.
    // Order: engine::ids, glam, serde, smallvec.
    if !niche_ids.is_empty() {
        let joined: Vec<&str> = niche_ids.iter().copied().collect();
        if joined.len() == 1 {
            writeln!(out, "use engine::ids::{};", joined[0]).unwrap();
        } else {
            writeln!(out, "use engine::ids::{{{}}};", joined.join(", ")).unwrap();
        }
    }
    if needs_vec3 {
        writeln!(out, "use glam::Vec3;").unwrap();
    }
    writeln!(out, "use serde::{{Deserialize, Serialize}};").unwrap();
    if needs_smallvec {
        writeln!(out, "use smallvec::SmallVec;").unwrap();
    }
    writeln!(out).unwrap();
}

fn collect_imports(
    ty: &IrType,
    niche_ids: &mut BTreeSet<&'static str>,
    needs_vec3: &mut bool,
    needs_smallvec: &mut bool,
) {
    match ty {
        IrType::AgentId => {
            niche_ids.insert("AgentId");
        }
        IrType::ItemId => {
            niche_ids.insert("ItemId");
        }
        IrType::GroupId => {
            niche_ids.insert("GroupId");
        }
        IrType::QuestId => {
            niche_ids.insert("QuestId");
        }
        IrType::AuctionId => {
            niche_ids.insert("AuctionId");
        }
        IrType::EventId => {
            niche_ids.insert("EventId");
        }
        IrType::AbilityId => {
            niche_ids.insert("AbilityId");
        }
        IrType::Vec3 => {
            *needs_vec3 = true;
        }
        IrType::SortedVec(inner, _)
        | IrType::RingBuffer(inner, _)
        | IrType::SmallVec(inner, _)
        | IrType::Array(inner, _)
        | IrType::Optional(inner)
        | IrType::List(inner) => {
            if matches!(ty, IrType::SmallVec(_, _)) {
                *needs_smallvec = true;
            }
            collect_imports(inner, niche_ids, needs_vec3, needs_smallvec);
        }
        IrType::Tuple(items) => {
            for t in items {
                collect_imports(t, niche_ids, needs_vec3, needs_smallvec);
            }
        }
        _ => {}
    }
}

fn emit_struct(out: &mut String, event: &EventIR) {
    // TODO(milestone-3+): annotations (`@replayable`, `@high_volume`, `@traced`)
    // will influence derives and trace emission. For now every event gets
    // `Serialize + Deserialize` unconditionally.
    let all_copy = event.fields.iter().all(|f| is_copy(&f.ty));
    if all_copy {
        writeln!(
            out,
            "#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]"
        )
        .unwrap();
    } else {
        writeln!(out, "#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]").unwrap();
    }
    writeln!(out, "pub struct {} {{", event.name).unwrap();
    for f in &event.fields {
        // Niche IDs don't implement serde themselves; bridge via the
        // hand-written `engine_rules::id_serde` module.
        if let Some(helper) = id_serde_module(&f.ty) {
            writeln!(out, "    #[serde(with = \"crate::id_serde::{}\")]", helper).unwrap();
        }
        writeln!(out, "    pub {}: {},", f.name, render_type(&f.ty)).unwrap();
    }
    writeln!(out, "}}").unwrap();
}

fn id_serde_module(ty: &IrType) -> Option<&'static str> {
    match ty {
        IrType::AgentId => Some("agent_id"),
        IrType::ItemId => Some("item_id"),
        IrType::GroupId => Some("group_id"),
        IrType::QuestId => Some("quest_id"),
        IrType::AuctionId => Some("auction_id"),
        IrType::AbilityId => Some("ability_id"),
        IrType::EventId => Some("event_id"),
        _ => None,
    }
}

fn is_copy(ty: &IrType) -> bool {
    match ty {
        IrType::Bool
        | IrType::I8
        | IrType::U8
        | IrType::I16
        | IrType::U16
        | IrType::I32
        | IrType::U32
        | IrType::I64
        | IrType::U64
        | IrType::F32
        | IrType::F64
        | IrType::Vec3
        | IrType::AgentId
        | IrType::ItemId
        | IrType::GroupId
        | IrType::QuestId
        | IrType::AuctionId
        | IrType::EventId
        | IrType::AbilityId => true,
        IrType::Array(inner, _) | IrType::Optional(inner) => is_copy(inner),
        IrType::Tuple(items) => items.iter().all(is_copy),
        _ => false,
    }
}

fn render_type(ty: &IrType) -> String {
    match ty {
        IrType::Bool => "bool".into(),
        IrType::I8 => "i8".into(),
        IrType::U8 => "u8".into(),
        IrType::I16 => "i16".into(),
        IrType::U16 => "u16".into(),
        IrType::I32 => "i32".into(),
        IrType::U32 => "u32".into(),
        IrType::I64 => "i64".into(),
        IrType::U64 => "u64".into(),
        IrType::F32 => "f32".into(),
        IrType::F64 => "f64".into(),
        IrType::Vec3 => "Vec3".into(),
        IrType::String => "String".into(),
        IrType::AgentId => "AgentId".into(),
        IrType::ItemId => "ItemId".into(),
        IrType::GroupId => "GroupId".into(),
        IrType::QuestId => "QuestId".into(),
        IrType::AuctionId => "AuctionId".into(),
        IrType::EventId => "EventId".into(),
        IrType::AbilityId => "AbilityId".into(),
        IrType::SortedVec(inner, cap) => {
            format!("SmallVec<[{}; {}]>", render_type(inner), cap)
        }
        IrType::RingBuffer(inner, cap) => {
            format!("SmallVec<[{}; {}]>", render_type(inner), cap)
        }
        IrType::SmallVec(inner, cap) => {
            format!("SmallVec<[{}; {}]>", render_type(inner), cap)
        }
        IrType::Array(inner, cap) => format!("[{}; {}]", render_type(inner), cap),
        IrType::Optional(inner) => format!("Option<{}>", render_type(inner)),
        IrType::Tuple(items) => {
            let parts: Vec<String> = items.iter().map(render_type).collect();
            format!("({})", parts.join(", "))
        }
        IrType::List(inner) => format!("Vec<{}>", render_type(inner)),
        IrType::EntityRef(_) => "u32 /* EntityRef */".into(),
        IrType::EventRef(_) => "u32 /* EventRef */".into(),
        IrType::Enum { name, .. } => name.clone(),
        IrType::Named(n) => n.clone(),
        IrType::Unknown => "()".into(),
    }
}

fn snake_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 4);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            for lower in ch.to_lowercase() {
                out.push(lower);
            }
            prev_upper = true;
        } else {
            out.push(ch);
            prev_upper = false;
        }
    }
    out
}

