//! Probe declaration emission — closes the silent-drop gap on
//! `probe <name> { scenario / seed / seeds / ticks / tolerance / assert
//! { … } }` declarations flagged in the 2026-04-26 spec audit
//! (`docs/spec/dsl.md` §2.9 callout) and tracked in the 2026-05-03
//! verb/probe/metric plan
//! (`docs/superpowers/plans/2026-05-03-verb-probe-metric-emit.md`,
//! Slice B).
//!
//! # What this emits
//!
//! For each [`ProbeIR`] in the [`Compilation`], this synthesises a
//! generic Rust function:
//!
//! ```ignore
//! pub fn run_<probe_name><State: ::engine::sim_trait::CompiledSim>(
//!     state: &mut State,
//! ) -> ProbeOutcome
//! ```
//!
//! The function reads the probe's `scenario` / `seed` / `seeds` /
//! `ticks` metadata as a constant-folded test header (so the runtime
//! caller doesn't have to thread those values through), drives the
//! supplied [`engine::sim_trait::CompiledSim`] through the declared
//! tick count via repeated `state.step()` calls, and after the run
//! evaluates each `IrAssertExpr::{Count, Pr, Mean}` predicate against
//! constant-folded source-side metadata. The final return is a typed
//! [`ProbeOutcome`] indicating whether every assert held, which one
//! failed (with expected / actual payload), or whether the predicate
//! shape sat outside the supported subset and was skipped.
//!
//! # Supported assert shape (initial scope)
//!
//! The emitter today supports the **constant-foldable scalar shape**:
//!
//! ```text
//! count[<literal>] <op> <literal>
//! ```
//!
//! plus the analogous `pr[ … | … ]` and `mean[ … | … ]` heads when
//! both sides constant-fold. The check evaluates the LHS expression
//! to an `f64`, the RHS to an `f64`, and applies the comparator. This
//! keeps the emit pass independent of per-fixture readback methods —
//! the runtime needn't expose any extra accessors for these probes to
//! drive.
//!
//! Predicates whose filter / scalar / value sub-expressions don't
//! constant-fold (the binder-bound `count[a in agents where …]` form,
//! per-tick event-ring queries, `forall t in 0..ticks` quantifiers)
//! land in the `// SKIP <reason>` bucket. The probe still emits a
//! callable `run_<name>` fn that drives the runtime through `ticks`
//! ticks and returns [`ProbeOutcome::Skipped`] with the reason —
//! authors see the gap at build time as a comment plus at runtime as
//! a typed payload, exactly mirroring the invariant + metric SKIP
//! pattern.
//!
//! # Why a separate function (not `emit_cg_program`)
//!
//! Probes, like invariants and metrics, don't correspond to CG ops,
//! kernels, or the schedule — they're host-side test drivers over
//! runtime state. They live outside [`crate::cg::CgProgram`] (which
//! carries only kernel-bound IR), so the emit pass takes the
//! [`Compilation`] directly. The companion entry point
//! [`emit_cg_program_with_invariants_metrics_and_probes`] wraps
//! [`super::metrics::emit_cg_program_with_invariants_and_metrics`] and
//! injects the synthesized `probes.rs` artifact alongside the existing
//! `invariants.rs` + `metrics.rs` siblings.

use std::fmt::Write as _;

use dsl_ast::ir::{Compilation, IrAssertExpr, IrExpr, IrExprNode, ProbeIR};

use super::metrics::emit_cg_program_with_invariants_and_metrics;
use super::program::{EmittedArtifacts, ProgramEmitError};
use crate::cg::program::CgProgram;
use crate::cg::schedule::synthesis::ComputeSchedule;

/// Synthesise the per-fixture `probes.rs` source. Returns `None` when
/// the compilation declares no probes (so callers can skip the
/// artifact entirely instead of writing an empty file).
pub fn synthesize_probes(comp: &Compilation) -> Option<String> {
    if comp.probes.is_empty() {
        return None;
    }

    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Source: each `probe <name> { scenario / seed / seeds / ticks /\n");
    out.push_str("//          tolerance / assert { count[…] / pr[…] / mean[…] } }`\n");
    out.push_str("// declaration in the fixture's .sim file. Regenerate by editing\n");
    out.push_str("// the .sim file and rebuilding.\n\n");

    out.push_str("/// Outcome of a single `run_<probe_name>(state)` driver call.\n");
    out.push_str("/// `Passed` carries the actual tick count run (matches the probe's\n");
    out.push_str("/// declared `ticks` for non-skip shapes); `Failed` carries which\n");
    out.push_str("/// assert tripped, the expected literal, the observed scalar, the\n");
    out.push_str("/// comparator string, and the tick at which the check ran (always\n");
    out.push_str("/// the final tick today — per-tick assertion is a follow-up shape);\n");
    out.push_str("/// `Skipped` carries the human-readable reason the predicate's\n");
    out.push_str("/// shape sat outside the supported subset.\n");
    out.push_str("#[derive(Clone, Debug, PartialEq)]\n");
    out.push_str("pub enum ProbeOutcome {\n");
    out.push_str("    Passed { ticks_run: u64 },\n");
    out.push_str("    Failed {\n");
    out.push_str("        probe:    &'static str,\n");
    out.push_str("        assert:   usize,\n");
    out.push_str("        op:       &'static str,\n");
    out.push_str("        expected: f64,\n");
    out.push_str("        actual:   f64,\n");
    out.push_str("        tick:     u64,\n");
    out.push_str("    },\n");
    out.push_str("    Skipped { probe: &'static str, reason: &'static str },\n");
    out.push_str("}\n\n");

    for probe in &comp.probes {
        emit_probe_runner(probe, &mut out);
        out.push('\n');
    }

    Some(out)
}

/// Wrap [`emit_cg_program_with_invariants_and_metrics`] so callers
/// (per-fixture `build.rs`) get the `probes.rs` artifact alongside the
/// existing `invariants.rs` + `metrics.rs` + kernel + cross-cutting
/// set without duplicating the emit-and-merge boilerplate. Existing
/// callers of the metrics+invariants entry point are untouched; they
/// get the same artifact set (no `probes.rs`, since probes inject
/// independently of the schedule).
pub fn emit_cg_program_with_invariants_metrics_and_probes(
    schedule: &ComputeSchedule,
    prog: &CgProgram,
    comp: &Compilation,
) -> Result<EmittedArtifacts, ProgramEmitError> {
    let mut artifacts =
        emit_cg_program_with_invariants_and_metrics(schedule, prog, comp)?;
    if let Some(src) = synthesize_probes(comp) {
        artifacts.rust_files.insert("probes.rs".to_string(), src);
    }
    Ok(artifacts)
}

// ---------------------------------------------------------------------------
// Per-probe emit
// ---------------------------------------------------------------------------

fn emit_probe_runner(probe: &ProbeIR, out: &mut String) {
    let name = &probe.name;
    let ticks = probe.ticks.unwrap_or(0);
    let scenario = probe.scenario.as_deref().unwrap_or("<unspecified>");
    let seed = probe.seed.unwrap_or(0);

    // Doc-comment header — bakes the source-side metadata so authors
    // can grep for the probe's parameters in the emitted file.
    let _ = writeln!(
        out,
        "/// Probe `{name}` — drives the supplied runtime through {ticks} ticks\n\
         /// (scenario `{scenario}`, seed = {seed:#x}, asserts = {n_asserts}).\n\
         /// Returns [`ProbeOutcome::Passed`] when every constant-foldable\n\
         /// assertion holds at the final tick, [`ProbeOutcome::Failed`] when\n\
         /// any tripped, or [`ProbeOutcome::Skipped`] when the first\n\
         /// unsupported-shape assertion hits.",
        name = name,
        ticks = ticks,
        scenario = scenario,
        seed = seed,
        n_asserts = probe.asserts.len(),
    );

    let _ = writeln!(
        out,
        "pub fn run_{name}<State: ::engine::sim_trait::CompiledSim>(\n\
         \x20   state: &mut State,\n\
         ) -> ProbeOutcome {{",
        name = name,
    );
    // Constant-folded test header — same shape as the source `.sim`,\n
    // exposed as `let` bindings so the assertion bodies can reference\n
    // them (and so the emitted file is self-documenting).
    let _ = writeln!(
        out,
        "    const PROBE_NAME: &str = \"{name}\";\n\
         \x20   const TICKS: u32 = {ticks};\n\
         \x20   const SEED: u64 = {seed}_u64;\n\
         \x20   let _ = (PROBE_NAME, TICKS, SEED);",
        name = name,
        ticks = ticks,
        seed = seed,
    );

    // Drive the runtime forward by `ticks`. This is the only side
    // effect of the probe — the assertions are pure scalar
    // comparisons over the source-side constants today.
    let _ = writeln!(out, "    for _ in 0..TICKS {{ state.step(); }}");
    let _ = writeln!(out, "    let final_tick = state.tick();");

    // Per-assert evaluation. Each one emits as a small block that
    // either continues to the next assert (success) or returns
    // ProbeOutcome::Failed / ProbeOutcome::Skipped.
    if probe.asserts.is_empty() {
        let _ = writeln!(
            out,
            "    // Probe declared no asserts — successful run is just\n\
             \x20   // \"the runtime didn't panic for {ticks} ticks\".",
            ticks = ticks,
        );
    }
    for (idx, assert) in probe.asserts.iter().enumerate() {
        emit_one_assert(name, idx, assert, out);
    }

    let _ = writeln!(out, "    let _ = final_tick;");
    let _ = writeln!(out, "    ProbeOutcome::Passed {{ ticks_run: state.tick() }}");
    let _ = writeln!(out, "}}");
}

fn emit_one_assert(probe_name: &str, idx: usize, assert: &IrAssertExpr, out: &mut String) {
    let cls = classify_assert(assert);
    match cls {
        AssertShape::ConstScalar { lhs, op, rhs } => {
            // Round-trip-safe debug format for f64 so byte-identical
            // emission across runs is preserved (mirrors the invariant
            // + metric `{value:?}` discipline).
            let _ = writeln!(
                out,
                "    {{\n\
                 \x20       let lhs: f64 = {lhs:?}_f64;\n\
                 \x20       let rhs: f64 = {rhs:?}_f64;\n\
                 \x20       let ok = {predicate};\n\
                 \x20       if !ok {{\n\
                 \x20           return ProbeOutcome::Failed {{\n\
                 \x20               probe:    \"{probe}\",\n\
                 \x20               assert:   {idx},\n\
                 \x20               op:       \"{op_str}\",\n\
                 \x20               expected: rhs,\n\
                 \x20               actual:   lhs,\n\
                 \x20               tick:     final_tick,\n\
                 \x20           }};\n\
                 \x20       }}\n\
                 \x20   }}",
                lhs = lhs,
                rhs = rhs,
                predicate = const_predicate_expr("lhs", &op, "rhs"),
                probe = probe_name,
                idx = idx,
                op_str = op,
            );
        }
        AssertShape::Skipped(reason) => {
            let _ = writeln!(
                out,
                "    // SKIP `{probe}` assert #{idx}: {reason}.",
                probe = probe_name,
                idx = idx,
                reason = reason,
            );
            let _ = writeln!(
                out,
                "    return ProbeOutcome::Skipped {{ probe: \"{probe}\", reason: \"{reason}\" }};",
                probe = probe_name,
                reason = reason,
            );
        }
    }
}

/// Lower a comparator string into a Rust binary-op invocation. Returns
/// the textual `<lhs> <rust_op> <rhs>` snippet — caller wraps in
/// braces / let / etc. Unknown comparators fall through to `false`
/// (with the assert reporting Failed at runtime); the resolver should
/// only ever produce the six recognised forms.
fn const_predicate_expr(lhs: &str, op: &str, rhs: &str) -> String {
    match op {
        "==" => format!("{lhs} == {rhs}"),
        "!=" => format!("{lhs} != {rhs}"),
        "<" => format!("{lhs} < {rhs}"),
        "<=" => format!("{lhs} <= {rhs}"),
        ">" => format!("{lhs} > {rhs}"),
        ">=" => format!("{lhs} >= {rhs}"),
        _ => "false".to_string(),
    }
}

// ---------------------------------------------------------------------------
// Predicate-shape classifier
// ---------------------------------------------------------------------------

enum AssertShape {
    /// Both sides constant-fold to an `f64`. The op string is whatever
    /// the parser captured ("==", "<=", …) and we trust the resolver
    /// to have produced one of the six recognised forms.
    ConstScalar { lhs: f64, op: String, rhs: f64 },
    /// The assertion's filter / scalar / value sub-expressions don't
    /// constant-fold under the supported subset. Surfaces in the
    /// emitted file as a `// SKIP` comment + a typed runtime payload.
    Skipped(&'static str),
}

fn classify_assert(a: &IrAssertExpr) -> AssertShape {
    match a {
        IrAssertExpr::Count { filter, op, value, .. } => {
            // `count[<filter>] <op> <value>` — today only constant-foldable
            // filters and values are supported. The binder-bound shape
            // (`count[a in agents where <pred>]`) needs scope-binding
            // in the resolver before we can lower it; out of scope for
            // slice B.
            classify_const_scalar(filter, op, value)
        }
        IrAssertExpr::Pr { action_filter, op, value, .. } => {
            // `pr[<action_filter> | <obs_filter>] <op> <value>` — the
            // observation filter informs the conditional probability,
            // but for the constant-foldable case we accept either side
            // of the bar as the LHS (the constant case folds both into
            // the same scalar). Today's first cut only checks the
            // action_filter side.
            classify_const_scalar(action_filter, op, value)
        }
        IrAssertExpr::Mean { scalar, op, value, .. } => {
            // `mean[<scalar> | <filter>] <op> <value>` — same constraint:
            // the scalar inside the brackets must constant-fold to a
            // number for slice B's first cut. View-call / aggregate
            // forms are deferred.
            classify_const_scalar(scalar, op, value)
        }
    }
}

fn classify_const_scalar(lhs: &IrExprNode, op: &str, rhs: &IrExprNode) -> AssertShape {
    let lhs_val = match const_fold_scalar(lhs) {
        Some(v) => v,
        None => {
            return AssertShape::Skipped(
                "LHS of assert does not constant-fold (binder-bound aggregates / view calls / per-tick reads not yet wired)",
            );
        }
    };
    let rhs_val = match const_fold_scalar(rhs) {
        Some(v) => v,
        None => {
            return AssertShape::Skipped(
                "RHS of assert does not constant-fold (binder-bound aggregates / view calls / per-tick reads not yet wired)",
            );
        }
    };
    if !matches!(op, "==" | "!=" | "<" | "<=" | ">" | ">=") {
        return AssertShape::Skipped(
            "comparator must be one of ==, !=, <, <=, >, >=",
        );
    }
    AssertShape::ConstScalar { lhs: lhs_val, op: op.to_string(), rhs: rhs_val }
}

/// Fold a single [`IrExprNode`] to an `f64` if it's a numeric literal.
/// Returns `None` for any shape that depends on runtime state (binder
/// references, view calls, namespace fields, builtin calls, fold /
/// quantifier forms) — the caller surfaces a SKIP for those.
fn const_fold_scalar(n: &IrExprNode) -> Option<f64> {
    match &n.kind {
        IrExpr::LitInt(i) => Some(*i as f64),
        IrExpr::LitFloat(f) => Some(*f),
        IrExpr::LitBool(b) => Some(if *b { 1.0 } else { 0.0 }),
        // Tiny constant-folding for binary ops between two literals.
        // Useful when the source is `count[…] == 100 + 50` etc.
        IrExpr::Binary(op, l, r) => {
            let lv = const_fold_scalar(l)?;
            let rv = const_fold_scalar(r)?;
            use dsl_ast::ast::BinOp;
            match op {
                BinOp::Add => Some(lv + rv),
                BinOp::Sub => Some(lv - rv),
                BinOp::Mul => Some(lv * rv),
                BinOp::Div => {
                    if rv == 0.0 {
                        None
                    } else {
                        Some(lv / rv)
                    }
                }
                _ => None,
            }
        }
        _ => None,
    }
}

