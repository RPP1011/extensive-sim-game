#[cfg(test)]
mod tests {
    use super::*;
    use dsl_ast::resolve::resolve;

    fn compile(src: &str) -> Compilation {
        let p = crate::parse(src).expect("parse");
        resolve(p).expect("resolve")
    }

    #[test]
    fn empty_compilation_emits_nothing() {
        let comp = Compilation::default();
        assert!(synthesize_probes(&comp).is_none());
    }

    #[test]
    fn const_count_emits_runner_fn() {
        // count[<literal>] == <literal> — the simplest fully-folding
        // shape. Should produce a runnable `run_<name>` that drives
        // the runtime through `ticks` ticks and returns Passed when
        // the literal comparison holds.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe basic_count_holds {
  scenario "synthetic"
  seed 7
  ticks 10
  assert {
    count[5.0] == 5.0
  }
}
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        assert!(
            out.contains(
                "pub fn run_basic_count_holds<State: ::engine::sim_trait::CompiledSim>"
            ),
            "expected per-probe runner fn:\n{out}",
        );
        assert!(
            out.contains("const TICKS: u32 = 10;"),
            "expected ticks constant baked in:\n{out}",
        );
        assert!(
            out.contains("for _ in 0..TICKS { state.step(); }"),
            "expected runtime tick-driver loop:\n{out}",
        );
        assert!(
            out.contains("ProbeOutcome::Passed"),
            "expected pass-path return:\n{out}",
        );
        assert!(
            out.contains("ProbeOutcome::Failed"),
            "expected fail-path return:\n{out}",
        );
    }

    #[test]
    fn const_count_failing_emits_failure_path() {
        // count[<literal>] == <different literal> — the runner must
        // emit the failure-path return because the constant compare
        // tripped at compile time.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe always_fails {
  scenario "synthetic"
  ticks 1
  assert {
    count[5.0] == 99.0
  }
}
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        // Both branches present: the predicate evaluation happens at
        // runtime so we always have both paths in the source.
        assert!(out.contains("let lhs: f64 = 5.0_f64;"), "expected LHS literal:\n{out}");
        assert!(out.contains("let rhs: f64 = 99.0_f64;"), "expected RHS literal:\n{out}");
        assert!(out.contains("op:       \"==\""), "expected captured op string:\n{out}");
    }

    #[test]
    fn unsupported_shape_emits_skip() {
        // mean[<scalar> | <filter>] where the scalar is bare-binder
        // `1.0` and the filter references an unbound binder — today
        // the resolver doesn't bind binders inside assert filters,
        // so this is exercised via a non-folding RHS.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
config { unsupported { value: i32 = 42, } }
probe non_const_rhs {
  ticks 5
  assert {
    count[1.0] < 10.0 + 0.0
  }
}
"#;
        // The shape above DOES constant-fold (10.0 + 0.0). For a true
        // SKIP, use a builtin call form on the RHS.
        let _ = src;

        // Try with a comparison that fails to constant-fold via a
        // builtin call as RHS. Even simpler: max(1, 2) doesn't fold.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe non_const_rhs {
  ticks 5
  assert {
    count[1.0] < max(1.0, 2.0)
  }
}
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        assert!(
            out.contains("// SKIP `non_const_rhs` assert #0"),
            "expected SKIP comment for unsupported shape:\n{out}",
        );
        assert!(
            out.contains("ProbeOutcome::Skipped"),
            "expected Skipped runtime payload:\n{out}",
        );
    }

    #[test]
    fn folded_arithmetic_rhs_supported() {
        // count[1.0] == (3.0 + 2.0) — RHS is a binary expression of
        // two literals. The constant folder should compute 5.0 and
        // emit the comparison successfully.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe folded_rhs {
  ticks 1
  assert {
    count[1.0] == 3.0 + 2.0
  }
}
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        assert!(
            out.contains("let rhs: f64 = 5.0_f64;"),
            "expected folded RHS = 5.0:\n{out}",
        );
        assert!(
            !out.contains("// SKIP"),
            "folded arithmetic must not skip:\n{out}",
        );
    }

    #[test]
    fn empty_asserts_just_drives_ticks() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe just_drives {
  scenario "synthetic"
  ticks 3
}
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        assert!(
            out.contains("for _ in 0..TICKS { state.step(); }"),
            "expected tick-driver loop even without asserts:\n{out}",
        );
        assert!(
            out.contains("// Probe declared no asserts"),
            "expected explanatory comment:\n{out}",
        );
        assert!(
            out.contains("ProbeOutcome::Passed"),
            "expected unconditional pass for assert-less probes:\n{out}",
        );
    }

    #[test]
    fn outcome_enum_present() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
probe any { ticks 1 }
"#;
        let comp = compile(src);
        let out = synthesize_probes(&comp).expect("non-empty");
        assert!(out.contains("pub enum ProbeOutcome"), "expected outcome enum:\n{out}");
        assert!(out.contains("Passed { ticks_run: u64 }"), "expected Passed variant:\n{out}");
        assert!(out.contains("Failed {"), "expected Failed variant:\n{out}");
        assert!(out.contains("Skipped { probe:"), "expected Skipped variant:\n{out}");
    }
}
