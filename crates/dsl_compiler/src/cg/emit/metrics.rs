#[cfg(test)]
mod tests {
    use super::*;
    use dsl_ast::resolve::resolve;

    fn compile(src: &str) -> Compilation {
        let p = crate::parse(src).expect("parse");
        resolve(p).expect("resolve")
    }

    #[test]
    fn empty_compilation_emits_nothing() {
        let comp = Compilation::default();
        assert!(synthesize_metrics(&comp).is_none());
    }

    #[test]
    fn gauge_world_tick_emits_auto_driver() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric tick_gauge = gauge(world.tick) emit_every 1
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        assert!(
            out.contains("pub tick_gauge: Gauge,"),
            "expected MetricsSink field for gauge:\n{out}"
        );
        assert!(
            out.contains("self.tick_gauge.record(world_tick as f32);"),
            "expected auto-drive line for world.tick gauge:\n{out}"
        );
        assert!(
            out.contains("pub fn record_tick"),
            "expected record_tick driver fn:\n{out}"
        );
    }

    #[test]
    fn counter_constant_emits_constant_driver() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric ticks_seen = counter(1.0) emit_every 1
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        assert!(
            out.contains("pub ticks_seen: Counter,"),
            "expected MetricsSink field for counter:\n{out}"
        );
        assert!(
            out.contains("self.ticks_seen.record(1.0_f32);"),
            "expected auto-drive line with constant 1.0:\n{out}"
        );
    }

    #[test]
    fn histogram_emit_every_gates_record() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric tick_hist = histogram(world.tick) emit_every 10
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        assert!(
            out.contains("pub tick_hist: Histogram,"),
            "expected Histogram-typed field:\n{out}"
        );
        assert!(
            out.contains("if world_tick % 10 == 0 {"),
            "expected emit_every gating in record_tick:\n{out}"
        );
    }

    #[test]
    fn unsupported_value_shape_emits_skip_comment() {
        // Wrapper with two args (recognised wrapper name but wrong
        // arity) — must produce a SKIP plus a setter.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric two_arg_gauge = gauge(world.tick, 1.0)
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        assert!(
            out.contains("// SKIP `two_arg_gauge`"),
            "expected SKIP comment for wrong-arity wrapper:\n{out}"
        );
        assert!(
            out.contains("pub fn record_two_arg_gauge"),
            "even SKIPped metrics must expose a manual setter:\n{out}"
        );
    }

    #[test]
    fn unsupported_inner_expr_emits_skip_comment() {
        // Recognised wrapper around a binary expression — wrapper
        // shape is fine but the inner expr's source isn't auto-driveable
        // (not a literal, not world.tick). Expect SKIP + setter.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric tick_plus_one = gauge(world.tick + 1)
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        assert!(
            out.contains("// SKIP `tick_plus_one`"),
            "expected SKIP comment for non-source-expr inner:\n{out}"
        );
        assert!(
            out.contains("pub fn record_tick_plus_one"),
            "even SKIPped metrics must expose a manual setter:\n{out}"
        );
        // The auto-drive path must NOT fire for this metric — the
        // emitted record_tick driver would otherwise reference the
        // metric's `record(world_tick as f32)` setter (or a constant
        // form). The per-metric setter `record_tick_plus_one` calls
        // `self.tick_plus_one.record(value)` (note `value`, not
        // `world_tick as f32`); the auto-drive call would carry a
        // different argument. Asserting the auto-drive call shape is
        // absent proves auto-drive correctly skipped this metric.
        assert!(
            !out.contains("self.tick_plus_one.record(world_tick as f32)"),
            "auto-drive must skip non-recognised inner exprs:\n{out}"
        );
    }

    #[test]
    fn missing_emit_every_defaults_to_every_tick() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
metric {
  metric always = gauge(world.tick)
}
"#;
        let comp = compile(src);
        let out = synthesize_metrics(&comp).expect("non-empty");
        // No `if world_tick % N == 0` gate; just the bare record line.
        assert!(
            out.contains("self.always.record(world_tick as f32);"),
            "expected unconditional record line for missing emit_every:\n{out}"
        );
        assert!(
            !out.contains("if world_tick %"),
            "must not emit a gate when emit_every is absent:\n{out}"
        );
    }
}
