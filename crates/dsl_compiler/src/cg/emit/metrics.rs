fn emit_metric_setter(m: &MetricIR, out: &mut String) {
    let cls = classify(m);
    let kind_str = match cls.kind {
        MetricKind::Counter => "counter",
        MetricKind::Gauge => "gauge",
        MetricKind::Histogram => "histogram",
        MetricKind::Unknown => "gauge (default for unwrapped value)",
    };
    let _ = writeln!(
        out,
        "    /// Setter for metric `{name}` ({kind_str}). The runtime\n\
         \x20   /// calls this each tick with the freshly-evaluated value;\n\
         \x20   /// `record_tick` may also drive it automatically when the\n\
         \x20   /// metric's source is recognised by the emitter.",
        name = m.name,
        kind_str = kind_str,
    );
    if let MetricSource::Skipped(reason) = &cls.source {
        let _ = writeln!(
            out,
            "    // SKIP `{name}`: value-source shape not yet recognised — {reason}.\n\
             \x20   // (Parses + resolves cleanly; auto-drive disabled. The runtime\n\
             \x20   //  must call `record_{name}(value)` itself with whatever scalar\n\
             \x20   //  it computes for this metric.)",
            name = m.name,
            reason = reason,
        );
    }
    let _ = writeln!(
        out,
        "    pub fn record_{name}(&mut self, value: f32) {{",
        name = m.name,
    );
    let _ = writeln!(out, "        self.{name}.record(value);", name = m.name);
    let _ = writeln!(out, "    }}");
}

// ---------------------------------------------------------------------------
// Value-shape classifier
// ---------------------------------------------------------------------------

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
enum MetricKind {
    Counter,
    Gauge,
    Histogram,
    /// Bare scalar (no `gauge(...)` / `counter(...)` / `histogram(...)`
    /// wrapper). Defaults to `Gauge` semantics in the sink.
    Unknown,
}

#[derive(Clone, Debug)]
enum MetricSource {
    /// Compile-time constant `f32` — `record_tick` drives the metric
    /// with this value every tick.
    Constant(f32),
    /// `world.tick` — `record_tick` drives the metric with the runtime
    /// tick counter.
    WorldTick,
    /// Recognised wrapper but the inner expression isn't a
    /// constant or `world.tick`. The emitter still creates a per-metric
    /// setter the runtime fills in, but `record_tick` doesn't drive it
    /// automatically.
    #[allow(dead_code)]
    Computed,
    /// The wrapper itself doesn't fit a recognised shape. Surfaces in
    /// the emitted file as a `// SKIP` comment so authors see the gap.
    Skipped(&'static str),
}

#[derive(Clone, Debug)]
struct MetricClass {
    kind: MetricKind,
    source: MetricSource,
}

fn classify(m: &MetricIR) -> MetricClass {
    // Strip the optional gauge/counter/histogram wrapper. Per spec
    // §7.3 these are part of the metric grammar (not stdlib calls)
    // but parse as UnresolvedCall today — the `MetricKind` lift to a
    // real enum on the IR is a deliberate follow-up. Until then,
    // emit-side recognition is simpler than a parse-time change.
    let (kind, inner_expr) = match &m.value.kind {
        IrExpr::UnresolvedCall(name, args) if args.len() == 1 => {
            let kind = match name.as_str() {
                "gauge" => Some(MetricKind::Gauge),
                "counter" => Some(MetricKind::Counter),
                "histogram" => Some(MetricKind::Histogram),
                _ => None,
            };
            match kind {
                Some(k) => (k, &args[0].value),
                None => {
                    return MetricClass {
                        kind: MetricKind::Unknown,
                        source: MetricSource::Skipped(
                            "wrapper-form value must be `gauge` / `counter` / `histogram`",
                        ),
                    };
                }
            }
        }
        IrExpr::UnresolvedCall(_, _) => {
            return MetricClass {
                kind: MetricKind::Unknown,
                source: MetricSource::Skipped(
                    "wrapper-form must take exactly one argument",
                ),
            };
        }
        // Bare scalar: unwrapped value, default to Gauge semantics.
        _ => (MetricKind::Unknown, &m.value),
    };

    let source = match &inner_expr.kind {
        IrExpr::LitFloat(f) => MetricSource::Constant(*f as f32),
        IrExpr::LitInt(i) => MetricSource::Constant(*i as f32),
        IrExpr::NamespaceField { ns, field, .. }
            if *ns == NamespaceId::World && field == "tick" =>
        {
            MetricSource::WorldTick
        }
        IrExpr::NamespaceField { .. } => MetricSource::Skipped(
            "value source must be a literal or `world.tick` (other namespace fields not yet wired)",
        ),
        IrExpr::ViewCall(_, _) => MetricSource::Skipped(
            "view-call value source not yet wired (needs an aggregation strategy across slots)",
        ),
        IrExpr::BuiltinCall(_, _) => MetricSource::Skipped(
            "builtin-call (count / mean / sum) value source not yet wired",
        ),
        IrExpr::Quantifier { .. } | IrExpr::Fold { .. } => MetricSource::Skipped(
            "quantifier / fold value source not yet wired",
        ),
        _ => MetricSource::Skipped(
            "value source must be a literal, `world.tick`, or one of the recognised aggregate forms",
        ),
    };

    MetricClass { kind, source }
}

