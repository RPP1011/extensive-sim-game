//! Metric declaration emission — closes the silent-drop gap on
//! `metric { metric <name> = <expr> [...] }` declarations flagged in
//! the 2026-04-26 spec audit (`docs/spec/dsl.md` §2.6 callout) and
//! tracked in the 2026-05-03 verb/probe/metric plan
//! (`docs/superpowers/plans/2026-05-03-verb-probe-metric-emit.md`,
//! Slice C).
//!
//! # What this emits
//!
//! For each [`MetricIR`] in the [`Compilation`], this synthesises a
//! Rust struct field on the per-fixture `MetricsSink` plus a
//! `record_<name>(&mut self, value: f32)` setter the runtime calls
//! each tick with the freshly-evaluated metric value. A companion
//! `record_tick(&mut self, world_tick: u64)` driver fires the metrics
//! whose value source is `world.tick` automatically — those metrics
//! the runtime doesn't have to know about by name. Metrics whose
//! source is a constant or world.tick are auto-driven; others emit
//! per-name setters the runtime invokes manually with whatever scalar
//! it computes.
//!
//! # Supported value shape (initial scope)
//!
//! The emitter today supports the **wrapped-scalar shape**:
//!
//! ```text
//! metric <name> = <kind>(<inner>) [...]
//! ```
//!
//! where `<kind>` is one of `gauge` / `counter` / `histogram` (parsed
//! as `UnresolvedCall`, since these are part of the metric grammar
//! per spec §7.3 not stdlib functions; `MetricKind` lift is
//! deliberately NOT performed here — see plan's open questions §C
//! "emit-side recognition is simpler"), and `<inner>` is one of:
//!
//! - A numeric literal (`f32` / `i64`).
//! - The `world.tick` namespace field (resolves as
//!   `NamespaceField { ns: World, field: "tick", ty: U64 }`).
//!
//! Both literal and `world.tick` sources let the emitter generate a
//! self-driving `record_tick` that fires every metric automatically.
//! Authoring shapes the emitter doesn't yet recognise (aggregates
//! like `count(e in events.this_tick where ...)`, view calls,
//! quantifiers, conditioned_on / alert_when bodies) get a
//! per-metric `record_<name>` setter the runtime fills in, plus a
//! `// SKIP <reason>` comment so authors see the gap at build time
//! instead of runtime.
//!
//! # Why a separate function (not `emit_cg_program`)
//!
//! Metrics, like invariants, don't correspond to CG ops, kernels, or
//! the schedule — they're host-side telemetry over runtime state.
//! They live outside [`crate::cg::CgProgram`] (which carries only
//! kernel-bound IR), so the emit pass takes the [`Compilation`]
//! directly. The companion entry point
//! [`emit_cg_program_with_invariants_and_metrics`] wraps
//! [`super::invariants::emit_cg_program_with_invariants`] and injects
//! the synthesized `metrics.rs` into the artifact map alongside the
//! `invariants.rs` artifact.

use std::fmt::Write as _;

use dsl_ast::ir::{Compilation, IrExpr, MetricIR, NamespaceId};

use super::invariants::emit_cg_program_with_invariants;
use super::program::{EmittedArtifacts, ProgramEmitError};
use crate::cg::program::CgProgram;
use crate::cg::schedule::synthesis::ComputeSchedule;

/// Synthesise the per-fixture `metrics.rs` source. Returns `None` when
/// the compilation declares no metrics (so callers can skip the
/// artifact entirely instead of writing an empty file).
pub fn synthesize_metrics(comp: &Compilation) -> Option<String> {
    if comp.metrics.is_empty() {
        return None;
    }

    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Source: each `metric <name> = <expr> [window N] [emit_every N]\n");
    out.push_str("//          [conditioned_on EXPR] [alert when EXPR]` declaration in\n");
    out.push_str("// the fixture's .sim file. Regenerate by editing the .sim file and\n");
    out.push_str("// rebuilding.\n\n");

    out.push_str("/// Monotonic counter — accumulates a sum + sample count across\n");
    out.push_str("/// every recorded value. The sum is f64 to keep precision over\n");
    out.push_str("/// long runs; per-sample values stay f32 to match the metric IR's\n");
    out.push_str("/// scalar value type. `count` is the number of recorded samples\n");
    out.push_str("/// (not the cumulative event count fed into a `count(...)` body).\n");
    out.push_str("#[derive(Clone, Debug, Default)]\n");
    out.push_str("pub struct Counter {\n");
    out.push_str("    pub count: u64,\n");
    out.push_str("    pub total: f64,\n");
    out.push_str("}\n");
    out.push_str("impl Counter {\n");
    out.push_str("    pub fn record(&mut self, value: f32) {\n");
    out.push_str("        self.count += 1;\n");
    out.push_str("        self.total += value as f64;\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("/// Last-write-wins gauge — tracks the most recent recorded value\n");
    out.push_str("/// plus the total number of samples for sanity-checking. `last` is\n");
    out.push_str("/// `None` until the first `record()` call.\n");
    out.push_str("#[derive(Clone, Debug, Default)]\n");
    out.push_str("pub struct Gauge {\n");
    out.push_str("    pub last: Option<f32>,\n");
    out.push_str("    pub count: u64,\n");
    out.push_str("}\n");
    out.push_str("impl Gauge {\n");
    out.push_str("    pub fn record(&mut self, value: f32) {\n");
    out.push_str("        self.last = Some(value);\n");
    out.push_str("        self.count += 1;\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("/// Per-sample histogram — records every value into a `Vec`. The\n");
    out.push_str("/// runtime can compute bucketed views (max_bin etc.) off the raw\n");
    out.push_str("/// samples; this emit pass keeps the storage shape simple so the\n");
    out.push_str("/// per-tick record path is allocation-free after the initial Vec\n");
    out.push_str("/// capacity is sized.\n");
    out.push_str("#[derive(Clone, Debug, Default)]\n");
    out.push_str("pub struct Histogram {\n");
    out.push_str("    pub samples: Vec<f32>,\n");
    out.push_str("}\n");
    out.push_str("impl Histogram {\n");
    out.push_str("    pub fn record(&mut self, value: f32) {\n");
    out.push_str("        self.samples.push(value);\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // MetricsSink struct — one field per metric, of the type matching
    // the metric's wrapper kind (or `Gauge` as the default for bare
    // scalars / unsupported shapes). Every field is `pub` so tests
    // and runtime introspection can read the accumulated values
    // directly without a per-field accessor proliferation.
    out.push_str("/// Per-fixture metrics sink. One field per `metric` declaration\n");
    out.push_str("/// in the source `.sim` file. Construct via `Default::default()`\n");
    out.push_str("/// and feed values via `record_tick(world_tick)` for the auto-\n");
    out.push_str("/// driven metrics, or per-metric `record_<name>(value)` setters\n");
    out.push_str("/// for the ones whose value the runtime computes itself.\n");
    out.push_str("#[derive(Clone, Debug, Default)]\n");
    out.push_str("pub struct MetricsSink {\n");
    for m in &comp.metrics {
        let kind_name = match classify(m).kind {
            MetricKind::Counter => "Counter",
            MetricKind::Gauge | MetricKind::Unknown => "Gauge",
            MetricKind::Histogram => "Histogram",
        };
        let _ = writeln!(out, "    pub {name}: {ty},", name = m.name, ty = kind_name);
    }
    out.push_str("}\n\n");

    out.push_str("impl MetricsSink {\n");

    // record_tick — auto-drives the metrics whose value source the
    // emitter recognises (Constant / WorldTick). Metrics whose
    // source falls into the Skipped / Computed bucket get a
    // per-metric setter and are NOT touched here; the runtime drives
    // them.
    out.push_str("    /// Auto-drive every metric whose value source the compiler\n");
    out.push_str("    /// recognises. Call this from the per-fixture runtime once per\n");
    out.push_str("    /// tick; the runtime is also responsible for the per-metric\n");
    out.push_str("    /// `record_<name>(value)` setters for metrics whose source is\n");
    out.push_str("    /// computed host-side (those carry a `// SKIP` annotation in\n");
    out.push_str("    /// the per-metric setter docs).\n");
    out.push_str("    pub fn record_tick(&mut self, world_tick: u64) {\n");
    let mut auto_driven_any = false;
    for m in &comp.metrics {
        let cls = classify(m);
        let driver_value = match (&cls.source, &cls.kind) {
            (MetricSource::Constant(v), _) => Some(format!("{v:?}_f32")),
            (MetricSource::WorldTick, _) => Some("world_tick as f32".to_string()),
            _ => None,
        };
        if let (Some(value_expr), kind) = (driver_value, cls.kind) {
            // Honor `emit_every`: only record on ticks where
            // `world_tick % emit_every == 0`. The default is every
            // tick (`emit_every = 1`) when the modifier is absent.
            let emit_every = m.emit_every.unwrap_or(1).max(1);
            if matches!(kind, MetricKind::Counter | MetricKind::Gauge | MetricKind::Histogram | MetricKind::Unknown) {
                auto_driven_any = true;
                if emit_every == 1 {
                    let _ = writeln!(
                        out,
                        "        self.{name}.record({value});",
                        name = m.name,
                        value = value_expr,
                    );
                } else {
                    let _ = writeln!(
                        out,
                        "        if world_tick % {modulus} == 0 {{ self.{name}.record({value}); }}",
                        name = m.name,
                        modulus = emit_every,
                        value = value_expr,
                    );
                }
            }
        }
    }
    if !auto_driven_any {
        out.push_str("        // No auto-driven metrics in this fixture; every metric's\n");
        out.push_str("        // value source is computed host-side and recorded via the\n");
        out.push_str("        // per-metric `record_<name>` setters below.\n");
        out.push_str("        let _ = world_tick;\n");
    }
    out.push_str("    }\n\n");

    // Per-metric setters. Auto-driven metrics still get a setter so
    // hand-written tests + alternative drivers can override the
    // auto-tick behaviour; computed metrics REQUIRE the setter
    // because record_tick won't touch them.
    for m in &comp.metrics {
        emit_metric_setter(m, &mut out);
        out.push('\n');
    }

    out.push_str("}\n");

    Some(out)
}

/// Wrap [`emit_cg_program_with_invariants`] so callers (per-fixture
/// `build.rs`) get the `metrics.rs` artifact alongside the
/// `invariants.rs` + kernel + cross-cutting set without duplicating
/// the emit-and-merge boilerplate. Existing callers of
/// [`emit_cg_program_with_invariants`] are untouched; they get the
/// same artifact set (no `metrics.rs`, since metrics live outside the
/// schedule + invariants inject independently).
pub fn emit_cg_program_with_invariants_and_metrics(
    schedule: &ComputeSchedule,
    prog: &CgProgram,
    comp: &Compilation,
) -> Result<EmittedArtifacts, ProgramEmitError> {
    let mut artifacts = emit_cg_program_with_invariants(schedule, prog, comp)?;
    if let Some(src) = synthesize_metrics(comp) {
        artifacts.rust_files.insert("metrics.rs".to_string(), src);
    }
    Ok(artifacts)
}

