/// The set of emitted source artifacts produced by [`emit_cg_program`].
///
/// File-name keys carry the basename only (e.g. `fused_mask.wgsl`); the
/// xtask resolves them under `crates/engine_gpu_rules/src/`. Both maps
/// use [`BTreeMap`] so iteration is deterministic across runs.
#[derive(Debug, Clone, Default, Eq, PartialEq)]
pub struct EmittedArtifacts {
    /// Per-kernel WGSL file content. Key is the output filename
    /// (`<kernel_name>.wgsl`); value is the full file contents.
    pub wgsl_files: BTreeMap<String, String>,
    /// Per-kernel Rust module content. Key is the output filename
    /// (`<kernel_name>.rs`); value is the full file contents.
    pub rust_files: BTreeMap<String, String>,
    /// Index of all kernels in schedule order — each entry is the
    /// kernel's snake_case name. Useful for building a `mod.rs` /
    /// `lib.rs` registry (Task 5.1).
    pub kernel_index: Vec<String>,
}

/// Errors that can surface from [`emit_cg_program`]. Every variant
/// carries typed payload — no free-form `String` reasons.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ProgramEmitError {
    /// A [`KernelTopology`] failed to lower to a [`KernelSpec`]. The
    /// `(stage_index, kernel_index)` locates the failing kernel inside
    /// the schedule.
    KernelLowering {
        stage_index: usize,
        kernel_index: usize,
        error: KernelEmitError,
    },
    /// Two kernels in the same schedule produced the same name. With
    /// Task 5.2's semantic naming distinct ops with distinct semantic
    /// identities (mask name, view name, rule name, etc.) produce
    /// distinct kernel names; this branch only fires on a real
    /// ambiguity — e.g. two ops referencing the same mask id, or two
    /// fused kernels whose first op produces the same name. The
    /// collision is surfaced as a typed error rather than silently
    /// overwriting one of the files.
    KernelNameCollision {
        name: String,
        first_at: (usize, usize),
        second_at: (usize, usize),
    },
}

impl fmt::Display for ProgramEmitError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            ProgramEmitError::KernelLowering {
                stage_index,
                kernel_index,
                error,
            } => write!(
                f,
                "kernel emit failed at stage[{stage_index}].kernels[{kernel_index}]: {error}"
            ),
            ProgramEmitError::KernelNameCollision {
                name,
                first_at,
                second_at,
            } => write!(
                f,
                "kernel name collision: `{name}` first emitted at \
                 stage[{}].kernels[{}], collided at stage[{}].kernels[{}]",
                first_at.0, first_at.1, second_at.0, second_at.1
            ),
        }
    }
}

impl std::error::Error for ProgramEmitError {}

