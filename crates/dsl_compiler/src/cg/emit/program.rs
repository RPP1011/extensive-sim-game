//! Full-program emission — Task 4.3.
//!
//! Walks every kernel in a [`ComputeSchedule`] and produces the file
//! set the xtask writes into `crates/engine_gpu_rules/src/`. Each
//! kernel becomes one `.wgsl` file (the WGSL kernel source) plus one
//! `.rs` file (the Rust kernel module that owns the pipeline, BGL,
//! `Bindings` struct, `bind()`, and `build_cfg()`).
//!
//! # Pipeline
//!
//! ```text
//!   ComputeSchedule  ─┐
//!                     ├─►  emit_cg_program  ─►  EmittedArtifacts
//!   CgProgram        ─┘                           ├─ wgsl_files: BTreeMap<String, String>
//!                                                 ├─ rust_files: BTreeMap<String, String>
//!                                                 └─ kernel_index: Vec<String>
//! ```
//!
//! For each `(stage, kernel)` pair this calls Task 4.2's
//! [`kernel_topology_to_spec_and_body`] to obtain the `(KernelSpec,
//! body)` pair, then runs the four
//! [`crate::kernel_lowerings::lower_*`] helpers — drift across the
//! WGSL and Rust outputs is structurally impossible because every
//! string derives from the same [`KernelSpec`].
//!
//! # Determinism
//!
//! Outputs are byte-identical across runs:
//! - [`BTreeMap`] preserves sorted iteration order.
//! - The schedule iteration is deterministic (Task 3.x guarantees a
//!   stable stage list).
//! - All inner lowerings ([`crate::kernel_lowerings`], Task 4.1's
//!   walks, Task 4.2's spec lowering) are deterministic.
//!
//! The `program_emit_is_deterministic` test pins the contract.
//!
//! # Limitations
//!
//! - **Inherits Task 4.2's body limitations.** Placeholder bodies for
//!   [`crate::cg::op::ComputeOpKind::ScoringArgmax`],
//!   [`crate::cg::op::ComputeOpKind::SpatialQuery`], and
//!   [`crate::cg::op::ComputeOpKind::Plumbing`] are emitted as
//!   documented `// TODO(task-X.Y)` comment lines, not panics. The cfg
//!   uniform's struct shape is a placeholder
//!   `{ agent_cap: u32, _pad: [u32; 3] }`.
//! - **Kernel names are semantic** (`fused_mask`, `scoring`,
//!   `fold_<view>`, `physics_<rule>`, `spatial_<query>`,
//!   `alive_pack` / `seed_indirect_<ring>` / …). See
//!   [`super::kernel::semantic_kernel_name`] for the full mapping.
//! - **`Kernel` trait `impl`, `lib.rs`, and `bgl_storage` /
//!   `bgl_uniform` helpers are emitted as of Task 5.2.** The
//!   `lib.rs` content is keyed under `"lib.rs"` in
//!   [`EmittedArtifacts::rust_files`] (xtask writes it as
//!   `<src>/lib.rs`). The trait `impl` body uses
//!   `pass.dispatch_workgroups(...)` derived from the topology's
//!   [`DispatchShape::workgroup_size`]; indirect-dispatch wiring (real
//!   `dispatch_workgroups_indirect`) is a Task 5.4+ concern — today the
//!   record body emits the agent-cap-bounded direct dispatch the
//!   legacy emitters use as their stub.
//! - **Output file format approximates legacy emit; exact byte-for-byte
//!   parity with the hand-written `crates/engine_gpu_rules/src/<k>.{rs,wgsl}`
//!   files is later cross-cutting work (Task 5.4+).** Differences in
//!   this commit:
//!   - Header comment is `// GENERATED by dsl_compiler — do not edit
//!     by hand.` rather than the legacy
//!     `// GENERATED by dsl_compiler::emit_<...>. ...`.
//!   - The `Kernel::new` body uses `include_str!("<name>.wgsl")` for
//!     the WGSL source — works when xtask writes both files into the
//!     same directory.
//! - **`kernel_index` is a flat `Vec`.** The synthesized `lib.rs`
//!   walks it to produce the `mod <name>;` + `pub use` lines.
//! - **Name-collision detection is a defensive safety net.** Distinct
//!   ops with distinct semantic identities (mask name, view name,
//!   rule name) produce distinct kernel names. The collision check
//!   only fires on a real ambiguity — e.g. two ops referencing the
//!   same mask id — and surfaces as a typed error rather than
//!   silently overwriting.

