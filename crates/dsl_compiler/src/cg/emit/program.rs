/// Compose a single `.wgsl` file from a [`KernelSpec`] + body string +
/// originating [`KernelTopology`] (used to derive the workgroup size).
///
/// The output mirrors the legacy WGSL files' shape:
/// 1. A `// GENERATED ...` header comment.
/// 2. The `@group(0) @binding(N)` declaration block from
///    [`lower_wgsl_bindings`].
/// 3. The `@compute @workgroup_size(N)` annotation, derived from the
///    topology's [`DispatchShape::workgroup_size`].
/// 4. The entry-point function with its `gid` argument and the body
///    from Task 4.2.
fn compose_wgsl_file(
    spec: &KernelSpec,
    body: &str,
    topology: &KernelTopology,
    prog: &CgProgram,
) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push('\n');

    // The cfg uniform is referenced by name (e.g. `cfg: MaskHoldCfg`),
    // so the WGSL must declare the matching struct inline before the
    // uniform binding. Shape mirrors the Rust cfg_struct_decl:
    //   ViewFold:  { event_count, tick, _pad0, _pad1 }
    //   Generic:   { agent_cap, _pad0, _pad1, _pad2 }
    // (WGSL doesn't accept `[u32; N]` array members in uniforms the way
    // Rust does; the padding is unrolled into scalar `_padN` fields.)
    out.push_str(&compose_wgsl_cfg_struct(spec));
    out.push('\n');

    out.push_str(&lower_wgsl_bindings(spec));
    out.push('\n');

    // Emit `const config_<id>: f32 = <value>;` for every config-const
    // referenced in the kernel body. The substring scan keys on
    // `config_<id>` (the WGSL identifier `lower_cg_expr_to_wgsl`
    // produces for `CgExpr::Read(DataHandle::ConfigConst { id })`).
    // Today every config field defaults to a numeric literal (Boids'
    // `flock` block), so a single-pass `f32` emission covers it; non-
    // numeric defaults skip silently because no kernel references
    // them. Sorted by id for deterministic output.
    let mut emitted_consts = false;
    for (id, value) in &prog.config_const_values {
        let needle = format!("config_{}", id);
        if body.contains(&needle) {
            out.push_str(&format!("const config_{}: f32 = {:?};\n", id, value));
            emitted_consts = true;
        }
    }
    if emitted_consts {
        out.push('\n');
    }

    // Spatial-grid prelude: constants + pos_to_cell / cell_index
    // helpers, emitted only when the body touches a spatial binding.
    // The same constants are mirrored on the runtime side
    // (`crate::cg::emit::spatial::{CELL_SIZE,WORLD_HALF_EXTENT,...}`)
    // so per-fixture runtime crates can size matching wgpu buffers
    // without re-deriving the values from the WGSL.
    let spatial_prelude = super::spatial::compose_spatial_prelude(body);
    if !spatial_prelude.is_empty() {
        out.push_str(&spatial_prelude);
    }

    // Emit B1-stub prelude functions for every namespace method whose
    // WGSL function name appears in this kernel's body. The registry
    // (`prog.namespace_registry`) is the source of truth for both the
    // function name (used for the substring scan) and the stub body
    // (emitted verbatim). Walk-and-collect rather than emit-all so the
    // generated WGSL stays minimal and the kernel-by-kernel diff is
    // legible. See `populate_namespace_registry` in `lower::driver`
    // for the registered methods.
    let prelude = compose_namespace_prelude(body, prog);
    if !prelude.is_empty() {
        out.push_str(&prelude);
        out.push('\n');
    }

    // PerCell topologies (today only the tiled-MoveBoid kernel)
    // need module-level `var<workgroup>` declarations for the
    // cooperative tile arrays — they're shared across all lanes of
    // the workgroup but addressed per-lane inside the kernel body.
    // Sized for a 27-cell × MAX_PER_CELL neighborhood (= 864 entries
    // for boids' defaults). The kernel preamble + per-fold inner
    // walk both reference these symbols by name (`tile_pos`,
    // `tile_vel`, `tile_count`); emitting the decls here keeps them
    // alongside the matching `@compute @workgroup_size` header
    // without smuggling a `var<workgroup>` mid-body (illegal in WGSL).
    let dispatch_shape = topology_dispatch_shape(topology);
    let is_per_cell = matches!(dispatch_shape, DispatchShape::PerCell);
    let is_per_scan_chunk = matches!(dispatch_shape, DispatchShape::PerScanChunk);
    if is_per_scan_chunk {
        // PerScanChunk topologies (parallel prefix-scan kernels)
        // share a 256-entry workgroup-local buffer holding the
        // Hillis-Steele scan working set. Sized to match
        // `PER_SCAN_CHUNK_WORKGROUP_X` so every lane has exactly
        // one slot — keep the constant in sync if the chunk size
        // is ever bumped.
        let chunk = crate::cg::dispatch::PER_SCAN_CHUNK_WORKGROUP_X;
        out.push_str(&format!(
            "// Workgroup-local scratch for the Hillis-Steele scan.\n\
             // One u32 per lane; total = {chunk} × 4 = {bytes} B.\n\
             var<workgroup> scan_shared: array<u32, {chunk}>;\n\n",
            chunk = chunk,
            bytes = chunk * 4,
        ));
    }
    if is_per_cell {
        // The tile-array size derives from `MAX_PER_CELL` so a knob
        // change in `crate::cg::emit::spatial` flows through here
        // without a separate edit. The 27 stays a literal — it's a
        // structural property of "3³ neighborhood" the tile-walk emit
        // hardcodes too, and surfacing it as a constant would just
        // make both sites read the same magic number.
        let max_per_cell = super::spatial::MAX_PER_CELL;
        let total_slots = 27 * max_per_cell;
        out.push_str(&format!(
            "// Workgroup-local tile arrays for the 27-cell neighborhood.\n\
             // Sized for SPATIAL_MAX_PER_CELL (= {max}) slots per cell × 27\n\
             // cells = {total} entries each. tile_count holds the per-cell\n\
             // population; tile_pos/tile_vel mirror the agent fields the\n\
             // body's projections read via per_pair_candidate.\n\
             var<workgroup> tile_count: array<u32, 27>;\n\
             var<workgroup> tile_pos: array<vec3<f32>, 27 * {max}>;\n\
             var<workgroup> tile_vel: array<vec3<f32>, 27 * {max}>;\n\n",
            max = max_per_cell,
            total = total_slots,
        ));
    }

    let workgroup_x = topology_workgroup_size_x(topology);

    if is_per_cell || is_per_scan_chunk {
        // PerCell + PerScanChunk entry points: take `workgroup_id`
        // + `local_invocation_id` instead of the usual
        // `global_invocation_id`. The body's per-shape preamble
        // (PerCell: `tiled_per_cell_preamble`; PerScanChunk:
        // `thread_indexing_preamble`) reads these to derive its
        // working indices.
        out.push_str(&format!(
            "@compute @workgroup_size({workgroup_x})\n\
             fn {entry}(\n\
             \x20   @builtin(workgroup_id) workgroup_id: vec3<u32>,\n\
             \x20   @builtin(local_invocation_id) local_invocation_id: vec3<u32>,\n\
             ) {{\n",
            entry = spec.entry_point
        ));
    } else {
        out.push_str(&format!(
            "@compute @workgroup_size({workgroup_x})\n\
             fn {entry}(@builtin(global_invocation_id) gid: vec3<u32>) {{\n",
            entry = spec.entry_point
        ));
    }
    out.push_str(body);
    if !body.ends_with('\n') {
        out.push('\n');
    }
    out.push_str("}\n");

    out
}

/// Pull the dispatch shape off a topology — handles all variants by
/// returning the per-stage shape stored on the topology. Used by
/// `compose_wgsl_file` to switch between the standard
/// `global_invocation_id` entry point and the PerCell
/// `(workgroup_id, local_invocation_id)` form.
fn topology_dispatch_shape(topology: &KernelTopology) -> DispatchShape {
    match topology {
        KernelTopology::Fused { dispatch, .. } => *dispatch,
        KernelTopology::Split { dispatch, .. } => *dispatch,
        KernelTopology::Indirect { .. } => DispatchShape::PerEvent {
            source_ring: crate::cg::data_handle::EventRingId(0),
        },
    }
}

/// Walk the kernel `body` for substrings naming registered namespace-
/// method WGSL function names; emit the matching B1-stub prelude
/// declarations in registry order. Returns an empty string when no
/// method is referenced.
///
/// The substring scan looks for `<wgsl_fn_name>(` (function call
/// syntax) so a comment that incidentally names the function doesn't
/// trigger a stub declaration. Order is iteration order over the
/// namespace registry's `BTreeMap`s — deterministic across runs.
fn compose_namespace_prelude(body: &str, prog: &CgProgram) -> String {
    let mut out = String::new();
    for ns_def in prog.namespace_registry.namespaces.values() {
        for method_def in ns_def.methods.values() {
            let needle = format!("{}(", method_def.wgsl_fn_name);
            if body.contains(&needle) {
                out.push_str(&method_def.wgsl_stub);
                out.push('\n');
            }
        }
    }
    out
}

/// Emit the WGSL `struct {Pascal}Cfg { ... };` declaration kernels
/// reference from the cfg uniform binding. Sourced from the cfg
/// binding's WGSL type name; field shape branches on
/// [`KernelKind`] to mirror the Rust [`KernelSpec::cfg_struct_decl`]:
///   - `ViewFold`: `{ event_count, tick, _pad0, _pad1 }`
///   - `Generic`:  `{ agent_cap, _pad0, _pad1, _pad2 }`
///
/// WGSL doesn't accept `[u32; N]` array members in uniform-shaped
/// structs the same way Rust does, so the padding is unrolled into
/// scalar `_padN` fields.
///
/// # Limitations
///
/// - **Field shape is hard-coded per `KernelKind`.** As more kernel
///   kinds land with bespoke cfgs (per-kind refinements anticipated by
///   `build_generic_cfg_struct_decl`'s comment), the right move is to
///   surface a `cfg_wgsl_decl` field from the kernel-spec layer rather
///   than branching here.
fn compose_wgsl_cfg_struct(spec: &KernelSpec) -> String {
    let cfg = spec
        .bindings
        .iter()
        .find(|b| matches!(b.bg_source, BgSource::Cfg))
        .expect("KernelSpec must carry a cfg binding");
    // Generic-kernel cfg gained `tick: u32` when PerAgent rules with
    // `emit <Event>` bodies started writing the tick header word into
    // the event ring. Layout matches `build_generic_cfg_struct_decl`
    // (Rust side): { agent_cap: u32, tick: u32, _pad0: u32, _pad1: u32 }.
    let fields = match spec.kind {
        // `second_key_pop` joined the layout with the pair_map storage
        // gap fix (2026-05-03): fold body's RMW indexes
        // `view_storage_primary[k1 * cfg.second_key_pop + k2]` for
        // 2-D views; single-key views set the field to 1 so the index
        // reduces to `k_last`. Mirrors the Rust `FoldXxxCfg` struct
        // (build_view_fold_cfg_struct_decl).
        KernelKind::ViewFold => "event_count: u32, tick: u32, second_key_pop: u32, _pad0: u32",
        // `slot_count` joined the layout in lockstep — pair_map views
        // over-allocate `agent_cap × second_pop` slots; the decay
        // kernel iterates `slot_count` instead of `agent_cap` so the
        // anchor multiplier reaches every (k1, k2). Single-key views
        // set `slot_count == agent_cap`. Mirrors
        // build_view_decay_cfg_struct_decl.
        KernelKind::ViewDecay => "agent_cap: u32, tick: u32, slot_count: u32, _pad0: u32",
        KernelKind::Generic => "agent_cap: u32, tick: u32, _pad0: u32, _pad1: u32",
    };
    format!("struct {ty} {{ {fields} }};\n", ty = cfg.wgsl_ty)
}

/// Compose a single `.rs` file from a [`KernelSpec`] + originating
/// [`KernelTopology`] (used to derive the dispatch-workgroup expression
/// inside the [`crate::Kernel::record`] body).
///
/// The output mirrors the legacy Rust kernel modules' shape (see
/// `crates/engine_gpu_rules/src/alive_pack.rs` for a template), with
/// the differences documented in the module-level `# Limitations`. Each
/// section is sourced from one of the [`crate::kernel_lowerings`]
/// helpers, so a regression in any single emitted form is caught by the
/// lowering's own tests; this composer is structural glue only.
///
/// The synthesized module is `crate::Kernel`-implementing: the
/// `<Pascal>Kernel` struct, `<Pascal>Bindings<'a>`, the cfg struct, an
/// `impl crate::Kernel for <Pascal>Kernel { … }` block (real bodies,
/// no panics), and the standalone helper fns (`<name>_bgl_entries`,
/// `<name>_bind_group_entries`, `<name>_build_cfg`) the legacy callers
/// expect.
///
/// # Limitations
/// - The `record()` body uses
///   `pass.dispatch_workgroups((agent_cap + N - 1) / N, 1, 1)` for
///   per-agent / per-event / per-pair / per-word kernels, and
///   `(1, 1, 1)` for one-shot. Indirect dispatch
///   (`dispatch_workgroups_indirect`) is a Task 5.4+ concern — the
///   legacy emitters today use the same agent-cap-bounded direct
///   dispatch as a placeholder for per-event kernels.
/// - The `bind()` body is built from the spec's [`BgSource`] tags via
///   [`render_bg_source_expr`]; ViewHandle / AliasOf are routed
///   through `unwrap_or(primary)` patterns that mirror the legacy fold
///   kernels' pattern. None of today's CG kernels emit those variants
///   (Task 5.4+ wires fold-view tuple destructuring); the helpers stay
///   in place defensively so the composer never panics.
fn compose_rust_module_file(spec: &KernelSpec, topology: &KernelTopology) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push('\n');

    // Phase 7 boids GPU pipeline (2026-05-02): kernel modules now
    // target `engine::gpu::Kernel` (hand-written platform trait) and
    // construct `Bindings` directly with explicit `&wgpu::Buffer`
    // refs. The deleted `bind()` method took a `&BindingSources`
    // aggregate that the wolf-sim runtime owned; per-fixture
    // runtimes now wire bindings inline at the dispatch call site.

    // Per-kernel struct (carries the pipeline + BGL).
    out.push_str(&format!(
        "pub struct {pascal}Kernel {{\n    \
         pipeline: wgpu::ComputePipeline,\n    \
         bgl: wgpu::BindGroupLayout,\n}}\n\n",
        pascal = spec.pascal
    ));

    // Bindings struct. ViewFold kernels emit `Option<&'a wgpu::Buffer>`
    // for the anchor/ids slots (matching the legacy fold-kernel
    // contract); generic kernels use the spec-driven lowering which
    // emits `&'a wgpu::Buffer` for every binding.
    out.push_str(&format!(
        "pub struct {pascal}Bindings<'a> {{\n",
        pascal = spec.pascal
    ));
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_bindings_struct_fields(spec));
    } else {
        out.push_str(&lower_rust_bindings_struct_fields(spec));
    }
    out.push_str("}\n\n");

    // Cfg struct decl — KernelSpec.cfg_struct_decl is the full
    // `#[repr(C)] ... pub struct <Cfg> { ... }` form.
    out.push_str(&spec.cfg_struct_decl);
    out.push_str("\n\n");

    // WGSL source — the `.wgsl` file is co-located in the output
    // directory; `include_str!` resolves it at compile time when the
    // emitted crate is built.
    out.push_str(&format!(
        "const SHADER_SRC: &str = include_str!(\"{name}.wgsl\");\n\n",
        name = spec.name
    ));

    // Kernel trait impl.
    out.push_str(&compose_kernel_trait_impl(spec, topology));
    out.push('\n');

    // BGL entries function. ViewFold kernels skip the standalone
    // `_bgl_entries` / `_bind_group_entries` / `_build_cfg` helpers —
    // the legacy `crates/engine_gpu_rules/src/fold_<view>.rs` modules
    // do not expose them, and the `lower_rust_bg_entries` lowering
    // would produce `Option`-typed expressions that don't carry
    // `.as_entire_binding()`. The Kernel trait impl above remains the
    // single entry point for ViewFold callers.
    if !matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&format!(
            "pub fn {name}_bgl_entries() -> Vec<wgpu::BindGroupLayoutEntry> {{\n    \
             vec![\n",
            name = spec.name
        ));
        out.push_str(&render_cg_bgl_entries(spec));
        out.push_str("    ]\n}\n\n");

        // Bind-group entries function. Returns the BindGroupEntry list a
        // caller wraps with the BGL into a BindGroup.
        out.push_str(&format!(
            "pub fn {name}_bind_group_entries<'a>(\n    \
             bindings: &'a {pascal}Bindings<'a>,\n) -> \
             Vec<wgpu::BindGroupEntry<'a>> {{\n    vec![\n",
            name = spec.name,
            pascal = spec.pascal
        ));
        out.push_str(&lower_rust_bg_entries(spec));
        out.push_str("    ]\n}\n\n");

        // Phase 7 boids GPU pipeline (2026-05-02): dropped the
        // `<name>_build_cfg(state: &engine::state::SimState)` standalone
        // helper. The cfg shape (`<Pascal>Cfg`) is `#[repr(C)]` POD;
        // per-fixture runtimes construct it directly with primitive
        // args at the dispatch call site (e.g.,
        // `PhysicsMoveBoidCfg { agent_cap: 16, _pad: [0; 3] }`).
    }

    out
}

/// Compose the `impl engine::gpu::Kernel for <Pascal>Kernel { … }`
/// block. The trait carries the minimum surface every kernel
/// implements: `new` (compile shader + build pipeline) + `record`
/// (encode dispatch). Per-kernel `build_cfg` and `bind` methods are
/// gone — `Cfg` is a `#[repr(C)]` POD that callers construct
/// directly, and `Bindings` is a typed struct wired inline at the
/// dispatch call site with explicit `&wgpu::Buffer` refs.
fn compose_kernel_trait_impl(spec: &KernelSpec, topology: &KernelTopology) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "impl engine::gpu::Kernel for {pascal}Kernel {{\n    \
         type Bindings<'a> = {pascal}Bindings<'a>;\n    \
         type Cfg = {cfg};\n\n",
        pascal = spec.pascal,
        cfg = spec.cfg_struct
    ));

    // new()
    out.push_str("    fn new(device: &wgpu::Device) -> Self {\n");
    out.push_str(&format!(
        "        let shader = device.create_shader_module(wgpu::ShaderModuleDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::wgsl\"),\n            \
             source: wgpu::ShaderSource::Wgsl(SHADER_SRC.into()),\n        \
             }});\n",
        name = spec.name
    ));
    out.push_str(&format!(
        "        let bgl = device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::bgl\"),\n            \
             entries: &[\n",
        name = spec.name
    ));
    out.push_str(&render_cg_bgl_entries(spec));
    out.push_str("            ],\n        });\n");
    out.push_str(&format!(
        "        let pl = device.create_pipeline_layout(&wgpu::PipelineLayoutDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::pl\"),\n            \
             bind_group_layouts: &[&bgl],\n            \
             push_constant_ranges: &[],\n        \
             }});\n",
        name = spec.name
    ));
    out.push_str(&format!(
        "        let pipeline = device.create_compute_pipeline(&wgpu::ComputePipelineDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::pipeline\"),\n            \
             layout: Some(&pl),\n            \
             module: &shader,\n            \
             entry_point: Some(\"{entry}\"),\n            \
             compilation_options: Default::default(),\n            \
             cache: None,\n        \
             }});\n        \
             Self {{ pipeline, bgl }}\n    }}\n\n",
        name = spec.name,
        entry = spec.entry_point
    ));

    // build_cfg + bind dropped from the trait — see compose_kernel_trait_impl
    // doc. `Cfg` is `#[repr(C)]` POD (caller constructs directly);
    // `Bindings` wired inline at dispatch with explicit buffer refs.

    // record()
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_record_method(spec));
    } else {
        // OneShot dispatch resolves to `pass.dispatch_workgroups(1, 1, 1)`
        // and never references `agent_cap` — emit the parameter as
        // `_agent_cap` so the synthesized kernel module compiles under
        // `-D unused-variables`. PerAgent / PerEvent / PerPair / PerWord
        // all derive workgroup count from `agent_cap`. Mirrors the
        // legacy `seed_indirect.rs` convention.
        let cap_param = if dispatch_is_one_shot(topology) {
            "_agent_cap"
        } else {
            "agent_cap"
        };
        out.push_str(&format!(
            "    fn record(&self, device: &wgpu::Device, encoder: &mut wgpu::CommandEncoder, bindings: &{pascal}Bindings<'_>, {cap_param}: u32) {{\n",
            pascal = spec.pascal,
            cap_param = cap_param,
        ));
        out.push_str(&format!(
            "        let bg = device.create_bind_group(&wgpu::BindGroupDescriptor {{\n            \
                 label: Some(\"engine_gpu_rules::{name}::bg\"),\n            \
                 layout: &self.bgl,\n            \
                 entries: &[\n",
            name = spec.name
        ));
        out.push_str(&lower_rust_bg_entries(spec));
        out.push_str("            ],\n        });\n");
        out.push_str(&format!(
            "        let mut pass = encoder.begin_compute_pass(&wgpu::ComputePassDescriptor {{\n            \
                 label: Some(\"engine_gpu_rules::{name}::pass\"),\n            \
                 timestamp_writes: None,\n        \
                 }});\n",
            name = spec.name
        ));
        out.push_str("        pass.set_pipeline(&self.pipeline);\n");
        out.push_str("        pass.set_bind_group(0, &bg, &[]);\n");
        out.push_str(&compose_dispatch_call(topology));
        out.push_str("    }\n");
    }

    out.push_str("}\n");
    out
}

/// Render the BGL entries for a CG-emitted kernel, routing through the
/// CG-side helpers `engine::gpu::bgl_storage` / `engine::gpu::bgl_uniform` defined
/// in the synthesized `lib.rs`. Distinct from
/// [`crate::kernel_lowerings::lower_rust_bgl_entries`] which routes
/// through the legacy `crate::fused_mask::bgl_*` path; the legacy
/// path requires the legacy `fused_mask.rs` to define the helpers,
/// which a CG-only output never carries.
fn render_cg_bgl_entries(spec: &KernelSpec) -> String {
    let mut out = String::new();
    for b in &spec.bindings {
        let descriptor = match b.access {
            AccessMode::ReadStorage => format!("engine::gpu::bgl_storage({}, true)", b.slot),
            AccessMode::ReadWriteStorage | AccessMode::AtomicStorage => {
                format!("engine::gpu::bgl_storage({}, false)", b.slot)
            }
            AccessMode::Uniform => format!("engine::gpu::bgl_uniform({})", b.slot),
        };
        writeln!(out, "                {}, // {}", descriptor, b.name)
            .expect("write to String never fails");
    }
    out
}

/// Render the `bindings: <field>: <expr>,` rows for the `bind()`
/// initializer body.
#[allow(dead_code)]
fn compose_bind_body(spec: &KernelSpec) -> String {
    let mut out = String::new();
    for b in &spec.bindings {
        // AliasOf bindings don't have their own struct field; the alias
        // is handled in `lower_rust_bg_entries` at BindGroupEntry time.
        if matches!(b.bg_source, BgSource::AliasOf(_)) {
            continue;
        }
        let value = render_bg_source_expr(&b.bg_source, &b.name);
        out.push_str(&format!("            {field}: {value},\n", field = b.name, value = value));
    }
    out
}

/// Render a [`BgSource`] as the Rust expression that yields the
/// `&'a wgpu::Buffer` used by the `bind()` initializer.
///
/// `field` is the binding's name — used to surface a self-referential
/// fallback for ViewHandle (the generic CG path; today no kernel emits
/// it, so the helper stays defensive).
#[allow(dead_code)]
fn render_bg_source_expr(src: &BgSource, field: &str) -> String {
    match src {
        // `ResidentPathContext` fields are owned `wgpu::Buffer`s (not
        // references). Borrow at the use-site so the synthesized
        // `Bindings { ... }` initializer matches the `&'a wgpu::Buffer`
        // field type. The legacy ViewFold emit path bypasses this
        // helper for its event_ring/event_tail bindings (it constructs
        // the borrow inline at `compose_view_fold_bindings_init`).
        BgSource::Resident(f) => format!("&sources.resident.{f}"),
        BgSource::Transient(f) => format!("sources.transient.{f}"),
        BgSource::External(f) => format!("sources.external.{f}"),
        // `Pool` fields are owned `wgpu::Buffer`s (not references) on
        // the runtime `Pool` struct, unlike `TransientHandles` whose
        // fields are `&'a wgpu::Buffer`. Borrow at the use-site so
        // the synthesized `Bindings { ... }` initializer matches the
        // `&'a wgpu::Buffer` field type.
        BgSource::Pool(f) => format!("&sources.pool.{f}"),
        BgSource::Cfg => "cfg".to_string(),
        BgSource::ViewHandle { accessor, tuple_idx } => {
            // Defensive: until Task 5.4 wires fold-view tuple
            // destructuring at the kernel-module emit layer, the
            // generic path renders a `<accessor>().<idx>`-style
            // expression. The legacy fold kernels destructure into
            // (primary, anchor, ids) locals; today no CG kernel emits
            // ViewHandle — this arm exists so the composer never falls
            // through to a panic if a future spec does.
            format!(
                "sources.resident.{accessor}().{idx} /* {field} */",
                accessor = accessor,
                idx = tuple_idx
            )
        }
        BgSource::AliasOf(target) => {
            // Defensive — AliasOf bindings are filtered out before this
            // helper is called. If a caller skipped the filter, render
            // a self-evidently routable expression rather than panic.
            format!("/* alias of {target} */ sources.external.sim_cfg")
        }
    }
}

