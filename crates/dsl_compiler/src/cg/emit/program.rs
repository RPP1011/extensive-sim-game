/// Build the synthesized `lib.rs` content for the CG-emitted crate.
///
/// `kernel_index` is the schedule-order list of snake_case kernel names
/// (one per emitted module). The output declares each module, re-exports
/// the `<Pascal>Kernel` struct, defines the [`crate::Kernel`] trait,
/// the `KernelId` registry enum, the `BufferRef` enum, and the
/// `bgl_storage` / `bgl_uniform` helper fns every per-kernel module
/// invokes as `engine::gpu::bgl_storage(...)` / `engine::gpu::bgl_uniform(...)`.
///
/// The CG-emitted kernel modules use [`render_cg_bgl_entries`] (NOT
/// the legacy `lower_rust_bgl_entries` which routes via
/// `crate::fused_mask::bgl_*`) so the helper paths align with the
/// top-level helpers synthesized here.
///
/// # Limitations
/// - **`mod binding_sources;` is declared but `binding_sources.rs` is
///   not emitted today.** The CG pipeline emits per-kernel modules
///   only; `binding_sources.rs` is hand-authored in the legacy crate
///   and a Task 5.4+ concern for the CG side. A consumer overlaying
///   the CG output on the legacy crate inherits the legacy
///   `binding_sources.rs`; a fresh CG-only crate would need a stub.
/// - **`schedule`, `resident_context`, `pingpong_context`, `pool`,
///   `transient_handles`, `external_buffers` modules are NOT
///   declared.** The legacy `lib.rs` emits them; the CG pipeline does
///   not yet, but every emitted kernel imports `BindingSources` from
///   `binding_sources` (which transitively pulls in the rest). Task
///   5.4+ extends.
/// - **`KernelId` variants are PascalCase derived from
///   [`snake_to_pascal`].** Two distinct snake names that pascalize to
///   the same identifier (e.g. `foo_bar` and `foo__bar`) would collide
///   here; today the [`super::kernel::semantic_kernel_name`] mapping
///   keeps every name's snake form unique, so pascal collisions are
///   structurally impossible. The synthesized helper has no defensive
///   collision check — adding one is cheap if a future renaming
///   policy makes the invariant fragile.
pub fn synthesize_lib_rs(kernel_index: &[String]) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push('\n');
    out.push_str("#![allow(clippy::all)]\n");
    out.push_str("#![allow(unused_imports)]\n");
    // Per-mask kernel suffixes carry the mask's interned name verbatim
    // from the .sim source (e.g. `Flee`, `Hold`, `MoveToward`); those
    // PascalCase fragments propagate into module / function identifiers
    // (`mask_Hold`, `fused_mask_Flee_bgl_entries`). Properly snake-casing
    // these requires walking interner names through a casing pass —
    // out of scope for the runtime-contract rename. Until then,
    // suppress the lint at crate scope so `RUSTFLAGS=-D warnings`
    // builds pass (matches the legacy emit's blanket
    // `#![allow(clippy::all)]` style).
    out.push_str("#![allow(non_snake_case)]\n");
    out.push('\n');

    // Per-kernel module declarations + re-exports.
    for name in kernel_index {
        out.push_str(&format!("pub mod {name};\n"));
    }
    out.push('\n');
    for name in kernel_index {
        let pascal = snake_to_pascal(name);
        out.push_str(&format!("pub use {name}::{pascal}Kernel;\n"));
    }
    out.push('\n');

    // Per-fixture compiler-output modules. Phase 7 boids GPU pipeline
    // (2026-05-02): the wolf-sim runtime container modules
    // (binding_sources / resident_context / external_buffers /
    // transient_handles / pingpong_context / pool) are no longer
    // emitted — per-fixture runtimes own their own buffers and wire
    // bindings inline at dispatch.
    out.push_str("pub mod schedule;\n");
    out.push_str("pub mod dispatch;\n");
    out.push('\n');

    // KernelId enum.
    out.push_str("#[derive(Copy, Clone, Debug, Eq, PartialEq, Hash)]\n");
    out.push_str("pub enum KernelId {\n");
    for name in kernel_index {
        out.push_str(&format!("    {},\n", snake_to_pascal(name)));
    }
    out.push_str("}\n\n");

    // BufferRef enum (matches legacy lib.rs).
    out.push_str("#[derive(Copy, Clone, Debug, Eq, PartialEq, Hash)]\n");
    out.push_str("pub enum BufferRef {\n");
    out.push_str("    /// Resident path indirect-args buffer.\n");
    out.push_str("    ResidentIndirectArgs,\n");
    out.push_str("    /// Cascade-physics event ring tail buffer (gates further iterations when zero).\n");
    out.push_str("    CascadeRingTail,\n");
    out.push_str("}\n");

    // Phase 7 boids GPU pipeline (2026-05-02): the `Kernel` trait +
    // `bgl_storage` / `bgl_uniform` helpers were relocated to
    // `engine::gpu` (hand-written platform infrastructure). Per-kernel
    // modules `impl engine::gpu::Kernel` instead of `crate::Kernel`,
    // and use `engine::gpu::bgl_storage` / `engine::gpu::bgl_uniform`
    // instead of crate-local helpers. Hand-writing them once removes
    // per-fixture duplication.

    out
}

