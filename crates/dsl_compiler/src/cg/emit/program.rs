/// Emit the full set of WGSL + Rust source files for a
/// [`ComputeSchedule`].
///
/// Walks every kernel in every stage, lowers each to a `(KernelSpec,
/// body)` pair via Task 4.2's [`kernel_topology_to_spec_and_body`], and
/// composes the WGSL + Rust file contents using the four
/// [`crate::kernel_lowerings::lower_*`] helpers.
///
/// # Errors
///
/// - [`ProgramEmitError::KernelLowering`] — any kernel's spec lowering
///   raised a typed [`KernelEmitError`] (op-id out-of-range, empty
///   topology, inner-walk arena failure, spec-validation failure).
/// - [`ProgramEmitError::KernelNameCollision`] — two kernels in the
///   schedule produced the same snake_case name. (See `# Limitations`
///   on the module-level docs.)
pub fn emit_cg_program(
    schedule: &ComputeSchedule,
    prog: &CgProgram,
) -> Result<EmittedArtifacts, ProgramEmitError> {
    let mut wgsl_files: BTreeMap<String, String> = BTreeMap::new();
    let mut rust_files: BTreeMap<String, String> = BTreeMap::new();
    let mut kernel_index: Vec<String> = Vec::new();
    let mut seen_names: BTreeMap<String, (usize, usize)> = BTreeMap::new();

    let ctx = EmitCtx {
        prog,
        naming: HandleNamingStrategy::Structural,
    };

    for (stage_idx, stage) in schedule.stages.iter().enumerate() {
        for (kernel_idx, topology) in stage.kernels.iter().enumerate() {
            let (spec, body) = kernel_topology_to_spec_and_body(topology, prog, &ctx).map_err(
                |error| ProgramEmitError::KernelLowering {
                    stage_index: stage_idx,
                    kernel_index: kernel_idx,
                    error,
                },
            )?;

            if let Some(prior) = seen_names.get(&spec.name) {
                return Err(ProgramEmitError::KernelNameCollision {
                    name: spec.name.clone(),
                    first_at: *prior,
                    second_at: (stage_idx, kernel_idx),
                });
            }
            seen_names.insert(spec.name.clone(), (stage_idx, kernel_idx));

            let wgsl = compose_wgsl_file(&spec, &body, topology);
            wgsl_files.insert(format!("{}.wgsl", spec.name), wgsl);

            let rs = compose_rust_module_file(&spec, topology);
            rust_files.insert(format!("{}.rs", spec.name), rs);

            kernel_index.push(spec.name.clone());
        }
    }

    // Synthesize lib.rs (kernel registry + Kernel trait + bgl_storage /
    // bgl_uniform helpers). Stored under the `"lib.rs"` key (matching
    // the per-kernel `<name>.rs` keys) so the xtask side-channel writes
    // it as `<src>/lib.rs` verbatim.
    rust_files.insert("lib.rs".to_string(), synthesize_lib_rs(&kernel_index));

    // Synthesize the seven cross-cutting modules (Task 5.4). Each
    // becomes a separate `<name>.rs` entry; the side-channel writer
    // resolves them under `<src>/<name>.rs`. lib.rs declares each via
    // `pub mod <name>;`.
    rust_files.insert(
        "binding_sources.rs".to_string(),
        super::cross_cutting::synthesize_binding_sources(),
    );
    rust_files.insert(
        "resident_context.rs".to_string(),
        super::cross_cutting::synthesize_resident_context(prog),
    );
    rust_files.insert(
        "external_buffers.rs".to_string(),
        super::cross_cutting::synthesize_external_buffers(),
    );
    rust_files.insert(
        "transient_handles.rs".to_string(),
        super::cross_cutting::synthesize_transient_handles(),
    );
    rust_files.insert(
        "pingpong_context.rs".to_string(),
        super::cross_cutting::synthesize_pingpong_context(),
    );
    rust_files.insert(
        "pool.rs".to_string(),
        super::cross_cutting::synthesize_pool(),
    );
    rust_files.insert(
        "schedule.rs".to_string(),
        super::cross_cutting::synthesize_schedule(schedule, prog),
    );

    Ok(EmittedArtifacts {
        wgsl_files,
        rust_files,
        kernel_index,
    })
}

