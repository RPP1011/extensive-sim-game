/// Compose a single `.wgsl` file from a [`KernelSpec`] + body string +
/// originating [`KernelTopology`] (used to derive the workgroup size).
///
/// The output mirrors the legacy WGSL files' shape:
/// 1. A `// GENERATED ...` header comment.
/// 2. The `@group(0) @binding(N)` declaration block from
///    [`lower_wgsl_bindings`].
/// 3. The `@compute @workgroup_size(N)` annotation, derived from the
///    topology's [`DispatchShape::workgroup_size`].
/// 4. The entry-point function with its `gid` argument and the body
///    from Task 4.2.
fn compose_wgsl_file(spec: &KernelSpec, body: &str, topology: &KernelTopology) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push('\n');

    // ViewFold kernels reference the cfg uniform via a named WGSL
    // struct (`{Pascal}Cfg { event_count, tick, _pad0, _pad1 }`); the
    // legacy fold WGSL files declare that struct inline before the
    // uniform binding so naga can resolve `cfg.event_count`.
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_wgsl_cfg_struct(spec));
        out.push('\n');
    }

    out.push_str(&lower_wgsl_bindings(spec));
    out.push('\n');

    let workgroup_x = topology_workgroup_size_x(topology);

    out.push_str(&format!(
        "@compute @workgroup_size({workgroup_x})\n\
         fn {entry}(@builtin(global_invocation_id) gid: vec3<u32>) {{\n",
        entry = spec.entry_point
    ));
    out.push_str(body);
    if !body.ends_with('\n') {
        out.push('\n');
    }
    out.push_str("}\n");

    out
}

/// Emit the WGSL `struct {Pascal}Cfg { event_count: u32, tick: u32,
/// _pad0: u32, _pad1: u32 };` declaration ViewFold kernels reference
/// from the cfg uniform binding. Sourced directly from the cfg
/// binding's WGSL type name; the WGSL field shape mirrors the Rust
/// `{ event_count, tick, _pad: [u32; 2] }` cfg, expanded to four
/// scalar fields because WGSL doesn't support array members in
/// uniform-shaped structs the same way Rust does.
///
/// # Limitations
///
/// - **Field shape is hard-coded to the ViewFold cfg.** Future
///   per-kind cfg refinements will need to surface the WGSL struct
///   decl from the kernel-spec layer rather than rebuilding it here.
fn compose_view_fold_wgsl_cfg_struct(spec: &KernelSpec) -> String {
    let cfg = spec
        .bindings
        .iter()
        .find(|b| matches!(b.bg_source, BgSource::Cfg))
        .expect("ViewFold spec must carry a cfg binding");
    format!(
        "struct {ty} {{ event_count: u32, tick: u32, _pad0: u32, _pad1: u32 }};\n",
        ty = cfg.wgsl_ty
    )
}

/// Compose a single `.rs` file from a [`KernelSpec`] + originating
/// [`KernelTopology`] (used to derive the dispatch-workgroup expression
/// inside the [`crate::Kernel::record`] body).
///
/// The output mirrors the legacy Rust kernel modules' shape (see
/// `crates/engine_gpu_rules/src/alive_pack.rs` for a template), with
/// the differences documented in the module-level `# Limitations`. Each
/// section is sourced from one of the [`crate::kernel_lowerings`]
/// helpers, so a regression in any single emitted form is caught by the
/// lowering's own tests; this composer is structural glue only.
///
/// The synthesized module is `crate::Kernel`-implementing: the
/// `<Pascal>Kernel` struct, `<Pascal>Bindings<'a>`, the cfg struct, an
/// `impl crate::Kernel for <Pascal>Kernel { … }` block (real bodies,
/// no panics), and the standalone helper fns (`<name>_bgl_entries`,
/// `<name>_bind_group_entries`, `<name>_build_cfg`) the legacy callers
/// expect.
///
/// # Limitations
/// - The `record()` body uses
///   `pass.dispatch_workgroups((agent_cap + N - 1) / N, 1, 1)` for
///   per-agent / per-event / per-pair / per-word kernels, and
///   `(1, 1, 1)` for one-shot. Indirect dispatch
///   (`dispatch_workgroups_indirect`) is a Task 5.4+ concern — the
///   legacy emitters today use the same agent-cap-bounded direct
///   dispatch as a placeholder for per-event kernels.
/// - The `bind()` body is built from the spec's [`BgSource`] tags via
///   [`render_bg_source_expr`]; ViewHandle / AliasOf are routed
///   through `unwrap_or(primary)` patterns that mirror the legacy fold
///   kernels' pattern. None of today's CG kernels emit those variants
///   (Task 5.4+ wires fold-view tuple destructuring); the helpers stay
///   in place defensively so the composer never panics.
fn compose_rust_module_file(spec: &KernelSpec, topology: &KernelTopology) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler — do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push('\n');

    out.push_str("use crate::binding_sources::BindingSources;\n\n");

    // Per-kernel struct (carries the pipeline + BGL).
    out.push_str(&format!(
        "pub struct {pascal}Kernel {{\n    \
         pipeline: wgpu::ComputePipeline,\n    \
         bgl: wgpu::BindGroupLayout,\n}}\n\n",
        pascal = spec.pascal
    ));

    // Bindings struct. ViewFold kernels emit `Option<&'a wgpu::Buffer>`
    // for the anchor/ids slots (matching the legacy fold-kernel
    // contract); generic kernels use the spec-driven lowering which
    // emits `&'a wgpu::Buffer` for every binding.
    out.push_str(&format!(
        "pub struct {pascal}Bindings<'a> {{\n",
        pascal = spec.pascal
    ));
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_bindings_struct_fields(spec));
    } else {
        out.push_str(&lower_rust_bindings_struct_fields(spec));
    }
    out.push_str("}\n\n");

    // Cfg struct decl — KernelSpec.cfg_struct_decl is the full
    // `#[repr(C)] ... pub struct <Cfg> { ... }` form.
    out.push_str(&spec.cfg_struct_decl);
    out.push_str("\n\n");

    // WGSL source — the `.wgsl` file is co-located in the output
    // directory; `include_str!` resolves it at compile time when the
    // emitted crate is built.
    out.push_str(&format!(
        "const SHADER_SRC: &str = include_str!(\"{name}.wgsl\");\n\n",
        name = spec.name
    ));

    // Kernel trait impl.
    out.push_str(&compose_kernel_trait_impl(spec, topology));
    out.push('\n');

    // BGL entries function. ViewFold kernels skip the standalone
    // `_bgl_entries` / `_bind_group_entries` / `_build_cfg` helpers —
    // the legacy `crates/engine_gpu_rules/src/fold_<view>.rs` modules
    // do not expose them, and the `lower_rust_bg_entries` lowering
    // would produce `Option`-typed expressions that don't carry
    // `.as_entire_binding()`. The Kernel trait impl above remains the
    // single entry point for ViewFold callers.
    if !matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&format!(
            "pub fn {name}_bgl_entries() -> Vec<wgpu::BindGroupLayoutEntry> {{\n    \
             vec![\n",
            name = spec.name
        ));
        out.push_str(&render_cg_bgl_entries(spec));
        out.push_str("    ]\n}\n\n");

        // Bind-group entries function. Returns the BindGroupEntry list a
        // caller wraps with the BGL into a BindGroup.
        out.push_str(&format!(
            "pub fn {name}_bind_group_entries<'a>(\n    \
             bindings: &'a {pascal}Bindings<'a>,\n) -> \
             Vec<wgpu::BindGroupEntry<'a>> {{\n    vec![\n",
            name = spec.name,
            pascal = spec.pascal
        ));
        out.push_str(&lower_rust_bg_entries(spec));
        out.push_str("    ]\n}\n\n");

        // Cfg builder (free fn variant — kept for backwards compat with
        // any caller that hasn't switched to `<Pascal>Kernel::build_cfg`).
        out.push_str(&format!(
            "pub fn {name}_build_cfg(state: &engine::state::SimState) -> {cfg} {{\n    \
             {build}\n}}\n",
            name = spec.name,
            cfg = spec.cfg_struct,
            build = spec.cfg_build_expr,
        ));
    }

    out
}

/// Compose the `impl crate::Kernel for <Pascal>Kernel { … }` block.
fn compose_kernel_trait_impl(spec: &KernelSpec, topology: &KernelTopology) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "impl crate::Kernel for {pascal}Kernel {{\n    \
         type Bindings<'a> = {pascal}Bindings<'a>;\n    \
         type Cfg = {cfg};\n\n",
        pascal = spec.pascal,
        cfg = spec.cfg_struct
    ));

    // new()
    out.push_str("    fn new(device: &wgpu::Device) -> Self {\n");
    out.push_str(&format!(
        "        let shader = device.create_shader_module(wgpu::ShaderModuleDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::wgsl\"),\n            \
             source: wgpu::ShaderSource::Wgsl(SHADER_SRC.into()),\n        \
             }});\n",
        name = spec.name
    ));
    out.push_str(&format!(
        "        let bgl = device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::bgl\"),\n            \
             entries: &[\n",
        name = spec.name
    ));
    out.push_str(&render_cg_bgl_entries(spec));
    out.push_str("            ],\n        });\n");
    out.push_str(&format!(
        "        let pl = device.create_pipeline_layout(&wgpu::PipelineLayoutDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::pl\"),\n            \
             bind_group_layouts: &[&bgl],\n            \
             push_constant_ranges: &[],\n        \
             }});\n",
        name = spec.name
    ));
    out.push_str(&format!(
        "        let pipeline = device.create_compute_pipeline(&wgpu::ComputePipelineDescriptor {{\n            \
             label: Some(\"engine_gpu_rules::{name}::pipeline\"),\n            \
             layout: Some(&pl),\n            \
             module: &shader,\n            \
             entry_point: Some(\"{entry}\"),\n            \
             compilation_options: Default::default(),\n            \
             cache: None,\n        \
             }});\n        \
             Self {{ pipeline, bgl }}\n    }}\n\n",
        name = spec.name,
        entry = spec.entry_point
    ));

    // build_cfg()
    out.push_str(&format!(
        "    fn build_cfg(&self, state: &engine::state::SimState) -> {cfg} {{\n        \
         let _ = state;\n        \
         {build}\n    }}\n\n",
        cfg = spec.cfg_struct,
        build = spec.cfg_build_expr
    ));

    // bind()
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_bind_method(spec));
    } else {
        out.push_str(&format!(
            "    fn bind<'a>(&'a self, sources: &'a BindingSources<'a>, cfg: &'a wgpu::Buffer) -> {pascal}Bindings<'a> {{\n        \
             let _ = sources;\n        \
             {pascal}Bindings {{\n",
            pascal = spec.pascal
        ));
        out.push_str(&compose_bind_body(spec));
        out.push_str("        }\n    }\n\n");
    }

    // record()
    if matches!(spec.kind, KernelKind::ViewFold) {
        out.push_str(&compose_view_fold_record_method(spec));
    } else {
        // OneShot dispatch resolves to `pass.dispatch_workgroups(1, 1, 1)`
        // and never references `agent_cap` — emit the parameter as
        // `_agent_cap` so the synthesized kernel module compiles under
        // `-D unused-variables`. PerAgent / PerEvent / PerPair / PerWord
        // all derive workgroup count from `agent_cap`. Mirrors the
        // legacy `seed_indirect.rs` convention.
        let cap_param = if dispatch_is_one_shot(topology) {
            "_agent_cap"
        } else {
            "agent_cap"
        };
        out.push_str(&format!(
            "    fn record(&self, device: &wgpu::Device, encoder: &mut wgpu::CommandEncoder, bindings: &{pascal}Bindings<'_>, {cap_param}: u32) {{\n",
            pascal = spec.pascal,
            cap_param = cap_param,
        ));
        out.push_str(&format!(
            "        let bg = device.create_bind_group(&wgpu::BindGroupDescriptor {{\n            \
                 label: Some(\"engine_gpu_rules::{name}::bg\"),\n            \
                 layout: &self.bgl,\n            \
                 entries: &[\n",
            name = spec.name
        ));
        out.push_str(&lower_rust_bg_entries(spec));
        out.push_str("            ],\n        });\n");
        out.push_str(&format!(
            "        let mut pass = encoder.begin_compute_pass(&wgpu::ComputePassDescriptor {{\n            \
                 label: Some(\"engine_gpu_rules::{name}::pass\"),\n            \
                 timestamp_writes: None,\n        \
                 }});\n",
            name = spec.name
        ));
        out.push_str("        pass.set_pipeline(&self.pipeline);\n");
        out.push_str("        pass.set_bind_group(0, &bg, &[]);\n");
        out.push_str(&compose_dispatch_call(topology));
        out.push_str("    }\n");
    }

    out.push_str("}\n");
    out
}

/// Render the BGL entries for a CG-emitted kernel, routing through the
/// CG-side helpers `crate::bgl_storage` / `crate::bgl_uniform` defined
/// in the synthesized `lib.rs`. Distinct from
/// [`crate::kernel_lowerings::lower_rust_bgl_entries`] which routes
/// through the legacy `crate::fused_mask::bgl_*` path; the legacy
/// path requires the legacy `fused_mask.rs` to define the helpers,
/// which a CG-only output never carries.
fn render_cg_bgl_entries(spec: &KernelSpec) -> String {
    let mut out = String::new();
    for b in &spec.bindings {
        let descriptor = match b.access {
            AccessMode::ReadStorage => format!("crate::bgl_storage({}, true)", b.slot),
            AccessMode::ReadWriteStorage | AccessMode::AtomicStorage => {
                format!("crate::bgl_storage({}, false)", b.slot)
            }
            AccessMode::Uniform => format!("crate::bgl_uniform({})", b.slot),
        };
        writeln!(out, "                {}, // {}", descriptor, b.name)
            .expect("write to String never fails");
    }
    out
}

/// Render the `bindings: <field>: <expr>,` rows for the `bind()`
/// initializer body.
fn compose_bind_body(spec: &KernelSpec) -> String {
    let mut out = String::new();
    for b in &spec.bindings {
        // AliasOf bindings don't have their own struct field; the alias
        // is handled in `lower_rust_bg_entries` at BindGroupEntry time.
        if matches!(b.bg_source, BgSource::AliasOf(_)) {
            continue;
        }
        let value = render_bg_source_expr(&b.bg_source, &b.name);
        out.push_str(&format!("            {field}: {value},\n", field = b.name, value = value));
    }
    out
}

/// Render a [`BgSource`] as the Rust expression that yields the
/// `&'a wgpu::Buffer` used by the `bind()` initializer.
///
/// `field` is the binding's name — used to surface a self-referential
/// fallback for ViewHandle (the generic CG path; today no kernel emits
/// it, so the helper stays defensive).
fn render_bg_source_expr(src: &BgSource, field: &str) -> String {
    match src {
        BgSource::Resident(f) => format!("sources.resident.{f}"),
        BgSource::Transient(f) => format!("sources.transient.{f}"),
        BgSource::External(f) => format!("sources.external.{f}"),
        BgSource::Pool(f) => format!("sources.pool.{f}"),
        BgSource::Cfg => "cfg".to_string(),
        BgSource::ViewHandle { accessor, tuple_idx } => {
            // Defensive: until Task 5.4 wires fold-view tuple
            // destructuring at the kernel-module emit layer, the
            // generic path renders a `<accessor>().<idx>`-style
            // expression. The legacy fold kernels destructure into
            // (primary, anchor, ids) locals; today no CG kernel emits
            // ViewHandle — this arm exists so the composer never falls
            // through to a panic if a future spec does.
            format!(
                "sources.resident.{accessor}().{idx} /* {field} */",
                accessor = accessor,
                idx = tuple_idx
            )
        }
        BgSource::AliasOf(target) => {
            // Defensive — AliasOf bindings are filtered out before this
            // helper is called. If a caller skipped the filter, render
            // a self-evidently routable expression rather than panic.
            format!("/* alias of {target} */ sources.external.sim_cfg")
        }
    }
}

