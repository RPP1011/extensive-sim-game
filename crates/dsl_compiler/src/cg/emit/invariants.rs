#[cfg(test)]
mod tests {
    use super::*;
    use dsl_ast::resolve::resolve;

    fn compile(src: &str) -> Compilation {
        let p = crate::parse(src).expect("parse");
        resolve(p).expect("resolve")
    }

    #[test]
    fn empty_compilation_emits_nothing() {
        let comp = Compilation::default();
        assert!(synthesize_invariants(&comp).is_none());
    }

    #[test]
    fn scalar_view_bounded_emits_check_fn() {
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
@materialized(on_event = [Tick])
view kill_count(by: Agent) -> f32 {
    initial: 0.0,
    on Tick { } { self += 1.0 }
}
invariant bounded_kc(a: Agent) @debug_only {
    kill_count(a) < 1000.0
}
"#;
        let comp = compile(src);
        let out = synthesize_invariants(&comp).expect("non-empty");
        assert!(
            out.contains("pub fn check_bounded_kc(view_storage: &[f32]) -> Vec<Violation>"),
            "expected per-invariant check fn:\n{out}"
        );
        assert!(
            out.contains("Mode::DebugOnly"),
            "expected DebugOnly mode literal:\n{out}"
        );
    }

    #[test]
    fn unsupported_shape_emits_skip_comment() {
        // Predicate without a top-level comparator → SKIP.
        let src = r#"
event Tick { }
entity Agent_ : Agent { pos: vec3, }
invariant always_true() @runtime {
    true
}
"#;
        let comp = compile(src);
        let out = synthesize_invariants(&comp).expect("non-empty");
        assert!(
            out.contains("// SKIP `always_true`"),
            "expected SKIP comment for unsupported shape:\n{out}"
        );
    }
}
