//! Scoring-table emission. Scaffolded at milestone-3-completion; filled
//! in at milestone 4 (scoring).
//!
//! Emission target: `crates/engine_rules/src/scoring/<name>.rs`. Scoring
//! rows are POD (`#[repr(C)]`) so the emitted table is byte-identical for
//! CPU and GPU backends — the engine's scorer reads the table, the future
//! GPU kernel uploads it as a buffer. Type definitions (`ScoringEntry`,
//! `ModifierRow`, `PredicateDescriptor`) live in `engine_rules` so both the
//! emitted table and the CPU scorer can import them; the GPU path will
//! reuse the same layout.
//!
//! Until milestone 4 lands, `emit_scoring_mod` produces an empty aggregator
//! so the directory compiles.

use crate::ir::ScoringIR;

pub fn emit_scoring(_scoring: &ScoringIR, _source_file: Option<&str>) -> Result<String, String> {
    Err("scoring emission not yet implemented (milestone 4)".into())
}

pub fn emit_scoring_mod(scoring: &[ScoringIR]) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler. Do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push_str("\n");
    if scoring.is_empty() {
        out.push_str("// No `scoring` declarations in scope yet (milestone 4 fills\n");
        out.push_str("// this in). The engine's UtilityBackend reads the table via\n");
        out.push_str("// the re-export below; keeping the symbol stable lets the\n");
        out.push_str("// call site land before the first scoring row does.\n");
        out.push_str("\n");
        out.push_str("/// Empty scoring table placeholder. Replaced by the emitter\n");
        out.push_str("/// when milestone 4 lands the first `scoring` declaration.\n");
        out.push_str("pub const SCORING_TABLE: &[()] = &[];\n");
    } else {
        out.push_str("// milestone 4: aggregate scoring table\n");
        out.push_str("pub const SCORING_TABLE: &[()] = &[];\n");
    }
    out
}
