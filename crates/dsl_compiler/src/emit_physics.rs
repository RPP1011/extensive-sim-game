/// If `expr` is `agents.record_memory(observer, source, fact_payload, confidence, tick)`,
/// returns two Rust statement strings to emit as separate lines:
///   1. `let entry = MemoryEntry { source: <src>.raw(), value: <conf>, anchor_tick: <tick>, };`
///   2. `views.memory.push(<obs>.raw(), entry);`
/// Returns `None` for any other expression.
fn try_lower_record_memory_as_lines(expr: &IrExprNode) -> Option<Vec<String>> {
    match &expr.kind {
        IrExpr::NamespaceCall { ns, method, args }
            if *ns == NamespaceId::Agents && method == "record_memory" && args.len() == 5 =>
        {
            let observer   = lower_expr(&args[0].value).ok()?;
            let source     = lower_expr(&args[1].value).ok()?;
            // args[2] is fact_payload — not stored in MemoryEntry (dropped in the lowering)
            let confidence = lower_expr(&args[3].value).ok()?;
            let tick       = lower_expr(&args[4].value).ok()?;
            Some(vec![
                format!(
                    "let entry = MemoryEntry {{ source: {source}.raw(), value: {confidence}, anchor_tick: {tick}, }};"
                ),
                format!("views.memory.push({observer}.raw(), entry);"),
            ])
        }
        _ => None,
    }
}

fn emit_stmt(out: &mut String, stmt: &IrStmt, indent: usize) -> Result<(), EmitError> {
    let pad = " ".repeat(indent);
    match stmt {
        IrStmt::Let { name, value, .. } => {
            let v = lower_expr(value)?;
            writeln!(out, "{pad}let {name} = {v};").unwrap();
        }
        IrStmt::Emit(emit) => emit_emit(out, emit, indent)?,
        IrStmt::If { cond, then_body, else_body, .. } => {
            let c = lower_expr(cond)?;
            writeln!(out, "{pad}if {c} {{").unwrap();
            for s in then_body {
                emit_stmt(out, s, indent + 4)?;
            }
            if let Some(eb) = else_body {
                writeln!(out, "{pad}}} else {{").unwrap();
                for s in eb {
                    emit_stmt(out, s, indent + 4)?;
                }
            }
            writeln!(out, "{pad}}}").unwrap();
        }
        IrStmt::For { binder_name, iter, filter, body, .. } => {
            emit_for_stmt(out, binder_name, iter, filter.as_ref(), body, indent)?;
        }
        IrStmt::Match { scrutinee, arms, .. } => {
            emit_match_stmt(out, scrutinee, arms, indent)?;
        }
        IrStmt::SelfUpdate { .. } => {
            return Err(EmitError::Unsupported(
                "`self += ...` self-update statements only valid in fold bodies".into(),
            ));
        }
        IrStmt::Expr(e) => {
            // Special case: `agents.record_memory(o, s, f, c, t)` lowers to
            // two separate statements (MemoryEntry construction + push). Detect
            // this pattern and emit as two top-level lines instead of one block.
            if let Some(lines) = try_lower_record_memory_as_lines(e) {
                for line in &lines {
                    writeln!(out, "{pad}{line}").unwrap();
                }
            } else {
                let v = lower_expr(e)?;
                writeln!(out, "{pad}{v};").unwrap();
            }
        }
        IrStmt::BeliefObserve { observer, target, fields, .. } => {
            // T5: lower to a BoundedMap upsert on the observer's cold_beliefs.
            // agent_cold_beliefs_mut returns None if the observer is dead/invalid;
            // silently skip in that case (no-op is safe — belief is stale anyway).
            let obs = lower_expr(observer)?;
            let tgt = lower_expr(target)?;
            let mut field_inits: Vec<String> = fields
                .iter()
                .map(|f| {
                    let val = lower_expr(&f.value)?;
                    Ok(format!("{}: {}", f.name, val))
                })
                .collect::<Result<_, EmitError>>()?;
            // Spread default so partial field lists compile without listing every field.
            field_inits.push("..Default::default()".into());
            let inits = field_inits.join(", ");
            writeln!(
                out,
                "{pad}if let Some(__beliefs) = state.agent_cold_beliefs_mut({obs}) {{\
\n{pad}    __beliefs.upsert({tgt}, engine_data::belief::BeliefState {{ {inits} }});\
\n{pad}}}"
            )
            .unwrap();
        }
    }
    Ok(())
}

/// Lower `for <binder> in <iter> [where <filter>] { <body> }` into a Rust
/// `for ... in ... { ... }` loop. The iter expression is rendered as an
/// iterator-producing expression (e.g. the `abilities.effects(ab)` stdlib
/// call returns `.iter().copied()` as its lowered form — see
/// `lower_namespace_call`). The optional `where` filter becomes a nested
/// `if` that wraps the body.
fn emit_for_stmt(
    out: &mut String,
    binder: &str,
    iter: &IrExprNode,
    filter: Option<&IrExprNode>,
    body: &[IrStmt],
    indent: usize,
) -> Result<(), EmitError> {
    let pad = " ".repeat(indent);
    let iter_expr = lower_expr(iter)?;
    writeln!(out, "{pad}for {binder} in {iter_expr} {{").unwrap();
    if let Some(f) = filter {
        let cond = lower_expr(f)?;
        let fpad = " ".repeat(indent + 4);
        writeln!(out, "{fpad}if !({cond}) {{ continue; }}").unwrap();
    }
    for s in body {
        emit_stmt(out, s, indent + 4)?;
    }
    writeln!(out, "{pad}}}").unwrap();
    Ok(())
}

/// Lower `match <scrut> { <arm> => <body>, ... }` into a Rust `match`
/// expression. Patterns supported: `Struct { field, field: inner }` (enum
/// variant with named fields), `Ctor(inner)` (enum variant with positional
/// fields — rare; mostly for ctor-style events), bare bind, wildcard.
fn emit_match_stmt(
    out: &mut String,
    scrutinee: &IrExprNode,
    arms: &[IrStmtMatchArm],
    indent: usize,
) -> Result<(), EmitError> {
    let pad = " ".repeat(indent);
    let scrut = lower_expr(scrutinee)?;
    writeln!(out, "{pad}match {scrut} {{").unwrap();
    for arm in arms {
        let arm_pad = " ".repeat(indent + 4);
        let pat = lower_pattern(&arm.pattern)?;
        writeln!(out, "{arm_pad}{pat} => {{").unwrap();
        for s in &arm.body {
            emit_stmt(out, s, indent + 8)?;
        }
        writeln!(out, "{arm_pad}}}").unwrap();
    }
    writeln!(out, "{pad}}}").unwrap();
    Ok(())
}

/// Lower an `IrPattern` to a Rust pattern string suitable for a `match`
/// arm. Bare binds become identifiers; wildcards become `_`; struct
/// patterns become `Name { field, field: inner }` (with the enum prefix
/// added when `name` maps to a known stdlib sum type like `EffectOp`).
fn lower_pattern(pat: &IrPattern) -> Result<String, EmitError> {
    match pat {
        IrPattern::Bind { name, .. } => Ok(name.clone()),
        IrPattern::Wildcard => Ok("_".into()),
        IrPattern::Struct { name, bindings, .. } => {
            let qualified = qualified_variant_name(name);
            let mut fields: Vec<String> = Vec::with_capacity(bindings.len());
            for b in bindings {
                fields.push(lower_pattern_binding(b)?);
            }
            if fields.is_empty() {
                Ok(format!("{qualified} {{}}"))
            } else {
                Ok(format!("{qualified} {{ {} }}", fields.join(", ")))
            }
        }
        IrPattern::Ctor { name, inner, .. } => {
            let qualified = qualified_variant_name(name);
            if inner.is_empty() {
                Ok(qualified)
            } else {
                let parts: Vec<String> = inner
                    .iter()
                    .map(lower_pattern)
                    .collect::<Result<_, _>>()?;
                Ok(format!("{qualified}({})", parts.join(", ")))
            }
        }
        IrPattern::Expr(e) => {
            let rendered = lower_expr(e)?;
            Ok(rendered)
        }
    }
}

/// Render one `field` or `field: <inner-pattern>` entry of a struct
/// pattern. Shorthand binds (same local name as the field) collapse to
/// just `field` in Rust, matching the hand-written cast handler shape.
fn lower_pattern_binding(b: &IrPatternBinding) -> Result<String, EmitError> {
    match &b.value {
        IrPattern::Bind { name, .. } if name == &b.field => Ok(b.field.clone()),
        IrPattern::Wildcard => Ok(format!("{}: _", b.field)),
        other => {
            let rendered = lower_pattern(other)?;
            Ok(format!("{}: {}", b.field, rendered))
        }
    }
}

/// Resolve a pattern's source name to its fully-qualified Rust variant
/// form. The DSL doesn't (yet) declare `EffectOp` as a first-class sum
/// type, so the emitter hardcodes the known stdlib sum-type variant names
/// here. Unknown names pass through unchanged; that lets future DSL
/// enums (declared via `enum <Name> { ... }`) opt in by name.
fn qualified_variant_name(name: &str) -> String {
    // `EffectOp` variants used by the `cast` physics rule. Keep in sync
    // with `crates/engine/src/ability/program.rs::EffectOp`.
    const EFFECT_OP_VARIANTS: &[&str] = &[
        "Damage",
        "Heal",
        "Shield",
        "Stun",
        "Slow",
        "TransferGold",
        "ModifyStanding",
        "CastAbility",
    ];
    // `TargetSelector` variants (used in selector-based conditionals in the
    // cast rule). See `program.rs::TargetSelector`.
    const TARGET_SELECTOR_VARIANTS: &[&str] = &["Target", "Caster"];

    if EFFECT_OP_VARIANTS.contains(&name) {
        format!("crate::ability::EffectOp::{name}")
    } else if TARGET_SELECTOR_VARIANTS.contains(&name) {
        format!("crate::ability::TargetSelector::{name}")
    } else {
        name.to_string()
    }
}

fn emit_emit(out: &mut String, emit: &IrEmit, indent: usize) -> Result<(), EmitError> {
    let pad = " ".repeat(indent);
    if emit.event.is_none() {
        return Err(EmitError::UnresolvedEventInEmit(emit.event_name.clone()));
    }
    let mut field_lines: Vec<String> = Vec::with_capacity(emit.fields.len() + 1);
    let mut saw_tick = false;
    for f in &emit.fields {
        if f.name == "tick" {
            saw_tick = true;
        }
        let v = lower_expr(&f.value)?;
        if f.name == v {
            field_lines.push(f.name.clone());
        } else {
            field_lines.push(format!("{}: {}", f.name, v));
        }
    }
    // Implicit tick: every emit call gets `tick: state.tick` auto-appended
    // unless the author already specified it.
    if !saw_tick {
        field_lines.push("tick: state.tick".to_string());
    }
    let body = if field_lines.is_empty() {
        String::new()
    } else {
        format!(" {} ", field_lines.join(", "))
    };
    writeln!(
        out,
        "{pad}events.push(Event::{} {{{body}}});",
        emit.event_name
    )
    .unwrap();
    Ok(())
}

fn lower_expr(node: &IrExprNode) -> Result<String, EmitError> {
    lower_expr_kind(&node.kind)
}

fn lower_expr_kind(kind: &IrExpr) -> Result<String, EmitError> {
    match kind {
        IrExpr::LitBool(b) => Ok(if *b { "true".into() } else { "false".into() }),
        IrExpr::LitInt(v) => Ok(format!("{v}")),
        IrExpr::LitFloat(v) => Ok(render_float(*v)),
        IrExpr::LitString(s) => Ok(format!("{s:?}")),
        IrExpr::Local(_, name) => Ok(name.clone()),
        IrExpr::NamespaceField { ns, field, .. } => lower_namespace_field(*ns, field),
        IrExpr::NamespaceCall { ns, method, args } => lower_namespace_call(*ns, method, args),
        IrExpr::Binary(op, lhs, rhs) => {
            // `<expr> == None` / `<expr> != None` → `.is_none()` /
            // `.is_some()`. Copied from `emit_mask.rs` (task 157 landed it
            // for the `mask Cast` engagement-lock clause); the engagement
            // physics rules (task 163) need the same parity so
            // `agents.engaged_with(x) != None` compiles. Only the RHS-
            // `None` form is detected; a swapped source would need a
            // parser normalisation pass, which is out of scope here.
            if matches!(op, BinOp::Eq | BinOp::NotEq) {
                if let IrExpr::EnumVariant { ty, variant } = &rhs.kind {
                    if ty.is_empty() && variant == "None" {
                        let l = lower_expr(lhs)?;
                        let method = if matches!(op, BinOp::Eq) { "is_none" } else { "is_some" };
                        return Ok(format!("({l}).{method}()"));
                    }
                }
            }
            let l = lower_expr(lhs)?;
            let r = lower_expr(rhs)?;
            Ok(format!("({l} {} {r})", binop_str(*op)))
        }
        IrExpr::Unary(op, rhs) => {
            let r = lower_expr(rhs)?;
            Ok(format!("({}{r})", unop_str(*op)))
        }
        IrExpr::BuiltinCall(b, args) => lower_builtin_call(*b, args),
        IrExpr::If { cond, then_expr, else_expr } => {
            let c = lower_expr(cond)?;
            let t = lower_expr(then_expr)?;
            let e = match else_expr {
                Some(e) => lower_expr(e)?,
                None => return Err(EmitError::Unsupported("`if` expression without else".into())),
            };
            Ok(format!("(if {c} {{ {t} }} else {{ {e} }})"))
        }
        IrExpr::Field { base, field_name, .. } => {
            let b = lower_expr(base)?;
            Ok(format!("{b}.{field_name}"))
        }
        IrExpr::EnumVariant { ty, variant } => {
            // Route engine stdlib sum types (`EffectOp`, `TargetSelector`)
            // through `qualified_variant_name` so the emitted path carries
            // the `crate::ability::...` prefix. Other enums keep their
            // source-level `Ty::Variant` form (matches DSL-declared enums
            // re-exported at the `engine_rules::enums` top level).
            if ty.is_empty() {
                Ok(qualified_variant_name(variant))
            } else if ty == "TargetSelector" || ty == "EffectOp" {
                Ok(qualified_variant_name(variant))
            } else {
                Ok(format!("{ty}::{variant}"))
            }
        }
        other => Err(EmitError::Unsupported(format!(
            "expression shape {other:?} not supported in physics emission"
        ))),
    }
}

fn lower_namespace_field(ns: NamespaceId, field: &str) -> Result<String, EmitError> {
    if ns == NamespaceId::Config {
        if field.contains('.') {
            return Ok(format!("state.config.{field}"));
        }
        return Err(EmitError::Unsupported(format!(
            "bare `config.{field}` is not a value; address a specific field"
        )));
    }
    if ns == NamespaceId::World {
        if field == "tick" {
            return Ok("state.tick".into());
        }
    }
    if ns == NamespaceId::Cascade && field == "max_iterations" {
        // Compile-time constant — the cascade framework's per-tick
        // iteration ceiling. Used by the `cast` rule to bound
        // `CastAbility` recursion without a hand-written handler. Cast to
        // `u8` so comparisons against the event's `depth: u8` don't need an
        // explicit widening in DSL source (the literal `8` would default to
        // `i32`, forcing a cast; the generated `as u8` keeps the arithmetic
        // tidy).
        return Ok("(crate::cascade::MAX_CASCADE_ITERATIONS as u8)".into());
    }
    Err(EmitError::Unsupported(format!(
        "namespace-field `{}.{field}` not supported",
        ns.name()
    )))
}

