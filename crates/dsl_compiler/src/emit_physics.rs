fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(out, "// GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
}

fn emit_imports(
    out: &mut String,
    physics: &PhysicsIR,
    ctx: &EmitContext<'_>,
) -> Result<(), EmitError> {
    // Sanity check: every trigger + emit must resolve.
    for handler in &physics.handlers {
        match &handler.pattern {
            IrPhysicsPattern::Kind(p) => {
                if p.event.is_none() {
                    return Err(EmitError::UnresolvedEventInPattern(p.name.clone()));
                }
            }
            IrPhysicsPattern::Tag { name, tag, .. } => {
                if tag.is_none() {
                    return Err(EmitError::UnresolvedEventInPattern(format!("@{name}")));
                }
            }
        }
        scan_emits(&handler.body)?;
    }

    // Collect niche-id imports from the handler's argument types.
    let mut niche_ids: BTreeSet<&'static str> = BTreeSet::new();
    let mut needs_vec3 = false;
    let mut needs_quest_category = false;
    let mut needs_resolution = false;
    for h in &physics.handlers {
        for (_, ty) in handler_arg_types(h, ctx)? {
            collect_import(
                &ty,
                &mut niche_ids,
                &mut needs_vec3,
                &mut needs_quest_category,
                &mut needs_resolution,
            );
        }
    }

    // Scan handler bodies for view-mutation calls to determine if `views` param is needed.
    let needs_views = physics.handlers.iter().any(|h| rule_body_needs_views(&h.body));
    // Scan for record_memory specifically (needs MemoryEntry import).
    let needs_memory_entry = physics.handlers.iter().any(|h| body_uses_record_memory(&h.body));

    writeln!(out, "use crate::event::EventRing;").unwrap();
    writeln!(out, "use engine_data::events::Event;").unwrap();
    writeln!(out, "use crate::state::SimState;").unwrap();
    if !niche_ids.is_empty() {
        let joined: Vec<&str> = niche_ids.iter().copied().collect();
        if joined.len() == 1 {
            writeln!(out, "use crate::ids::{};", joined[0]).unwrap();
        } else {
            writeln!(out, "use crate::ids::{{{}}};", joined.join(", ")).unwrap();
        }
    }
    if needs_vec3 {
        writeln!(out, "use glam::Vec3;").unwrap();
    }
    if needs_quest_category {
        writeln!(out, "use crate::types::QuestCategory;").unwrap();
    }
    if needs_resolution {
        writeln!(out, "use crate::types::Resolution;").unwrap();
    }
    if needs_views {
        writeln!(out, "use crate::views::ViewRegistry;").unwrap();
    }
    if needs_memory_entry {
        writeln!(out, "use crate::views::memory::MemoryEntry;").unwrap();
    }
    writeln!(out).unwrap();
    Ok(())
}

fn collect_import(
    ty: &IrType,
    niche_ids: &mut BTreeSet<&'static str>,
    needs_vec3: &mut bool,
    needs_quest_category: &mut bool,
    needs_resolution: &mut bool,
) {
    match ty {
        IrType::AgentId => {
            niche_ids.insert("AgentId");
        }
        IrType::ItemId => {
            niche_ids.insert("ItemId");
        }
        IrType::GroupId => {
            niche_ids.insert("GroupId");
        }
        IrType::QuestId => {
            niche_ids.insert("QuestId");
        }
        IrType::AuctionId => {
            niche_ids.insert("AuctionId");
        }
        IrType::EventId => {
            niche_ids.insert("EventId");
        }
        IrType::AbilityId => {
            niche_ids.insert("AbilityId");
        }
        IrType::Vec3 => {
            *needs_vec3 = true;
        }
        IrType::Named(n) => match n.as_str() {
            "QuestCategory" => *needs_quest_category = true,
            "Resolution" => *needs_resolution = true,
            _ => {}
        },
        IrType::Optional(inner) | IrType::Array(inner, _) | IrType::List(inner) => {
            collect_import(inner, niche_ids, needs_vec3, needs_quest_category, needs_resolution)
        }
        IrType::Tuple(items) => {
            for t in items {
                collect_import(t, niche_ids, needs_vec3, needs_quest_category, needs_resolution);
            }
        }
        _ => {}
    }
}

/// Returns `true` when the handler body contains at least one `emit` statement
/// (directly or nested under `if` / `for` / `match`). Used by the emitter to
/// skip the `Event` import in rules that only mutate via `agents.*` accessors.
fn body_emits_event(stmts: &[IrStmt]) -> bool {
    for s in stmts {
        match s {
            IrStmt::Emit(_) => return true,
            IrStmt::If { then_body, else_body, .. } => {
                if body_emits_event(then_body) { return true; }
                if let Some(b) = else_body {
                    if body_emits_event(b) { return true; }
                }
            }
            IrStmt::For { body, .. } => {
                if body_emits_event(body) { return true; }
            }
            IrStmt::Match { arms, .. } => {
                for arm in arms {
                    if body_emits_event(&arm.body) { return true; }
                }
            }
            IrStmt::Let { .. }
            | IrStmt::Expr(_)
            | IrStmt::SelfUpdate { .. }
            | IrStmt::BeliefObserve { .. } => {}
        }
    }
    false
}

/// Returns `true` when the handler body calls `agents.adjust_standing` or
/// `agents.record_memory` — namespace calls that lower to direct `views.*`
/// mutations. These rules require a `views: &mut ViewRegistry` parameter.
fn rule_body_needs_views(stmts: &[IrStmt]) -> bool {
    fn expr_needs_views(expr: &IrExprNode) -> bool {
        match &expr.kind {
            IrExpr::NamespaceCall { ns, method, .. }
                if *ns == NamespaceId::Agents
                    && (method == "adjust_standing" || method == "record_memory") =>
            {
                true
            }
            IrExpr::NamespaceCall { args, .. }
            | IrExpr::BuiltinCall(_, args)
            | IrExpr::UnresolvedCall(_, args)
            | IrExpr::ViewCall(_, args) => args.iter().any(|a| expr_needs_views(&a.value)),
            IrExpr::Binary(_, l, r) => expr_needs_views(l) || expr_needs_views(r),
            IrExpr::Unary(_, r) => expr_needs_views(r),
            _ => false,
        }
    }
    fn stmts_need_views(stmts: &[IrStmt]) -> bool {
        for s in stmts {
            let found = match s {
                IrStmt::Expr(e) => expr_needs_views(e),
                IrStmt::Let { value, .. } => expr_needs_views(value),
                IrStmt::SelfUpdate { value, .. } => expr_needs_views(value),
                IrStmt::If { cond, then_body, else_body, .. } => {
                    expr_needs_views(cond)
                        || stmts_need_views(then_body)
                        || else_body.as_ref().map_or(false, |b| stmts_need_views(b))
                }
                IrStmt::For { iter, body, .. } => expr_needs_views(iter) || stmts_need_views(body),
                IrStmt::Match { scrutinee, arms, .. } => {
                    expr_needs_views(scrutinee) || arms.iter().any(|a| stmts_need_views(&a.body))
                }
                IrStmt::Emit(_) | IrStmt::BeliefObserve { .. } => false,
            };
            if found {
                return true;
            }
        }
        false
    }
    stmts_need_views(stmts)
}

/// Returns `true` when the handler body calls `agents.record_memory` specifically
/// (which requires the `MemoryEntry` import).
fn body_uses_record_memory(stmts: &[IrStmt]) -> bool {
    fn expr_uses_record_memory(expr: &IrExprNode) -> bool {
        match &expr.kind {
            IrExpr::NamespaceCall { ns, method, .. }
                if *ns == NamespaceId::Agents && method == "record_memory" =>
            {
                true
            }
            IrExpr::NamespaceCall { args, .. }
            | IrExpr::BuiltinCall(_, args)
            | IrExpr::UnresolvedCall(_, args)
            | IrExpr::ViewCall(_, args) => {
                args.iter().any(|a| expr_uses_record_memory(&a.value))
            }
            IrExpr::Binary(_, l, r) => {
                expr_uses_record_memory(l) || expr_uses_record_memory(r)
            }
            IrExpr::Unary(_, r) => expr_uses_record_memory(r),
            _ => false,
        }
    }
    fn stmts_use_record_memory(stmts: &[IrStmt]) -> bool {
        for s in stmts {
            let found = match s {
                IrStmt::Expr(e) => expr_uses_record_memory(e),
                IrStmt::Let { value, .. } => expr_uses_record_memory(value),
                IrStmt::SelfUpdate { value, .. } => expr_uses_record_memory(value),
                IrStmt::If { cond, then_body, else_body, .. } => {
                    expr_uses_record_memory(cond)
                        || stmts_use_record_memory(then_body)
                        || else_body.as_ref().map_or(false, |b| stmts_use_record_memory(b))
                }
                IrStmt::For { iter, body, .. } => {
                    expr_uses_record_memory(iter) || stmts_use_record_memory(body)
                }
                IrStmt::Match { scrutinee, arms, .. } => {
                    expr_uses_record_memory(scrutinee)
                        || arms.iter().any(|a| stmts_use_record_memory(&a.body))
                }
                IrStmt::Emit(_) | IrStmt::BeliefObserve { .. } => false,
            };
            if found {
                return true;
            }
        }
        false
    }
    stmts_use_record_memory(stmts)
}

fn scan_emits(stmts: &[IrStmt]) -> Result<(), EmitError> {
    for s in stmts {
        match s {
            IrStmt::Emit(e) => {
                if e.event.is_none() {
                    return Err(EmitError::UnresolvedEventInEmit(e.event_name.clone()));
                }
            }
            IrStmt::If { then_body, else_body, .. } => {
                scan_emits(then_body)?;
                if let Some(b) = else_body {
                    scan_emits(b)?;
                }
            }
            IrStmt::For { body, .. } => scan_emits(body)?,
            IrStmt::Match { arms, .. } => {
                for arm in arms {
                    scan_emits(&arm.body)?;
                }
            }
            IrStmt::Let { .. }
            | IrStmt::Expr(_)
            | IrStmt::SelfUpdate { .. }
            | IrStmt::BeliefObserve { .. } => {}
        }
    }
    Ok(())
}

fn emit_handler_fn(
    out: &mut String,
    physics: &PhysicsIR,
    ctx: &EmitContext<'_>,
) -> Result<(), EmitError> {
    if physics.handlers.len() != 1 {
        return Err(EmitError::Unsupported(format!(
            "expected exactly one `on` handler per physics rule (got {} in `{}`)",
            physics.handlers.len(),
            physics.name
        )));
    }
    let handler = &physics.handlers[0];
    let fn_name = handler_fn_name(&physics.name);
    let args = handler_arg_types(handler, ctx)?;

    let rule_needs_views = rule_body_needs_views(&handler.body);
    writeln!(out, "#[allow(unused_variables)]").unwrap();
    write!(out, "pub fn {fn_name}(").unwrap();
    for (name, ty) in &args {
        write!(out, "{name}: {}, ", render_type(ty)).unwrap();
    }
    if rule_needs_views {
        writeln!(out, "state: &mut SimState, views: &mut ViewRegistry, events: &mut EventRing<Event>) {{").unwrap();
    } else {
        writeln!(out, "state: &mut SimState, events: &mut EventRing<Event>) {{").unwrap();
    }

    if let Some(where_clause) = &handler.where_clause {
        let cond = lower_expr(where_clause)?;
        writeln!(out, "    if !({cond}) {{ return; }}").unwrap();
    }

    // For view-mutating rules, flatten a single `if cond { view_call }` body
    // into just the view call. This matches the hand-corrected committed files
    // where the view mutation is unconditional (the view handles no-op values).
    let effective_body: &[IrStmt] = if rule_needs_views
        && handler.body.len() == 1
        && handler.where_clause.is_none()
    {
        match &handler.body[0] {
            IrStmt::If { then_body, else_body, .. }
                if else_body.is_none()
                    && then_body.len() == 1
                    && rule_body_needs_views(then_body) =>
            {
                then_body.as_slice()
            }
            _ => &handler.body,
        }
    } else {
        &handler.body
    };

    for stmt in effective_body {
        emit_stmt(out, stmt, 4)?;
    }

    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Return the `(binding_name, type)` pairs the handler exposes as arguments.
/// For a kind-matched handler, types come from the event declaration. For a
/// tag-matched handler, types come from the tag declaration (plus `tick:
/// u32` if bound).
fn handler_arg_types(
    handler: &PhysicsHandlerIR,
    ctx: &EmitContext<'_>,
) -> Result<Vec<(String, IrType)>, EmitError> {
    let mut out = Vec::new();
    match &handler.pattern {
        IrPhysicsPattern::Kind(pat) => {
            let Some(event_ref) = pat.event else {
                return Err(EmitError::UnresolvedEventInPattern(pat.name.clone()));
            };
            let event = &ctx.events[event_ref.0 as usize];
            let fields = fields_with_implicit_tick(event);
            for b in &pat.bindings {
                if let IrPattern::Bind { name, .. } = &b.value {
                    let Some(field) = fields.iter().find(|f| f.name == b.field) else {
                        return Err(EmitError::Unsupported(format!(
                            "handler binds unknown field `{}` on event `{}`",
                            b.field, event.name
                        )));
                    };
                    out.push((name.clone(), field.ty.clone()));
                }
            }
        }
        IrPhysicsPattern::Tag { tag, bindings, name, .. } => {
            let Some(tref) = tag else {
                return Err(EmitError::UnresolvedEventInPattern(format!("@{name}")));
            };
            let tag_ir = &ctx.event_tags[tref.0 as usize];
            for b in bindings {
                if let IrPattern::Bind { name: bname, .. } = &b.value {
                    let ty = if b.field == "tick" {
                        IrType::U32
                    } else {
                        let Some(field) = tag_ir.fields.iter().find(|f| f.name == b.field) else {
                            return Err(EmitError::Unsupported(format!(
                                "handler binds unknown field `{}` on tag `@{}`",
                                b.field, tag_ir.name
                            )));
                        };
                        field.ty.clone()
                    };
                    out.push((bname.clone(), ty));
                }
            }
        }
    }
    Ok(out)
}

