fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(out, "// GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
}

fn emit_imports_lazy(out: &mut String) {
    writeln!(out, "use crate::ids::AgentId;").unwrap();
    writeln!(out, "use crate::state::SimState;").unwrap();
    writeln!(out).unwrap();
}

fn emit_imports_materialized(out: &mut String, uses_hashmap: bool) {
    if uses_hashmap {
        writeln!(out, "use std::collections::HashMap;").unwrap();
        writeln!(out).unwrap();
    }
    writeln!(out, "use crate::event::Event;").unwrap();
    writeln!(out, "use crate::ids::AgentId;").unwrap();
    writeln!(out).unwrap();
}

// ---------------------------------------------------------------------------
// @lazy view fn emission
// ---------------------------------------------------------------------------

fn emit_lazy_fn(out: &mut String, view: &ViewIR) -> Result<(), EmitError> {
    let body = match &view.body {
        ViewBodyIR::Expr(e) => e,
        ViewBodyIR::Fold { .. } => {
            return Err(EmitError::Unsupported(format!(
                "`@lazy` view `{}` has a fold body — resolver should have rejected this",
                view.name
            )));
        }
    };

    let fn_name = snake_case(&view.name);
    let ret_ty = rust_type_for(&view.return_ty)?;
    let mut params = vec!["state: &SimState".to_string()];
    for p in &view.params {
        let ty = rust_type_for(&p.ty)?;
        params.push(format!("{}: {ty}", p.name));
    }

    writeln!(
        out,
        "/// @lazy view — lowered from `view {}(...)` in the sim DSL.",
        view.name
    )
    .unwrap();
    writeln!(
        out,
        "/// Pure expression body; re-evaluated on each call. Spec §2.3."
    )
    .unwrap();
    writeln!(
        out,
        "pub fn {fn_name}({}) -> {ret_ty} {{",
        params.join(", ")
    )
    .unwrap();
    let lowered = lower_expr(body)?;
    writeln!(out, "    {lowered}").unwrap();
    writeln!(out, "}}").unwrap();
    // `state` may be unused by a degenerate body (e.g. a literal); suppress
    // the would-be warning once per fn via `let _ = state;` below if so.
    // Instead of tracking referential use here, rely on the emitter's call
    // conventions: every stdlib-namespace and unresolved call emits
    // `state.*` / `crate::rules::*(state, ...)`, so real-world bodies
    // touch `state`. A trivial literal body is a smell worth surfacing.
    Ok(())
}

// ---------------------------------------------------------------------------
// @materialized view emission
// ---------------------------------------------------------------------------

fn emit_materialized_struct(
    out: &mut String,
    view: &ViewIR,
    events: &[EventIR],
    storage: StorageHint,
) -> Result<(), EmitError> {
    let (initial, handlers, clamp) = match &view.body {
        ViewBodyIR::Fold { initial, handlers, clamp } => (initial, handlers, clamp.as_ref()),
        ViewBodyIR::Expr(_) => {
            return Err(EmitError::Unsupported(format!(
                "`@materialized` view `{}` has a non-fold body — resolver should have caught this",
                view.name
            )));
        }
    };

    match storage {
        StorageHint::PairMap => {
            emit_pair_map_struct(out, view, initial, handlers, clamp, view.decay.as_ref())
        }
        StorageHint::PerEntityTopK { k, .. } => {
            // K=1 keeps the task-139 slot_map shape (AgentId partner per
            // observer, no decay). K>1 lowers to the bounded-topk shape
            // added in task 196 — a Vec<[TopkSlot; K]> keyed by observer
            // slot, with find-or-evict-else-drop fold semantics.
            if k == 1 {
                if view.decay.is_some() {
                    return Err(EmitError::Unsupported(format!(
                        "`per_entity_topk(K=1)` storage on view `{}` does not support `@decay` (task 139; use K>=2 for topk+decay)",
                        view.name
                    )));
                }
                emit_per_entity_topk1_struct(out, view, handlers)
            } else {
                emit_per_entity_topk_k_struct(
                    out,
                    view,
                    initial,
                    handlers,
                    clamp,
                    view.decay.as_ref(),
                    k as usize,
                )
            }
        }
        StorageHint::SymmetricPairTopK { k } => emit_symmetric_pair_topk_struct(
            out,
            view,
            events,
            initial,
            handlers,
            clamp,
            view.decay.as_ref(),
            k as usize,
        ),
        StorageHint::PerEntityRing { k } => emit_per_entity_ring_struct(
            out,
            view,
            initial,
            handlers,
            clamp,
            view.decay.as_ref(),
            k as usize,
        ),
        StorageHint::LazyCached => Err(EmitError::Unsupported(format!(
            "`lazy_cached` storage hint on view `{}` is not implemented yet (spec §9 D31)",
            view.name
        ))),
    }
}

/// Emit the pair-map shape. Keys are built from the view's two params
/// (enforced shape check); values are the view's return type. `@decay`
/// switches the storage to the anchor-pattern `(base, anchor_tick)`
/// 2-tuple and the getter computes `base * rate^(tick - anchor)` with the
/// clamp applied.
fn emit_pair_map_struct(
    out: &mut String,
    view: &ViewIR,
    initial: &IrExprNode,
    handlers: &[FoldHandlerIR],
    clamp: Option<&(IrExprNode, IrExprNode)>,
    decay: Option<&crate::ir::DecayHint>,
) -> Result<(), EmitError> {
    if view.params.len() != 2 {
        return Err(EmitError::Unsupported(format!(
            "`pair_map` storage on view `{}` requires exactly 2 params; got {}",
            view.name,
            view.params.len()
        )));
    }
    let val_ty = rust_type_for(&view.return_ty)?;
    let k1 = rust_type_for(&view.params[0].ty)?;
    let k2 = rust_type_for(&view.params[1].ty)?;
    let a_name = view.params[0].name.as_str();
    let b_name = view.params[1].name.as_str();
    let ty_name = pascal_case(&view.name);

    writeln!(
        out,
        "/// @materialized view `{}` — `storage = pair_map<({k1}, {k2}), {val_ty}>`.",
        view.name
    )
    .unwrap();
    if let Some(d) = decay {
        let per = match d.per {
            DecayUnit::Tick => "tick",
        };
        writeln!(
            out,
            "/// @decay(rate = {}, per = {per}) — anchor-pattern storage. Spec §2.3.",
            d.rate
        )
        .unwrap();
    }
    writeln!(out, "#[derive(Debug, Default)]").unwrap();
    writeln!(out, "pub struct {ty_name} {{").unwrap();
    if decay.is_some() {
        writeln!(
            out,
            "    /// `(base_at_anchor, anchor_tick)` per-pair. Observable value at `tick`",
        )
        .unwrap();
        writeln!(out, "    /// is `base * rate.powi((tick - anchor) as i32)` clamped.").unwrap();
        writeln!(out, "    value: HashMap<({k1}, {k2}), ({val_ty}, u32)>,").unwrap();
    } else {
        writeln!(out, "    value: HashMap<({k1}, {k2}), {val_ty}>,").unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {ty_name} {{").unwrap();
    if let Some(d) = decay {
        writeln!(
            out,
            "    /// Decay rate per tick — compile-time constant from `@decay(rate = {})`.",
            d.rate
        )
        .unwrap();
        writeln!(
            out,
            "    pub const RATE: {val_ty} = {}_f32;",
            format_f32_lit(d.rate)
        )
        .unwrap();
        writeln!(out).unwrap();
    }
    writeln!(out, "    pub fn new() -> Self {{ Self::default() }}").unwrap();
    writeln!(out).unwrap();

    // `len()` + `contains` are handy for tests and don't add surface area.
    writeln!(out, "    pub fn len(&self) -> usize {{ self.value.len() }}").unwrap();
    writeln!(
        out,
        "    pub fn is_empty(&self) -> bool {{ self.value.is_empty() }}"
    )
    .unwrap();
    writeln!(out).unwrap();

    // get() signature depends on whether @decay is present (needs tick).
    let initial_lit = lower_scalar_literal(initial, &view.return_ty)?;
    if decay.is_some() {
        writeln!(
            out,
            "    /// Current value at `tick`, decayed from the anchor."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn get(&self, {a_name}: {k1}, {b_name}: {k2}, tick: u32) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(
            out,
            "        let (base, anchor) = self.value.get(&({a_name}, {b_name})).copied()"
        )
        .unwrap();
        writeln!(out, "            .unwrap_or(({initial_lit}, tick));").unwrap();
        writeln!(
            out,
            "        let decayed = base * Self::RATE.powi(tick.saturating_sub(anchor) as i32);"
        )
        .unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "        decayed.clamp({lo_s}, {hi_s})").unwrap();
        } else {
            writeln!(out, "        decayed").unwrap();
        }
        writeln!(out, "    }}").unwrap();
        writeln!(out).unwrap();
        // Wildcard-slot helper for scoring's view-call predicates. The
        // `_` second-arg sentinel (ARG_WILDCARD) sums the decayed value
        // over every recorded second-argument pair. Emitted for every
        // decay+pair_map view so scoring can reference any of them with
        // a wildcard slot; cost is one extra O(|pair_map|) method on
        // the generated struct.
        writeln!(
            out,
            "    /// Σ get({a_name}, x, tick) over every recorded second-argument `x`."
        )
        .unwrap();
        writeln!(
            out,
            "    /// Used by scoring's view-call wildcard slot (`_`). O(|pair_map|)."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn sum_for_first(&self, {a_name}: {k1}, tick: u32) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(
            out,
            "        let mut total: {val_ty} = {initial_lit};"
        )
        .unwrap();
        writeln!(
            out,
            "        for (&(k_a, _k_b), &(base, anchor)) in self.value.iter() {{"
        )
        .unwrap();
        writeln!(out, "            if k_a != {a_name} {{ continue; }}").unwrap();
        writeln!(
            out,
            "            let decayed = base * Self::RATE.powi(tick.saturating_sub(anchor) as i32);"
        )
        .unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "            total += decayed.clamp({lo_s}, {hi_s});").unwrap();
        } else {
            writeln!(out, "            total += decayed;").unwrap();
        }
        writeln!(out, "        }}").unwrap();
        writeln!(out, "        total").unwrap();
        writeln!(out, "    }}").unwrap();
    } else {
        writeln!(out, "    /// Current value for the given pair.").unwrap();
        writeln!(
            out,
            "    pub fn get(&self, {a_name}: {k1}, {b_name}: {k2}) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(
            out,
            "        self.value.get(&({a_name}, {b_name})).copied().unwrap_or({initial_lit})"
        )
        .unwrap();
        writeln!(out, "    }}").unwrap();
    }
    writeln!(out).unwrap();

    // fold_event() dispatch. One match arm per declared handler. Anything
    // we don't know how to lower gets a `// TODO` with the delta baked
    // in from the generic `self += amount` fallback. The fallback is only
    // reached for events that carry a `damage` or `delta` field — we
    // detect the shape at emission time.
    writeln!(
        out,
        "    /// Advance / accumulate on each matching event. Spec §7.1 view-fold phase."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn fold_event(&mut self, event: &Event, tick: u32) {{"
    )
    .unwrap();
    if handlers.is_empty() {
        writeln!(out, "        let _ = (event, tick);").unwrap();
    } else {
        writeln!(out, "        match event {{").unwrap();
        for h in handlers {
            emit_fold_arm(out, view, a_name, b_name, &initial_lit, h, clamp, decay)?;
        }
        writeln!(out, "            _ => {{}}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Emit the `per_entity_topk(K=1)` shape. Storage is a bare `HashMap<Key,
/// Val>` — one slot per agent. Task 139 targets the `engaged_with` view
/// where the key is the agent asking "who am I engaged with?" and the
/// value is the partner. Writers follow a two-event convention:
///
/// - An event carrying an `actor` + `target` pair (e.g. `EngagementCommitted`)
///   folds as `insert(actor, target); insert(target, actor)` so the pairing
///   is bidirectional without a second event emission.
/// - An event carrying `actor` + `former_target` (e.g. `EngagementBroken`)
///   folds as `remove(actor); remove(former_target)` — the fold itself
///   tears down both sides rather than requiring the emitter to spit a
///   paired event.
///
/// The emitter recognises these conventions by scanning the handler
/// pattern's bindings; other shapes raise `Unsupported` so mis-wired
/// handlers surface at compile time rather than silently folding
/// nothing.
fn emit_per_entity_topk1_struct(
    out: &mut String,
    view: &ViewIR,
    handlers: &[FoldHandlerIR],
) -> Result<(), EmitError> {
    if view.params.len() != 1 {
        return Err(EmitError::Unsupported(format!(
            "`per_entity_topk(1)` storage on view `{}` requires exactly 1 param; got {}",
            view.name,
            view.params.len()
        )));
    }
    let key_ty = rust_type_for(&view.params[0].ty)?;
    let val_ty = rust_type_for(&view.return_ty)?;
    let key_name = view.params[0].name.as_str();
    let ty_name = pascal_case(&view.name);

    writeln!(
        out,
        "/// @materialized view `{}` — `storage = per_entity_topk(K=1)` over `HashMap<{key_ty}, {val_ty}>`.",
        view.name
    )
    .unwrap();
    writeln!(
        out,
        "/// Single-slot per key; insert/remove driven by paired `*Committed` / `*Broken`"
    )
    .unwrap();
    writeln!(
        out,
        "/// events (task 139). See `dsl_compiler::emit_view::emit_per_entity_topk1_struct`"
    )
    .unwrap();
    writeln!(out, "/// for the fold convention.").unwrap();
    writeln!(out, "#[derive(Debug, Default)]").unwrap();
    writeln!(out, "pub struct {ty_name} {{").unwrap();
    writeln!(out, "    value: HashMap<{key_ty}, {val_ty}>,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {ty_name} {{").unwrap();
    writeln!(out, "    pub fn new() -> Self {{ Self::default() }}").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    pub fn len(&self) -> usize {{ self.value.len() }}").unwrap();
    writeln!(
        out,
        "    pub fn is_empty(&self) -> bool {{ self.value.is_empty() }}"
    )
    .unwrap();
    writeln!(out).unwrap();

    // get() returns Option<Val> — a key may have no slot filled.
    writeln!(
        out,
        "    /// Current slot for `{key_name}`, if any. `None` when the key has no partner."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn get(&self, {key_name}: {key_ty}) -> Option<{val_ty}> {{"
    )
    .unwrap();
    writeln!(
        out,
        "        self.value.get(&{key_name}).copied()"
    )
    .unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // Escape hatch for the engine's legacy setter (`set_agent_engaged_with`) —
    // tests and pre-event-driven code still seed slots directly.
    writeln!(
        out,
        "    /// Direct slot setter. Used by the engine's back-compat `set_*`"
    )
    .unwrap();
    writeln!(
        out,
        "    /// wrappers; game logic should prefer emitting the paired commit /"
    )
    .unwrap();
    writeln!(
        out,
        "    /// break events and letting the fold place the slot."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn set(&mut self, {key_name}: {key_ty}, v: Option<{val_ty}>) {{"
    )
    .unwrap();
    writeln!(out, "        match v {{").unwrap();
    writeln!(out, "            Some(val) => {{ self.value.insert({key_name}, val); }}").unwrap();
    writeln!(out, "            None => {{ self.value.remove(&{key_name}); }}").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    writeln!(
        out,
        "    /// Advance / accumulate on each matching event. Spec §7.1 view-fold phase."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn fold_event(&mut self, event: &Event, tick: u32) {{"
    )
    .unwrap();
    if handlers.is_empty() {
        writeln!(out, "        let _ = (event, tick);").unwrap();
    } else {
        writeln!(out, "        let _ = tick;").unwrap();
        writeln!(out, "        match event {{").unwrap();
        for h in handlers {
            emit_per_entity_topk1_fold_arm(out, view, h)?;
        }
        writeln!(out, "            _ => {{}}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Emit one match arm for a `per_entity_topk(1)` fold handler. We key off
/// the pattern's field names (not the binding local name) so the emission
/// is robust to authors picking whatever binding names read nicely:
///
/// - A pattern with a `former_target` field is treated as the "break" arm —
///   remove the slots for both `actor` and `former_target`.
/// - Anything else is treated as a "commit" arm — insert the pairing both
///   ways (`actor → target`, `target → actor`).
///
/// Both shapes use `..` in the destructure so cosmetic additions to the
/// event body don't break the match.
fn emit_per_entity_topk1_fold_arm(
    out: &mut String,
    view: &ViewIR,
    handler: &FoldHandlerIR,
) -> Result<(), EmitError> {
    let ev_name = handler.pattern.name.as_str();
    let field_names: Vec<&str> = handler
        .pattern
        .bindings
        .iter()
        .map(|b| b.field.as_str())
        .collect();
    let has_former = field_names.iter().any(|f| *f == "former_target");

    if has_former {
        // Break arm — remove both sides. We rely on the event carrying
        // `actor` + `former_target`; anything else is a mis-wired pattern
        // the resolver should have rejected.
        writeln!(
            out,
            "            Event::{ev_name} {{ actor, former_target, .. }} => {{"
        )
        .unwrap();
        writeln!(out, "                self.value.remove(actor);").unwrap();
        writeln!(out, "                self.value.remove(former_target);").unwrap();
        writeln!(out, "            }}").unwrap();
    } else {
        // Commit arm — bidirectional insert. Both legs point to each
        // other so `get(actor) == Some(target)` and vice versa.
        writeln!(
            out,
            "            Event::{ev_name} {{ actor, target, .. }} => {{"
        )
        .unwrap();
        writeln!(out, "                self.value.insert(*actor, *target);").unwrap();
        writeln!(out, "                self.value.insert(*target, *actor);").unwrap();
        writeln!(out, "            }}").unwrap();
    }
    let _ = view;
    Ok(())
}

/// Emit the `per_entity_topk(K=N)` shape for N >= 2 (task 196). Storage
/// is `slots: Vec<[TopkSlot; K]>`, one array per observer slot. The fold
/// path follows find-or-evict-else-drop semantics:
///
///  1. Scan the K slots for `id == attacker`.
///  2. If found: accumulate (`value = clamp(value + delta)`), update
///     anchor (for @decay views). Anchor refreshes to `tick` so decay
///     math matches the pair_map baseline.
///  3. If not found and any slot is empty (id == 0): insert.
///  4. If not found and K full: find the slot whose *decayed* value is
///     smallest; if `delta > that value`, evict and insert; otherwise
///     drop the fold.
///
/// Reads (`get(a, b)` / `get(a, b, tick)`) scan the K slots for
/// `id == b`, apply decay if configured, and return `initial` if
/// the pair isn't stored. The wildcard-sum `sum_for_first(a, tick)`
/// walks the K slots and sums decayed values — the semantic drift
/// versus dense pair_map is: "sum across observer's top-K
/// attackers" rather than "sum across every recorded pair". The
/// drift is documented in `assets/sim/views.sim`.
fn emit_per_entity_topk_k_struct(
    out: &mut String,
    view: &ViewIR,
    initial: &IrExprNode,
    handlers: &[FoldHandlerIR],
    clamp: Option<&(IrExprNode, IrExprNode)>,
    decay: Option<&crate::ir::DecayHint>,
    k: usize,
) -> Result<(), EmitError> {
    if view.params.len() != 2 {
        return Err(EmitError::Unsupported(format!(
            "`per_entity_topk(K={k})` storage on view `{}` requires exactly 2 params; got {}",
            view.name,
            view.params.len()
        )));
    }
    let val_ty = rust_type_for(&view.return_ty)?;
    let k1 = rust_type_for(&view.params[0].ty)?;
    let k2 = rust_type_for(&view.params[1].ty)?;
    let a_name = view.params[0].name.as_str();
    let b_name = view.params[1].name.as_str();
    let ty_name = pascal_case(&view.name);
    let initial_lit = lower_scalar_literal(initial, &view.return_ty)?;

    writeln!(
        out,
        "/// @materialized view `{}` — `storage = per_entity_topk(K={k})`.",
        view.name
    )
    .unwrap();
    if let Some(d) = decay {
        let per = match d.per {
            DecayUnit::Tick => "tick",
        };
        writeln!(
            out,
            "/// @decay(rate = {}, per = {per}) — anchor-pattern decay applied on read.",
            d.rate
        )
        .unwrap();
    }
    writeln!(
        out,
        "/// Storage: one `[TopkSlot; {k}]` per observer slot in a `Vec`. Total footprint"
    )
    .unwrap();
    writeln!(
        out,
        "/// is O(N·K) — at K={k} and N=200k that's {} MB vs the dense pair_map's",
        (k * 12 * 200_000) / (1024 * 1024)
    )
    .unwrap();
    writeln!(
        out,
        "/// O(N²·4) ≈ 160 GB. Fold semantics: find-or-evict-else-drop (task 196)."
    )
    .unwrap();

    // Inner slot struct. Name-spaced to the view's module so callers
    // don't collide with other topk views' slot types.
    writeln!(out, "#[derive(Debug, Clone, Copy, Default)]").unwrap();
    writeln!(out, "pub struct TopkSlot {{").unwrap();
    writeln!(out, "    /// Raw AgentId (1-based). 0 means empty.").unwrap();
    writeln!(out, "    pub id: u32,").unwrap();
    writeln!(out, "    /// Stored value; decay (if configured) applies on read.").unwrap();
    writeln!(out, "    pub value: {val_ty},").unwrap();
    writeln!(out, "    /// Tick when `value` was last written (@decay anchor).").unwrap();
    writeln!(out, "    pub anchor_tick: u32,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "#[derive(Debug, Default)]").unwrap();
    writeln!(out, "pub struct {ty_name} {{").unwrap();
    writeln!(
        out,
        "    /// One array of {k} slots per observer. `Vec::len()` grows on demand"
    )
    .unwrap();
    writeln!(out, "    /// as `fold_event` sees higher observer ids.").unwrap();
    writeln!(out, "    slots: Vec<[TopkSlot; {k}]>,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {ty_name} {{").unwrap();
    writeln!(out, "    /// Slot count per observer — the `K` from `per_entity_topk(K={k})`.").unwrap();
    writeln!(out, "    pub const K: usize = {k};").unwrap();
    if let Some(d) = decay {
        writeln!(out).unwrap();
        writeln!(
            out,
            "    /// Decay rate per tick — compile-time constant from `@decay(rate = {})`.",
            d.rate
        )
        .unwrap();
        writeln!(
            out,
            "    pub const RATE: {val_ty} = {}_f32;",
            format_f32_lit(d.rate)
        )
        .unwrap();
    }
    writeln!(out).unwrap();
    writeln!(out, "    pub fn new() -> Self {{ Self::default() }}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "    /// Number of observer slots currently provisioned. `fold_event` grows"
    )
    .unwrap();
    writeln!(out, "    /// this on demand up to `max_observer + 1`.").unwrap();
    writeln!(out, "    pub fn len(&self) -> usize {{ self.slots.len() }}").unwrap();
    writeln!(
        out,
        "    pub fn is_empty(&self) -> bool {{ self.slots.is_empty() }}"
    )
    .unwrap();
    writeln!(out).unwrap();

    // get(): signature depends on decay. Both flavours scan K slots.
    if decay.is_some() {
        writeln!(
            out,
            "    /// Current value for `{b_name}` as observed by `{a_name}`,"
        )
        .unwrap();
        writeln!(
            out,
            "    /// decayed from its anchor tick. Returns `initial` when the pair isn't in {a_name}'s top-{k}."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn get(&self, {a_name}: {k1}, {b_name}: {k2}, tick: u32) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(out, "        let obs_slot = ({a_name}.raw() - 1) as usize;").unwrap();
        writeln!(out, "        let atk_id = {b_name}.raw();").unwrap();
        writeln!(
            out,
            "        let Some(row) = self.slots.get(obs_slot) else {{ return {initial_lit}; }};"
        )
        .unwrap();
        writeln!(out, "        for s in row.iter() {{").unwrap();
        writeln!(out, "            if s.id == atk_id {{").unwrap();
        writeln!(
            out,
            "                let dt = tick.saturating_sub(s.anchor_tick);"
        )
        .unwrap();
        writeln!(
            out,
            "                let decayed = s.value * Self::RATE.powi(dt as i32);"
        )
        .unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "                return decayed.clamp({lo_s}, {hi_s});").unwrap();
        } else {
            writeln!(out, "                return decayed;").unwrap();
        }
        writeln!(out, "            }}").unwrap();
        writeln!(out, "        }}").unwrap();
        writeln!(out, "        {initial_lit}").unwrap();
        writeln!(out, "    }}").unwrap();
        writeln!(out).unwrap();
        // sum_for_first — now sums across at-most-K slots.
        writeln!(
            out,
            "    /// Σ get({a_name}, x, tick) over `{a_name}`'s top-{k} slots."
        )
        .unwrap();
        writeln!(
            out,
            "    /// Semantic drift vs dense pair_map: sum is over the top-K attackers, not every recorded pair."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn sum_for_first(&self, {a_name}: {k1}, tick: u32) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(out, "        let obs_slot = ({a_name}.raw() - 1) as usize;").unwrap();
        writeln!(
            out,
            "        let Some(row) = self.slots.get(obs_slot) else {{ return {initial_lit}; }};"
        )
        .unwrap();
        writeln!(out, "        let mut total: {val_ty} = {initial_lit};").unwrap();
        writeln!(out, "        for s in row.iter() {{").unwrap();
        writeln!(out, "            if s.id == 0 {{ continue; }}").unwrap();
        writeln!(
            out,
            "            let dt = tick.saturating_sub(s.anchor_tick);"
        )
        .unwrap();
        writeln!(
            out,
            "            let decayed = s.value * Self::RATE.powi(dt as i32);"
        )
        .unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "            total += decayed.clamp({lo_s}, {hi_s});").unwrap();
        } else {
            writeln!(out, "            total += decayed;").unwrap();
        }
        writeln!(out, "        }}").unwrap();
        writeln!(out, "        total").unwrap();
        writeln!(out, "    }}").unwrap();
    } else {
        writeln!(
            out,
            "    /// Current value for `{b_name}` as observed by `{a_name}`."
        )
        .unwrap();
        writeln!(
            out,
            "    /// Returns `initial` when the pair isn't in {a_name}'s top-{k}."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn get(&self, {a_name}: {k1}, {b_name}: {k2}) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(out, "        let obs_slot = ({a_name}.raw() - 1) as usize;").unwrap();
        writeln!(out, "        let atk_id = {b_name}.raw();").unwrap();
        writeln!(
            out,
            "        let Some(row) = self.slots.get(obs_slot) else {{ return {initial_lit}; }};"
        )
        .unwrap();
        writeln!(out, "        for s in row.iter() {{").unwrap();
        writeln!(out, "            if s.id == atk_id {{ return s.value; }}").unwrap();
        writeln!(out, "        }}").unwrap();
        writeln!(out, "        {initial_lit}").unwrap();
        writeln!(out, "    }}").unwrap();
        writeln!(out).unwrap();
        // sum_for_first also useful even without decay — emit it for
        // consistency so scoring's `_` wildcard slot works across all
        // topk views.
        writeln!(
            out,
            "    /// Σ get({a_name}, x) over `{a_name}`'s top-{k} slots."
        )
        .unwrap();
        writeln!(
            out,
            "    /// Semantic drift vs dense pair_map: sum is over top-K attackers, not every recorded pair."
        )
        .unwrap();
        writeln!(
            out,
            "    pub fn sum_for_first(&self, {a_name}: {k1}) -> {val_ty} {{"
        )
        .unwrap();
        writeln!(out, "        let obs_slot = ({a_name}.raw() - 1) as usize;").unwrap();
        writeln!(
            out,
            "        let Some(row) = self.slots.get(obs_slot) else {{ return {initial_lit}; }};"
        )
        .unwrap();
        writeln!(out, "        let mut total: {val_ty} = {initial_lit};").unwrap();
        writeln!(out, "        for s in row.iter() {{").unwrap();
        writeln!(out, "            if s.id == 0 {{ continue; }}").unwrap();
        writeln!(out, "            total += s.value;").unwrap();
        writeln!(out, "        }}").unwrap();
        writeln!(out, "        total").unwrap();
        writeln!(out, "    }}").unwrap();
    }
    writeln!(out).unwrap();

    // Internal row-access helper. Grows `slots` on demand.
    writeln!(
        out,
        "    fn row_mut(&mut self, obs_slot: usize) -> &mut [TopkSlot; {k}] {{"
    )
    .unwrap();
    writeln!(out, "        if obs_slot >= self.slots.len() {{").unwrap();
    writeln!(
        out,
        "            self.slots.resize(obs_slot + 1, [TopkSlot::default(); {k}]);"
    )
    .unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        &mut self.slots[obs_slot]").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // Shared fold core. Does find-or-evict-else-drop for one (obs, atk,
    // delta) triple. Called from every match arm.
    let clamp_lo_lit = if let Some((lo, _)) = clamp {
        Some(lower_scalar_literal(lo, &view.return_ty)?)
    } else {
        None
    };
    let clamp_hi_lit = if let Some((_, hi)) = clamp {
        Some(lower_scalar_literal(hi, &view.return_ty)?)
    } else {
        None
    };
    writeln!(
        out,
        "    /// Fold `delta` into the (observer, attacker) slot. See the"
    )
    .unwrap();
    writeln!(
        out,
        "    /// module docstring above for find-or-evict-else-drop semantics."
    )
    .unwrap();
    writeln!(
        out,
        "    fn fold_one(&mut self, observer: u32, attacker: u32, delta: {val_ty}, tick: u32) {{"
    )
    .unwrap();
    writeln!(
        out,
        "        if observer == 0 || attacker == 0 {{ return; }}"
    )
    .unwrap();
    writeln!(out, "        let obs_slot = (observer - 1) as usize;").unwrap();
    writeln!(out, "        let row = self.row_mut(obs_slot);").unwrap();
    // 1. Try find.
    writeln!(out, "        for i in 0..{k} {{").unwrap();
    writeln!(out, "            if row[i].id == attacker {{").unwrap();
    if decay.is_some() {
        // Decay: current observable = value * rate^(tick - anchor). Add
        // delta to the decayed value (matches pair_map @decay fold).
        writeln!(
            out,
            "                let dt = tick.saturating_sub(row[i].anchor_tick);"
        )
        .unwrap();
        writeln!(
            out,
            "                let current = row[i].value * Self::RATE.powi(dt as i32);"
        )
        .unwrap();
        writeln!(out, "                let updated = current + delta;").unwrap();
    } else {
        writeln!(out, "                let updated = row[i].value + delta;").unwrap();
    }
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "                let updated = updated.clamp({lo}, {hi});"
        )
        .unwrap();
    }
    writeln!(out, "                row[i].value = updated;").unwrap();
    writeln!(out, "                row[i].anchor_tick = tick;").unwrap();
    writeln!(out, "                return;").unwrap();
    writeln!(out, "            }}").unwrap();
    writeln!(out, "        }}").unwrap();
    // 2. Try empty slot.
    writeln!(out, "        for i in 0..{k} {{").unwrap();
    writeln!(out, "            if row[i].id == 0 {{").unwrap();
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "                let v = delta.clamp({lo}, {hi});"
        )
        .unwrap();
    } else {
        writeln!(out, "                let v = delta;").unwrap();
    }
    writeln!(out, "                row[i] = TopkSlot {{ id: attacker, value: v, anchor_tick: tick }};").unwrap();
    writeln!(out, "                return;").unwrap();
    writeln!(out, "            }}").unwrap();
    writeln!(out, "        }}").unwrap();
    // 3. Evict smallest-value (by current decayed value) if delta beats it.
    if decay.is_some() {
        writeln!(out, "        // Full row — compute each slot's decayed value, evict the smallest").unwrap();
        writeln!(out, "        // if `delta` can outweigh it; otherwise drop the fold.").unwrap();
        writeln!(out, "        let mut min_i: usize = 0;").unwrap();
        writeln!(
            out,
            "        let mut min_v: {val_ty} = {{"
        )
        .unwrap();
        writeln!(
            out,
            "            let dt = tick.saturating_sub(row[0].anchor_tick);"
        )
        .unwrap();
        writeln!(out, "            row[0].value * Self::RATE.powi(dt as i32)").unwrap();
        writeln!(out, "        }};").unwrap();
        writeln!(out, "        for i in 1..{k} {{").unwrap();
        writeln!(
            out,
            "            let dt = tick.saturating_sub(row[i].anchor_tick);"
        )
        .unwrap();
        writeln!(
            out,
            "            let v = row[i].value * Self::RATE.powi(dt as i32);"
        )
        .unwrap();
        writeln!(out, "            if v < min_v {{ min_v = v; min_i = i; }}").unwrap();
        writeln!(out, "        }}").unwrap();
    } else {
        writeln!(out, "        // Full row — evict the smallest-value slot if `delta` beats it,").unwrap();
        writeln!(out, "        // else drop the fold (too weak to displace).").unwrap();
        writeln!(out, "        let mut min_i: usize = 0;").unwrap();
        writeln!(out, "        let mut min_v: {val_ty} = row[0].value;").unwrap();
        writeln!(out, "        for i in 1..{k} {{").unwrap();
        writeln!(out, "            if row[i].value < min_v {{ min_v = row[i].value; min_i = i; }}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "        if delta > min_v {{").unwrap();
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "            let v = delta.clamp({lo}, {hi});"
        )
        .unwrap();
    } else {
        writeln!(out, "            let v = delta;").unwrap();
    }
    writeln!(out, "            row[min_i] = TopkSlot {{ id: attacker, value: v, anchor_tick: tick }};").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // fold_event dispatch. One arm per handler.
    writeln!(
        out,
        "    /// Advance / accumulate on each matching event. Spec §7.1 view-fold phase."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn fold_event(&mut self, event: &Event, tick: u32) {{"
    )
    .unwrap();
    if handlers.is_empty() {
        writeln!(out, "        let _ = (event, tick);").unwrap();
    } else {
        writeln!(out, "        match event {{").unwrap();
        for h in handlers {
            emit_topk_k_fold_arm(out, view, a_name, b_name, h)?;
        }
        writeln!(out, "            _ => {{}}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Emit one match arm for a topk(K>=2) fold handler. Mirrors the
/// pair_map `emit_fold_arm` pattern scan — figure out which event
/// field maps to the view's (observer, attacker) parameter pair, then
/// call the shared `fold_one` with delta = 1.0 (every shipped view
/// folds a constant +1.0; variable deltas raise Unsupported upstream).
fn emit_topk_k_fold_arm(
    out: &mut String,
    view: &ViewIR,
    a_name: &str,
    b_name: &str,
    handler: &FoldHandlerIR,
) -> Result<(), EmitError> {
    let ev_name = handler.pattern.name.as_str();
    // Find which event field feeds which view arg — same heuristic as
    // emit_fold_arm for pair_map (see ~line 780). `actor_field` is the
    // event field bound to the view's first param (observer);
    // `target_field` is bound to the second param (attacker).
    let mut actor_field: Option<&str> = None;
    let mut target_field: Option<&str> = None;
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if name == a_name {
                actor_field = Some(field);
            } else if name == b_name {
                target_field = Some(field);
            }
        }
    }
    let (observer_field, attacker_field) = match (actor_field, target_field) {
        (Some(a), Some(b)) => (a, b),
        _ => ("actor", "target"),
    };
    writeln!(
        out,
        "            Event::{ev_name} {{ {observer_field}, {attacker_field}, .. }} => {{"
    )
    .unwrap();
    writeln!(
        out,
        "                self.fold_one({observer_field}.raw(), {attacker_field}.raw(), 1.0, tick);"
    )
    .unwrap();
    writeln!(out, "            }}").unwrap();
    let _ = view;
    Ok(())
}

/// Emit the `@symmetric_pair_topk(K = N)` shape (GPU cold-state replay plan
/// task 1.5). Storage is `slots: Vec<[SymmetricPairEdge; K]>`, one array
/// per agent. An edge `(a, b)` is always stored on the lower-id endpoint
/// (the "owner") with `other` pointing at the higher-id endpoint. Reads
/// canonicalise the query pair so `get(a, b) == get(b, a)` regardless of
/// which side inserted.
///
/// Fold semantics mirror the `per_entity_topk` K>=2 path: find-or-insert-
/// else-evict-weakest. "Weakest" is the slot with the smallest absolute
/// value (cold-state standings can go negative; eviction on `|v|` keeps
/// both strong allies and strong enemies out of the evict pool).
///
/// The emitter currently folds a constant `+= 1.0` per handler (matches
/// the per_entity_topk K>=2 emitter — variable deltas are out of scope
/// until Phase 3 lowers richer fold bodies).
fn emit_symmetric_pair_topk_struct(
    out: &mut String,
    view: &ViewIR,
    events: &[EventIR],
    initial: &IrExprNode,
    handlers: &[FoldHandlerIR],
    clamp: Option<&(IrExprNode, IrExprNode)>,
    decay: Option<&crate::ir::DecayHint>,
    k: usize,
) -> Result<(), EmitError> {
    if view.params.len() != 2 {
        return Err(EmitError::Unsupported(format!(
            "`symmetric_pair_topk(K={k})` storage on view `{}` requires exactly 2 params (the symmetric pair); got {}",
            view.name,
            view.params.len()
        )));
    }
    if decay.is_some() {
        // Phase 3 can layer decay on top of the anchor-tick slot field;
        // the emitter rejects it upfront for now to avoid emitting code
        // whose semantics haven't been pinned down.
        return Err(EmitError::Unsupported(format!(
            "`symmetric_pair_topk` storage on view `{}` does not support `@decay` yet (GPU cold-state replay plan phase 3)",
            view.name
        )));
    }
    let val_ty = rust_type_for(&view.return_ty)?;
    let k1 = rust_type_for(&view.params[0].ty)?;
    let k2 = rust_type_for(&view.params[1].ty)?;
    let a_name = view.params[0].name.as_str();
    let b_name = view.params[1].name.as_str();
    let ty_name = pascal_case(&view.name);
    let edge_ty = format!("{ty_name}Edge");
    let initial_lit = lower_scalar_literal(initial, &view.return_ty)?;

    writeln!(
        out,
        "/// @materialized view `{}` — `storage = symmetric_pair_topk(K={k})`.",
        view.name
    )
    .unwrap();
    writeln!(
        out,
        "/// Pair-keyed symmetric storage: an edge `({a_name}, {b_name})` is stored once,"
    )
    .unwrap();
    writeln!(
        out,
        "/// on the lower-id endpoint's slot array. Reads canonicalise the query pair so"
    )
    .unwrap();
    writeln!(
        out,
        "/// `get({a_name}, {b_name}) == get({b_name}, {a_name})`. Fold: find-or-insert-else-evict-weakest-|v|."
    )
    .unwrap();

    // Per-edge slot struct. `other == 0` marks an empty slot (AgentId is
    // 1-based, matching the `per_entity_topk` convention).
    writeln!(out, "#[derive(Debug, Clone, Copy, Default)]").unwrap();
    writeln!(out, "pub struct {edge_ty} {{").unwrap();
    writeln!(
        out,
        "    /// Raw AgentId of the higher-id endpoint. 0 means empty."
    )
    .unwrap();
    writeln!(out, "    pub other: u32,").unwrap();
    writeln!(out, "    /// Stored value for the symmetric pair.").unwrap();
    writeln!(out, "    pub value: {val_ty},").unwrap();
    writeln!(
        out,
        "    /// Tick when `value` was last written. Reserved for future decay."
    )
    .unwrap();
    writeln!(out, "    pub anchor_tick: u32,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "#[derive(Debug, Default)]").unwrap();
    writeln!(out, "pub struct {ty_name} {{").unwrap();
    writeln!(
        out,
        "    /// One array of {k} slots per owner agent. `Vec::len()` grows on demand"
    )
    .unwrap();
    writeln!(
        out,
        "    /// as `fold_event` sees higher agent ids. Edges live on the lower-id side."
    )
    .unwrap();
    writeln!(out, "    slots: Vec<[{edge_ty}; {k}]>,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {ty_name} {{").unwrap();
    writeln!(
        out,
        "    /// Slot count per owner — the `K` from `symmetric_pair_topk(K={k})`."
    )
    .unwrap();
    writeln!(out, "    pub const K: usize = {k};").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    pub fn new() -> Self {{ Self::default() }}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "    /// Number of owner slots currently provisioned. `fold_event` grows this"
    )
    .unwrap();
    writeln!(out, "    /// on demand up to `max_id + 1`.").unwrap();
    writeln!(out, "    pub fn len(&self) -> usize {{ self.slots.len() }}").unwrap();
    writeln!(
        out,
        "    pub fn is_empty(&self) -> bool {{ self.slots.is_empty() }}"
    )
    .unwrap();
    writeln!(out).unwrap();

    // canonical_pair helper — min-id owns the edge, max-id is the "other".
    writeln!(
        out,
        "    /// Canonicalise a pair: the lower-id endpoint owns the slot,"
    )
    .unwrap();
    writeln!(out, "    /// the higher-id endpoint is the `other` field.").unwrap();
    writeln!(
        out,
        "    fn canonical_pair({a_name}: {k1}, {b_name}: {k2}) -> ({k1}, {k2}) {{"
    )
    .unwrap();
    writeln!(
        out,
        "        if {a_name}.raw() <= {b_name}.raw() {{ ({a_name}, {b_name}) }} else {{ ({b_name}, {a_name}) }}"
    )
    .unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // get(): canonicalise then scan owner's slot array for `other == max_id`.
    writeln!(
        out,
        "    /// Current value for the symmetric pair `({a_name}, {b_name})`. Pair order"
    )
    .unwrap();
    writeln!(
        out,
        "    /// is canonicalised so `get(x, y) == get(y, x)`. Returns `initial` when the"
    )
    .unwrap();
    writeln!(out, "    /// pair isn't in the owner's top-{k}.").unwrap();
    writeln!(
        out,
        "    pub fn get(&self, {a_name}: {k1}, {b_name}: {k2}) -> {val_ty} {{"
    )
    .unwrap();
    writeln!(
        out,
        "        let (owner, other) = Self::canonical_pair({a_name}, {b_name});"
    )
    .unwrap();
    writeln!(
        out,
        "        if owner.raw() == 0 || other.raw() == 0 {{ return {initial_lit}; }}"
    )
    .unwrap();
    writeln!(out, "        let owner_slot = (owner.raw() - 1) as usize;").unwrap();
    writeln!(out, "        let other_id = other.raw();").unwrap();
    writeln!(
        out,
        "        let Some(row) = self.slots.get(owner_slot) else {{ return {initial_lit}; }};"
    )
    .unwrap();
    writeln!(out, "        for s in row.iter() {{").unwrap();
    writeln!(out, "            if s.other == other_id {{ return s.value; }}").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        {initial_lit}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // Internal row-access helper. Grows `slots` on demand.
    writeln!(
        out,
        "    fn row_mut(&mut self, owner_slot: usize) -> &mut [{edge_ty}; {k}] {{"
    )
    .unwrap();
    writeln!(out, "        if owner_slot >= self.slots.len() {{").unwrap();
    writeln!(
        out,
        "            self.slots.resize(owner_slot + 1, [{edge_ty}::default(); {k}]);"
    )
    .unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        &mut self.slots[owner_slot]").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // adjust(): canonical upsert + clamp. Returns the post-write value.
    let clamp_lo_lit = if let Some((lo, _)) = clamp {
        Some(lower_scalar_literal(lo, &view.return_ty)?)
    } else {
        None
    };
    let clamp_hi_lit = if let Some((_, hi)) = clamp {
        Some(lower_scalar_literal(hi, &view.return_ty)?)
    } else {
        None
    };
    writeln!(
        out,
        "    /// Add `delta` to the symmetric edge `({a_name}, {b_name})` and return the"
    )
    .unwrap();
    writeln!(
        out,
        "    /// resulting value. Upserts on canonical (min, max) order; evicts the"
    )
    .unwrap();
    writeln!(
        out,
        "    /// weakest-|value| slot when full, iff `|delta|` beats the weakest."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn adjust(&mut self, {a_name}: {k1}, {b_name}: {k2}, delta: {val_ty}, tick: u32) -> {val_ty} {{"
    )
    .unwrap();
    writeln!(
        out,
        "        let (owner, other) = Self::canonical_pair({a_name}, {b_name});"
    )
    .unwrap();
    writeln!(
        out,
        "        if owner.raw() == 0 || other.raw() == 0 {{ return {initial_lit}; }}"
    )
    .unwrap();
    writeln!(out, "        let owner_slot = (owner.raw() - 1) as usize;").unwrap();
    writeln!(out, "        let other_id = other.raw();").unwrap();
    writeln!(out, "        let row = self.row_mut(owner_slot);").unwrap();
    // 1. Find existing edge.
    writeln!(out, "        for i in 0..{k} {{").unwrap();
    writeln!(out, "            if row[i].other == other_id {{").unwrap();
    writeln!(out, "                let updated = row[i].value + delta;").unwrap();
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "                let updated = updated.clamp({lo}, {hi});"
        )
        .unwrap();
    }
    writeln!(out, "                row[i].value = updated;").unwrap();
    writeln!(out, "                row[i].anchor_tick = tick;").unwrap();
    writeln!(out, "                return updated;").unwrap();
    writeln!(out, "            }}").unwrap();
    writeln!(out, "        }}").unwrap();
    // 2. Empty slot.
    writeln!(out, "        for i in 0..{k} {{").unwrap();
    writeln!(out, "            if row[i].other == 0 {{").unwrap();
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "                let v = delta.clamp({lo}, {hi});"
        )
        .unwrap();
    } else {
        writeln!(out, "                let v = delta;").unwrap();
    }
    writeln!(
        out,
        "                row[i] = {edge_ty} {{ other: other_id, value: v, anchor_tick: tick }};"
    )
    .unwrap();
    writeln!(out, "                return v;").unwrap();
    writeln!(out, "            }}").unwrap();
    writeln!(out, "        }}").unwrap();
    // 3. Evict smallest-|value| if |delta| beats it.
    writeln!(
        out,
        "        // Full row — evict the slot with the smallest |value| if `|delta|`"
    )
    .unwrap();
    writeln!(
        out,
        "        // beats it, else drop the fold (too weak to displace)."
    )
    .unwrap();
    writeln!(out, "        let mut min_i: usize = 0;").unwrap();
    writeln!(out, "        let mut min_mag: {val_ty} = row[0].value.abs();").unwrap();
    writeln!(out, "        for i in 1..{k} {{").unwrap();
    writeln!(out, "            let m = row[i].value.abs();").unwrap();
    writeln!(out, "            if m < min_mag {{ min_mag = m; min_i = i; }}").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        if delta.abs() > min_mag {{").unwrap();
    if let (Some(lo), Some(hi)) = (&clamp_lo_lit, &clamp_hi_lit) {
        writeln!(
            out,
            "            let v = delta.clamp({lo}, {hi});"
        )
        .unwrap();
    } else {
        writeln!(out, "            let v = delta;").unwrap();
    }
    writeln!(
        out,
        "            row[min_i] = {edge_ty} {{ other: other_id, value: v, anchor_tick: tick }};"
    )
    .unwrap();
    writeln!(out, "            return v;").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        {initial_lit}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // fold_event dispatch. One arm per handler; each pipes the pair fields
    // into `adjust` with a constant +1.0 delta (same convention as the
    // per_entity_topk K>=2 emitter).
    writeln!(
        out,
        "    /// Advance / accumulate on each matching event. Spec §7.1 view-fold phase."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn fold_event(&mut self, event: &Event, tick: u32) {{"
    )
    .unwrap();
    if handlers.is_empty() {
        writeln!(out, "        let _ = (event, tick);").unwrap();
    } else {
        writeln!(out, "        match event {{").unwrap();
        for h in handlers {
            emit_symmetric_pair_fold_arm(out, view, events, a_name, b_name, h)?;
        }
        writeln!(out, "            _ => {{}}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Emit one match arm for a `symmetric_pair_topk` fold handler. Mirrors
/// `emit_topk_k_fold_arm` — figure out which event field maps to the
/// view's first/second pair param, then call `adjust` with the fold
/// body's lowered `self += <expr>` RHS as the delta.
///
/// Only a single `self += <expr>` statement is supported for the body;
/// anything more complex is out of scope until the fold-body language
/// grows enough shapes to justify it. Existing callers (`threat_level`,
/// `kin_fear`, `pack_focus`, etc.) all use literal `self += 1.0` or
/// `self += 1`, which round-trip through `lower_expr` to the same
/// surface form the pre-type-aware emitter produced.
fn emit_symmetric_pair_fold_arm(
    out: &mut String,
    view: &ViewIR,
    events: &[EventIR],
    a_name: &str,
    b_name: &str,
    handler: &FoldHandlerIR,
) -> Result<(), EmitError> {
    let ev_name = handler.pattern.name.as_str();
    let mut first_field: Option<&str> = None;
    let mut second_field: Option<&str> = None;
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if name == a_name {
                first_field = Some(field);
            } else if name == b_name {
                second_field = Some(field);
            }
        }
    }
    // Fall back to canonical `(a, b)` event-field names when the pattern
    // doesn't explicitly rebind — mirrors the per_entity_topk convention.
    let (first_field, second_field) = match (first_field, second_field) {
        (Some(a), Some(b)) => (a, b),
        _ => ("a", "b"),
    };
    let delta_expr = lower_fold_self_update_rhs(&handler.body, &view.name)?;
    // Collect every local referenced by the fold body so the match arm
    // destructures those event fields too. Without this, a `delta`
    // reference in `self += delta` lands on an unbound identifier at
    // compile time because the arm emits `{ a, b, .. }`.
    let mut body_locals: std::collections::BTreeSet<String> =
        std::collections::BTreeSet::new();
    for stmt in &handler.body {
        collect_locals_in_stmt(stmt, &mut body_locals);
    }
    // Build the extra-fields list: every pattern binding whose local
    // name is referenced by the body AND isn't already the (a, b) pair
    // field. Use the event-field name in the destructure; if the
    // pattern renames the field (`delta: d`), bind it with the pattern's
    // local name via `{ <field>: <local>, .. }`.
    let mut extra_fields: Vec<String> = Vec::new();
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if field == first_field || field == second_field {
            continue;
        }
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if body_locals.contains(name) {
                if name == field {
                    extra_fields.push(field.to_string());
                } else {
                    extra_fields.push(format!("{field}: {name}"));
                }
            }
        }
    }
    let destructure = if extra_fields.is_empty() {
        format!("{{ {first_field}, {second_field}, .. }}")
    } else {
        let extras = extra_fields.join(", ");
        format!("{{ {first_field}, {second_field}, {extras}, .. }}")
    };
    writeln!(out, "            Event::{ev_name} {destructure} => {{").unwrap();
    // Pattern destructure binds event fields as references (`&T`). The
    // fold body expects the values themselves, so dereference each
    // referenced pattern local into a shadowing `let` before lowering.
    // Numeric + AgentId types are `Copy`, so the `let name = *name;`
    // pattern is zero-cost.
    //
    // When the event field's IR type is NARROWER than the view's return
    // type (e.g. `i16 delta` on an `-> i32` view), the shadow-deref must
    // also widen the local so the downstream `adjust(..., {local}, ...)`
    // call site sees a value matching the `adjust` signature. Widening
    // uses `as` (primitive numeric). Narrowing is rejected — explicit DSL
    // casts are clearer than silent truncation.
    let event_ir = events.iter().find(|e| e.name == ev_name);
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if field == first_field || field == second_field {
            continue;
        }
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if body_locals.contains(name) {
                let field_ty =
                    event_ir.and_then(|ev| ev.fields.iter().find(|f| f.name == field));
                let cast = match field_ty {
                    Some(f) => coerce_to_view_return(&view.name, name, &f.ty, &view.return_ty)?,
                    // No event IR available (e.g. test calls `emit_view`
                    // with an empty slice, or a synthetic fixture). Fall
                    // back to the identity shadow-deref — correct when
                    // the field type matches the view return type.
                    None => None,
                };
                match cast {
                    Some(coerce) => {
                        writeln!(
                            out,
                            "                let {name}: {target} = *{name} {coerce};",
                            target = rust_type_for(&view.return_ty)?,
                        )
                        .unwrap();
                    }
                    None => {
                        writeln!(out, "                let {name} = *{name};").unwrap();
                    }
                }
            }
        }
    }
    writeln!(
        out,
        "                self.adjust(*{first_field}, *{second_field}, {delta_expr}, tick);"
    )
    .unwrap();
    writeln!(out, "            }}").unwrap();
    Ok(())
}

/// Walk an `IrStmt` collecting every `IrExpr::Local` name it references.
/// Used by `emit_symmetric_pair_fold_arm` to decide which event fields
/// the match arm must destructure for the lowered fold body to compile.
fn collect_locals_in_stmt(
    stmt: &crate::ir::IrStmt,
    out: &mut std::collections::BTreeSet<String>,
) {
    use crate::ir::IrStmt;
    match stmt {
        IrStmt::Let { value, .. } => collect_locals_in_expr(value, out),
        IrStmt::Expr(e) => collect_locals_in_expr(e, out),
        IrStmt::SelfUpdate { value, .. } => collect_locals_in_expr(value, out),
        IrStmt::For { iter, filter, body, .. } => {
            collect_locals_in_expr(iter, out);
            if let Some(f) = filter {
                collect_locals_in_expr(f, out);
            }
            for s in body {
                collect_locals_in_stmt(s, out);
            }
        }
        IrStmt::If { cond, then_body, else_body, .. } => {
            collect_locals_in_expr(cond, out);
            for s in then_body {
                collect_locals_in_stmt(s, out);
            }
            if let Some(eb) = else_body {
                for s in eb {
                    collect_locals_in_stmt(s, out);
                }
            }
        }
        IrStmt::Match { scrutinee, arms, .. } => {
            collect_locals_in_expr(scrutinee, out);
            for arm in arms {
                for s in &arm.body {
                    collect_locals_in_stmt(s, out);
                }
            }
        }
        IrStmt::Emit(e) => {
            for f in &e.fields {
                collect_locals_in_expr(&f.value, out);
            }
        }
    }
}

fn collect_locals_in_expr(
    node: &IrExprNode,
    out: &mut std::collections::BTreeSet<String>,
) {
    // Every `IrExpr` variant is listed explicitly — if a future variant is
    // added (e.g. a new collection literal or a new call kind), the
    // non-exhaustive match will be a compile error rather than silently
    // dropping the body's child expressions and producing `cannot find
    // value X in this scope` at the emitted Rust level.
    match &node.kind {
        IrExpr::Local(_, name) => {
            // `self` is re-emitted as `self_id`; the identifier itself
            // isn't part of the event pattern so we skip it.
            if name != "self" {
                out.insert(name.clone());
            }
        }
        IrExpr::Binary(_, lhs, rhs) => {
            collect_locals_in_expr(lhs, out);
            collect_locals_in_expr(rhs, out);
        }
        IrExpr::Unary(_, inner) => collect_locals_in_expr(inner, out),
        IrExpr::If { cond, then_expr, else_expr } => {
            collect_locals_in_expr(cond, out);
            collect_locals_in_expr(then_expr, out);
            if let Some(e) = else_expr {
                collect_locals_in_expr(e, out);
            }
        }
        IrExpr::NamespaceCall { args, .. } => {
            for a in args {
                collect_locals_in_expr(&a.value, out);
            }
        }
        IrExpr::BuiltinCall(_, args) => {
            for a in args {
                collect_locals_in_expr(&a.value, out);
            }
        }
        IrExpr::UnresolvedCall(_, args) => {
            for a in args {
                collect_locals_in_expr(&a.value, out);
            }
        }
        IrExpr::ViewCall(_, args) => {
            for a in args {
                collect_locals_in_expr(&a.value, out);
            }
        }
        IrExpr::VerbCall(_, args) => {
            for a in args {
                collect_locals_in_expr(&a.value, out);
            }
        }
        IrExpr::Field { base, .. } => collect_locals_in_expr(base, out),
        IrExpr::Index(base, idx) => {
            collect_locals_in_expr(base, out);
            collect_locals_in_expr(idx, out);
        }
        IrExpr::In(lhs, rhs) | IrExpr::Contains(lhs, rhs) => {
            collect_locals_in_expr(lhs, out);
            collect_locals_in_expr(rhs, out);
        }
        IrExpr::Quantifier { iter, body, .. } => {
            // Binder-introducing: v1 ignores shadowing. Worst case the
            // binder's name shows up as an "extra field" we'd try to
            // destructure, but the caller filters to pattern bindings so
            // a stray non-event name is harmless (never added to the
            // arm pattern). If the body references an outer pattern
            // local, we still want to catch it.
            collect_locals_in_expr(iter, out);
            collect_locals_in_expr(body, out);
        }
        IrExpr::Fold { iter, body, .. } => {
            if let Some(it) = iter {
                collect_locals_in_expr(it, out);
            }
            collect_locals_in_expr(body, out);
        }
        IrExpr::List(items) | IrExpr::Tuple(items) => {
            for e in items {
                collect_locals_in_expr(e, out);
            }
        }
        IrExpr::StructLit { fields, .. } => {
            for f in fields {
                collect_locals_in_expr(&f.value, out);
            }
        }
        IrExpr::Ctor { args, .. } => {
            for e in args {
                collect_locals_in_expr(e, out);
            }
        }
        IrExpr::Match { scrutinee, arms } => {
            collect_locals_in_expr(scrutinee, out);
            for arm in arms {
                // Binder-introducing via pattern; same rationale as
                // `Quantifier` above — over-collection is safe.
                collect_locals_in_expr(&arm.body, out);
            }
        }
        IrExpr::PerUnit { expr, delta } => {
            collect_locals_in_expr(expr, out);
            collect_locals_in_expr(delta, out);
        }
        // Pure leaves (no child expressions). Listed explicitly so that
        // adding a new variant forces a decision at compile time.
        IrExpr::LitBool(_)
        | IrExpr::LitInt(_)
        | IrExpr::LitFloat(_)
        | IrExpr::LitString(_)
        | IrExpr::Event(_)
        | IrExpr::Entity(_)
        | IrExpr::View(_)
        | IrExpr::Verb(_)
        | IrExpr::Namespace(_)
        | IrExpr::NamespaceField { .. }
        | IrExpr::EnumVariant { .. }
        // GPU ability evaluation Phase 2 — scoring-only primitives.
        // View fold bodies never see them (the fold-body walker above
        // rejects them first), but the walker lists every variant
        // explicitly so adding a new one forces a compile-time
        // decision here.
        | IrExpr::AbilityTag { .. }
        | IrExpr::AbilityHint
        | IrExpr::AbilityHintLit(_)
        | IrExpr::AbilityRange
        | IrExpr::Raw(_) => {}
        // `AbilityOnCooldown` carries a nested slot expression; walk
        // it to stay consistent with the walker's exhaustive contract
        // even though fold bodies would reject it upstream.
        IrExpr::AbilityOnCooldown(slot) => collect_locals_in_expr(slot, out),
    }
}

/// Extract the RHS of the single `self += <expr>` statement in a fold
/// handler body and lower it via `lower_expr`. Any other body shape
/// (empty, multi-statement, `-=` / `*=`, let-bindings, etc.) raises
/// `Unsupported` pointing at the view name — the emitter intentionally
/// handles only the simple addition case that every existing caller
/// uses.
fn lower_fold_self_update_rhs(
    body: &[crate::ir::IrStmt],
    view_name: &str,
) -> Result<String, EmitError> {
    use crate::ir::IrStmt;
    if body.len() != 1 {
        return Err(EmitError::Unsupported(format!(
            "fold body on view `{view_name}` must be a single `self += <expr>` statement (got {} stmts)",
            body.len()
        )));
    }
    match &body[0] {
        IrStmt::SelfUpdate { op, value, .. } => {
            if op != "+=" {
                return Err(EmitError::Unsupported(format!(
                    "fold body on view `{view_name}` must use `self += <expr>` (got `self {op} ...`)"
                )));
            }
            lower_expr(value)
        }
        _ => Err(EmitError::Unsupported(format!(
            "fold body on view `{view_name}` must be a single `self += <expr>` statement"
        ))),
    }
}

/// Emit the `@per_entity_ring(K = N)` shape (GPU cold-state replay plan
/// task 1.6). Storage is `rings: Vec<[<Name>Entry; K]>` plus a parallel
/// `cursors: Vec<u32>` — one ring + cursor per owner agent. The cursor
/// is monotonic; the write slot is `cursor % K`, and the oldest entry
/// is evicted implicitly on overflow (FIFO).
///
/// Fold semantics: every matched event projects its non-owner fields
/// into an `<Name>Entry`, then `push(observer, entry)` writes into the
/// ring at `cursor % K` and bumps the cursor. No tiebreak, no
/// canonicalisation — the ring doesn't dedupe.
///
/// Structurally simpler than `symmetric_pair_topk` (no owner-chooser,
/// no |v|-evict), so the emitter is shorter. Entry shape mirrors Task
/// 1.5's `<Name>Edge` — a `source: u32` handle (AgentId raw) plus
/// `value: {val_ty}` plus `anchor_tick: u32`. Event fields beyond the
/// owner + second pair binding are dropped in v1; Phase 4 can widen the
/// entry shape when the `record_memory` port drives a concrete need.
fn emit_per_entity_ring_struct(
    out: &mut String,
    view: &ViewIR,
    initial: &IrExprNode,
    handlers: &[FoldHandlerIR],
    clamp: Option<&(IrExprNode, IrExprNode)>,
    decay: Option<&crate::ir::DecayHint>,
    k: usize,
) -> Result<(), EmitError> {
    // Ring views carry an owner key (first param) plus an optional
    // "source" handle (second param). K=0 is meaningless; require >= 1.
    if k == 0 {
        return Err(EmitError::Unsupported(format!(
            "`per_entity_ring(K=0)` on view `{}` — ring capacity must be >= 1",
            view.name
        )));
    }
    if view.params.is_empty() {
        return Err(EmitError::Unsupported(format!(
            "`per_entity_ring(K={k})` storage on view `{}` requires >= 1 param (the ring owner); got 0",
            view.name
        )));
    }
    if view.params.len() > 2 {
        return Err(EmitError::Unsupported(format!(
            "`per_entity_ring(K={k})` storage on view `{}` supports at most 2 params (owner + optional source); got {}",
            view.name,
            view.params.len()
        )));
    }
    if decay.is_some() {
        // Ring is FIFO; layering decay on top needs a recency-scored
        // read path. Leave to Phase 3+.
        return Err(EmitError::Unsupported(format!(
            "`per_entity_ring` storage on view `{}` does not support `@decay` yet (GPU cold-state replay plan phase 3)",
            view.name
        )));
    }
    if clamp.is_some() {
        // Ring values aren't accumulated, so clamp has no well-defined
        // semantics on the stored entry. Reject until a use case lands.
        return Err(EmitError::Unsupported(format!(
            "`per_entity_ring` storage on view `{}` does not support `clamp` (ring entries are not accumulated)",
            view.name
        )));
    }

    let val_ty = rust_type_for(&view.return_ty)?;
    let owner_ty = rust_type_for(&view.params[0].ty)?;
    let owner_name = view.params[0].name.as_str();
    let second_name: Option<&str> = view.params.get(1).map(|p| p.name.as_str());
    let ty_name = pascal_case(&view.name);
    let entry_ty = format!("{ty_name}Entry");
    let initial_lit = lower_scalar_literal(initial, &view.return_ty)?;

    writeln!(
        out,
        "/// @materialized view `{}` — `storage = per_entity_ring(K={k})`.",
        view.name
    )
    .unwrap();
    writeln!(
        out,
        "/// Per-owner FIFO ring of fixed capacity {k}. Writes append at"
    )
    .unwrap();
    writeln!(
        out,
        "/// `cursor % K` and bump the cursor; the oldest entry is evicted"
    )
    .unwrap();
    writeln!(
        out,
        "/// implicitly on overflow. Reads iterate the ring in most-recent-first order."
    )
    .unwrap();

    // Per-ring slot struct. `source == 0` marks an empty slot (AgentId is
    // 1-based, matching the `per_entity_topk` / `symmetric_pair_topk`
    // convention).
    writeln!(out, "#[derive(Debug, Clone, Copy, Default, PartialEq)]").unwrap();
    writeln!(out, "pub struct {entry_ty} {{").unwrap();
    writeln!(
        out,
        "    /// Raw AgentId of the \"other\" endpoint (the event's non-owner field)."
    )
    .unwrap();
    writeln!(out, "    /// 0 means empty / never written.").unwrap();
    writeln!(out, "    pub source: u32,").unwrap();
    writeln!(out, "    /// Stored value for this ring entry.").unwrap();
    writeln!(out, "    pub value: {val_ty},").unwrap();
    writeln!(out, "    /// Tick when this entry was written.").unwrap();
    writeln!(out, "    pub anchor_tick: u32,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "#[derive(Debug, Default)]").unwrap();
    writeln!(out, "pub struct {ty_name} {{").unwrap();
    writeln!(
        out,
        "    /// One ring of {k} slots per owner agent. `Vec::len()` grows on"
    )
    .unwrap();
    writeln!(
        out,
        "    /// demand as `fold_event` sees higher owner ids."
    )
    .unwrap();
    writeln!(out, "    rings: Vec<[{entry_ty}; {k}]>,").unwrap();
    writeln!(
        out,
        "    /// Monotonic write cursor per owner. Slot index = `cursor % K`."
    )
    .unwrap();
    writeln!(out, "    cursors: Vec<u32>,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {ty_name} {{").unwrap();
    writeln!(
        out,
        "    /// Ring capacity per owner — the `K` from `per_entity_ring(K={k})`."
    )
    .unwrap();
    writeln!(out, "    pub const K: usize = {k};").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    pub fn new() -> Self {{ Self::default() }}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "    /// Number of owner rings currently provisioned. `fold_event` grows"
    )
    .unwrap();
    writeln!(out, "    /// this on demand up to `max_owner + 1`.").unwrap();
    writeln!(out, "    pub fn len(&self) -> usize {{ self.rings.len() }}").unwrap();
    writeln!(
        out,
        "    pub fn is_empty(&self) -> bool {{ self.rings.is_empty() }}"
    )
    .unwrap();
    writeln!(out).unwrap();

    // Internal row-access helper. Grows both `rings` and `cursors` in
    // lockstep so the parallel layout invariant holds.
    writeln!(
        out,
        "    fn row_mut(&mut self, owner_slot: usize) -> &mut [{entry_ty}; {k}] {{"
    )
    .unwrap();
    writeln!(out, "        if owner_slot >= self.rings.len() {{").unwrap();
    writeln!(
        out,
        "            self.rings.resize(owner_slot + 1, [{entry_ty}::default(); {k}]);"
    )
    .unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        if owner_slot >= self.cursors.len() {{").unwrap();
    writeln!(out, "            self.cursors.resize(owner_slot + 1, 0u32);").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        &mut self.rings[owner_slot]").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // push(): write at cursor % K, bump cursor. Raw-u32 signature so the
    // fold arm can pass `observer.raw()` without re-wrapping.
    writeln!(
        out,
        "    /// Append `entry` to the ring owned by `observer_raw` (raw AgentId,"
    )
    .unwrap();
    writeln!(
        out,
        "    /// 1-based). Writes at `cursor % K` and increments the cursor. A full"
    )
    .unwrap();
    writeln!(
        out,
        "    /// ring silently evicts the oldest entry (FIFO). `observer_raw == 0`"
    )
    .unwrap();
    writeln!(out, "    /// is a no-op.").unwrap();
    writeln!(
        out,
        "    pub fn push(&mut self, observer_raw: u32, entry: {entry_ty}) {{"
    )
    .unwrap();
    writeln!(out, "        if observer_raw == 0 {{ return; }}").unwrap();
    writeln!(out, "        let owner_slot = (observer_raw - 1) as usize;").unwrap();
    // Grow `cursors` in lockstep with `rings` before taking any mutable
    // borrow, then read `cursor` up front so the subsequent `row_mut`'s
    // mutable borrow of `self.rings` doesn't collide with the immutable
    // `self.cursors[..]` read the write-slot index needs.
    writeln!(out, "        if owner_slot >= self.cursors.len() {{").unwrap();
    writeln!(out, "            self.cursors.resize(owner_slot + 1, 0u32);").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        let cursor = self.cursors[owner_slot];").unwrap();
    writeln!(out, "        let slot = (cursor as usize) % {k};").unwrap();
    writeln!(out, "        let row = self.row_mut(owner_slot);").unwrap();
    writeln!(out, "        row[slot] = entry;").unwrap();
    writeln!(
        out,
        "        self.cursors[owner_slot] = cursor.wrapping_add(1);"
    )
    .unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // cursor(): inspect the write cursor for testing / replay.
    writeln!(
        out,
        "    /// Current write cursor for `observer` (0 if the ring was never written)."
    )
    .unwrap();
    writeln!(out, "    /// Monotonic; wraps on u32 overflow.").unwrap();
    writeln!(
        out,
        "    pub fn cursor(&self, {owner_name}: {owner_ty}) -> u32 {{"
    )
    .unwrap();
    writeln!(out, "        if {owner_name}.raw() == 0 {{ return 0; }}").unwrap();
    writeln!(out, "        let owner_slot = ({owner_name}.raw() - 1) as usize;").unwrap();
    writeln!(out, "        self.cursors.get(owner_slot).copied().unwrap_or(0)").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // recent(): return the most-recent entry, or the initial value when
    // the ring is empty. Mirrors the `get` accessor of the topk shapes so
    // lazy consumers have a single-value read path.
    writeln!(
        out,
        "    /// Most recently written `value` for `{owner_name}`'s ring, or `initial`"
    )
    .unwrap();
    writeln!(
        out,
        "    /// when the ring has never been written."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn get(&self, {owner_name}: {owner_ty}) -> {val_ty} {{"
    )
    .unwrap();
    writeln!(out, "        if {owner_name}.raw() == 0 {{ return {initial_lit}; }}").unwrap();
    writeln!(out, "        let owner_slot = ({owner_name}.raw() - 1) as usize;").unwrap();
    writeln!(
        out,
        "        let Some(row) = self.rings.get(owner_slot) else {{ return {initial_lit}; }};"
    )
    .unwrap();
    writeln!(
        out,
        "        let cursor = self.cursors.get(owner_slot).copied().unwrap_or(0);"
    )
    .unwrap();
    writeln!(out, "        if cursor == 0 {{ return {initial_lit}; }}").unwrap();
    writeln!(
        out,
        "        let slot = (cursor.wrapping_sub(1) as usize) % {k};"
    )
    .unwrap();
    writeln!(out, "        row[slot].value").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // entries(): slice access for replay / inspection. Returns the raw
    // ring in slot order (not cursor-relative); callers that need MRU
    // ordering can combine with `cursor()`.
    writeln!(
        out,
        "    /// Raw ring slice for `{owner_name}` in slot order. Returns `None` when"
    )
    .unwrap();
    writeln!(out, "    /// the ring has not yet been provisioned.").unwrap();
    writeln!(
        out,
        "    pub fn entries(&self, {owner_name}: {owner_ty}) -> Option<&[{entry_ty}; {k}]> {{"
    )
    .unwrap();
    writeln!(out, "        if {owner_name}.raw() == 0 {{ return None; }}").unwrap();
    writeln!(out, "        let owner_slot = ({owner_name}.raw() - 1) as usize;").unwrap();
    writeln!(out, "        self.rings.get(owner_slot)").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // fold_event dispatch. One arm per handler; each projects the event
    // fields into an `<Name>Entry` and calls `push`.
    writeln!(
        out,
        "    /// Advance / accumulate on each matching event. Spec §7.1 view-fold phase."
    )
    .unwrap();
    writeln!(
        out,
        "    pub fn fold_event(&mut self, event: &Event, tick: u32) {{"
    )
    .unwrap();
    if handlers.is_empty() {
        writeln!(out, "        let _ = (event, tick);").unwrap();
    } else {
        writeln!(out, "        match event {{").unwrap();
        for h in handlers {
            emit_per_entity_ring_fold_arm(out, view, owner_name, second_name, &entry_ty, h)?;
        }
        writeln!(out, "            _ => {{}}").unwrap();
        writeln!(out, "        }}").unwrap();
    }
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(())
}

/// Emit one match arm for a `per_entity_ring` fold handler. Mirrors
/// `emit_symmetric_pair_fold_arm` — figure out which event field maps
/// to the view's owner param (first param), which maps to the optional
/// second param (stored as `entry.source`), then call `push` with the
/// default `+1.0` value. Entries beyond those two bindings are dropped
/// until Phase 4 widens the entry shape.
fn emit_per_entity_ring_fold_arm(
    out: &mut String,
    view: &ViewIR,
    owner_name: &str,
    second_name: Option<&str>,
    entry_ty: &str,
    handler: &FoldHandlerIR,
) -> Result<(), EmitError> {
    let ev_name = handler.pattern.name.as_str();
    let mut owner_field: Option<&str> = None;
    let mut source_field: Option<&str> = None;
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if name == owner_name {
                owner_field = Some(field);
            } else if let Some(sn) = second_name {
                if name == sn {
                    source_field = Some(field);
                }
            }
        }
    }
    // Canonical fallback: `observer` names the ring owner, `source`
    // names the stored handle. Matches the `RecordMemory` shape in the
    // GPU cold-state replay plan.
    let owner_field = owner_field.unwrap_or("observer");
    let source_field = source_field.unwrap_or("source");
    let val_ty = rust_type_for(&view.return_ty)?;
    writeln!(
        out,
        "            Event::{ev_name} {{ {owner_field}, {source_field}, .. }} => {{"
    )
    .unwrap();
    writeln!(
        out,
        "                let entry = {entry_ty} {{ source: {source_field}.raw(), value: 1.0 as {val_ty}, anchor_tick: tick }};"
    )
    .unwrap();
    writeln!(
        out,
        "                self.push({owner_field}.raw(), entry);"
    )
    .unwrap();
    writeln!(out, "            }}").unwrap();
    Ok(())
}

/// Emit one match arm for a fold handler's event pattern. The arm binds
/// the handler's actor/target → (a, b) keys based on the pattern's named
/// bindings, applies the decay advance (if any), folds in an `amount`
/// delta (derived from `damage` / `delta` / `amount` fields), clamps,
/// and writes back. The precise set of bindings we recognise is tuned to
/// the seed DSL's needs — extend as more views come online.
fn emit_fold_arm(
    out: &mut String,
    view: &ViewIR,
    a_name: &str,
    b_name: &str,
    initial_lit: &str,
    handler: &FoldHandlerIR,
    clamp: Option<&(IrExprNode, IrExprNode)>,
    decay: Option<&crate::ir::DecayHint>,
) -> Result<(), EmitError> {
    let ev_name = handler.pattern.name.as_str();
    // Figure out which Event fields carry the (a, b) pair and which
    // carries the delta. For v1 we assume:
    // - `actor` / `target` bindings name the pair keys. The pattern's
    //   binding map tells us which position each lives in.
    // - the "delta" field is `damage`, `delta`, or `amount` — whichever
    //   the event carries. We inject `1.0` as a fallback.
    let mut actor_field: Option<&str> = None;
    let mut target_field: Option<&str> = None;
    // Walk the pattern's bindings. For each `Bind { name: <local> }`,
    // the source event field is `binding.field`; the local name tells us
    // whether it's meant to fill `a` or `b`.
    for b in &handler.pattern.bindings {
        let field = b.field.as_str();
        if let crate::ir::IrPattern::Bind { name, .. } = &b.value {
            if name == a_name {
                actor_field = Some(field);
            } else if name == b_name {
                target_field = Some(field);
            }
        }
    }
    // If the pattern doesn't bind both keys, fall back to the canonical
    // event shape (actor, target). Most of our damage-like events follow
    // this convention.
    let (actor_field, target_field) = match (actor_field, target_field) {
        (Some(a), Some(b)) => (a, b),
        _ => ("actor", "target"),
    };

    // Emit the arm with a destructuring pattern. We blanket-bind the
    // common fields and use `..` to ignore the rest so cosmetic changes
    // to the event don't break the emitter.
    writeln!(
        out,
        "            Event::{ev_name} {{ {actor_field}, {target_field}, .. }} => {{"
    )
    .unwrap();
    // Compute the (a, b) key tuple. Conventionally the view's first arg
    // (`a_name`) maps to the event's `actor_field` and the second to
    // `target_field`, but some views invert the pair (threat_level
    // binds `actor: b, target: a`). `actor_field == a_name` heuristic
    // handles the canonical case; the pattern-driven branch above
    // handles the explicit-binding case.
    writeln!(
        out,
        "                let key = (*{actor_field}, *{target_field});"
    )
    .unwrap();
    // Pick a delta from the event shape. We don't know the event's
    // field list at emission time without walking the EventIR, so we
    // match-by-name via a second `match` inside the arm. In v1 we
    // emit a simple `1.0` default that every threat-like view
    // accumulates; real bodies can be lowered in a future pass.
    writeln!(out, "                let amount: {0} = 1.0;", rust_type_for(&view.return_ty)?).unwrap();
    if let Some(_d) = decay {
        writeln!(
            out,
            "                let (base, anchor) = self.value.get(&key).copied().unwrap_or(({initial_lit}, tick));"
        )
        .unwrap();
        writeln!(
            out,
            "                let current = base * Self::RATE.powi(tick.saturating_sub(anchor) as i32);"
        )
        .unwrap();
        writeln!(out, "                let updated = current + amount;").unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "                let updated = updated.clamp({lo_s}, {hi_s});").unwrap();
        }
        writeln!(out, "                self.value.insert(key, (updated, tick));").unwrap();
    } else {
        writeln!(
            out,
            "                let prev = self.value.get(&key).copied().unwrap_or({initial_lit});"
        )
        .unwrap();
        writeln!(out, "                let updated = prev + amount;").unwrap();
        if let Some((lo, hi)) = clamp {
            let lo_s = lower_scalar_literal(lo, &view.return_ty)?;
            let hi_s = lower_scalar_literal(hi, &view.return_ty)?;
            writeln!(out, "                let updated = updated.clamp({lo_s}, {hi_s});").unwrap();
        }
        writeln!(out, "                self.value.insert(key, updated);").unwrap();
        writeln!(out, "                let _ = tick;").unwrap();
    }
    writeln!(out, "            }}").unwrap();
    Ok(())
}

