//! Mask-predicate emission. Scaffolded at milestone-3-completion; filled
//! in at milestone 4.
//!
//! Emission target: `crates/engine/src/generated/mask/<name>.rs` (one file
//! per `mask` declaration). Mask predicates reference `engine::state::SimState`,
//! so they must live in the engine crate (same reason physics does; see
//! `docs/game/feature_flow.md`).
//!
//! Milestone 4 replaces the `todo!()` below with a real emitter. Until then,
//! `emit_mask_mod` produces a valid empty aggregator so the generated
//! directory compiles without any mask declarations in scope.

