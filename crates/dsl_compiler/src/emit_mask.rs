/// Emit `crates/engine/src/generated/mask/mod.rs`. The aggregator exposes
/// `pub fn register(...)` for the engine's mask-build path to call.
///
/// With zero masks in scope this returns a no-op aggregator so the module
/// compiles as soon as the scaffolding lands.
pub fn emit_mask_mod(masks: &[MaskIR]) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED by dsl_compiler. Do not edit by hand.\n");
    out.push_str("// Regenerate with `cargo run --bin xtask -- compile-dsl`.\n");
    out.push_str("\n");
    if masks.is_empty() {
        out.push_str("// No `mask` declarations in scope yet (milestone 4 will fill\n");
        out.push_str("// this in). The engine's mask-build path calls `register()`\n");
        out.push_str("// unconditionally — keeping the signature stable lets the\n");
        out.push_str("// call site land before the first mask does.\n");
        out.push_str("\n");
        out.push_str("pub fn register() {}\n");
    } else {
        // Real path lands in milestone 4.
        out.push_str("// milestone 4: aggregate mask registration\n");
        out.push_str("pub fn register() {}\n");
    }
    out
}
