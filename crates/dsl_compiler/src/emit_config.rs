// Public entry points
// ---------------------------------------------------------------------------

/// Emit a single per-block Rust module (`config/<snake>.rs`). Carries the
/// struct + `Default` impl; the aggregate `mod.rs` re-exports the struct
/// under its PascalCase name.
pub fn emit_config(block: &ConfigIR, source_file: Option<&str>) -> Result<String, EmitError> {
    let mut out = String::new();
    emit_header(&mut out, source_file);
    writeln!(out, "use serde::{{Deserialize, Serialize}};").unwrap();
    writeln!(out).unwrap();

    let struct_name = struct_name(&block.name);
    writeln!(out, "/// Compiler-emitted tunables for the `{}` config block.", block.name)
        .unwrap();
    writeln!(out, "#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]").unwrap();
    writeln!(out, "#[serde(default)]").unwrap();
    writeln!(out, "pub struct {struct_name} {{").unwrap();
    for f in &block.fields {
        let rust_ty = rust_type_name(&f.ty)?;
        writeln!(out, "    pub {}: {},", f.name, rust_ty).unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl Default for {struct_name} {{").unwrap();
    writeln!(out, "    fn default() -> Self {{").unwrap();
    writeln!(out, "        Self {{").unwrap();
    for f in &block.fields {
        let lit = render_default(&f.ty, &f.default)?;
        writeln!(out, "            {}: {},", f.name, lit).unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    Ok(out)
}

/// Emit the aggregate `config/mod.rs`. Exposes one `<Name>Config` re-export
/// per block and a top-level `Config` that bundles them all, with a
/// `from_toml` loader and a hand-emitted `ConfigError` type (we don't pull
/// in `thiserror` for one error enum).
pub fn emit_config_mod(blocks: &[ConfigIR]) -> String {
    let mut out = String::new();
    writeln!(out, "// GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(out, "// Regenerate with `cargo run --bin xtask -- compile-dsl`.").unwrap();
    writeln!(out).unwrap();

    // Sort by block name so the aggregator is reorder-stable.
    let mut sorted: Vec<&ConfigIR> = blocks.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    if sorted.is_empty() {
        writeln!(out, "// No `config` declarations in scope yet.").unwrap();
        writeln!(out).unwrap();
        writeln!(out, "use serde::{{Deserialize, Serialize}};").unwrap();
        writeln!(out).unwrap();
        writeln!(out, "#[derive(Debug, Clone, Default, PartialEq, Serialize, Deserialize)]")
            .unwrap();
        writeln!(out, "pub struct Config {{}}").unwrap();
        writeln!(out).unwrap();
        emit_config_error(&mut out);
        emit_config_impl(&mut out, &[]);
        return out;
    }

    for c in &sorted {
        writeln!(out, "pub mod {};", snake_case(&c.name)).unwrap();
    }
    writeln!(out).unwrap();
    for c in &sorted {
        let stem = snake_case(&c.name);
        let sname = struct_name(&c.name);
        writeln!(out, "pub use {stem}::{sname};").unwrap();
    }
    writeln!(out).unwrap();

    writeln!(out, "use serde::{{Deserialize, Serialize}};").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "/// Aggregate bundle of every `config` block declared in the DSL.").unwrap();
    writeln!(out, "/// Embedded in `SimState` so runtime code reads tunables via").unwrap();
    writeln!(out, "/// `state.config.<block>.<field>`; loaded from TOML at startup").unwrap();
    writeln!(out, "/// via [`Config::from_toml`], or [`Config::default`] bakes in the").unwrap();
    writeln!(out, "/// DSL's `= <default>` clauses.").unwrap();
    writeln!(out, "#[derive(Debug, Clone, PartialEq, Serialize, Deserialize, Default)]").unwrap();
    writeln!(out, "pub struct Config {{").unwrap();
    for c in &sorted {
        let sname = struct_name(&c.name);
        let field = snake_case(&c.name);
        writeln!(out, "    #[serde(default)]").unwrap();
        writeln!(out, "    pub {field}: {sname},").unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    emit_config_error(&mut out);
    emit_config_impl(&mut out, &sorted);
    out
}

/// Emit `assets/config/default.toml` — one `[block]` table per config block,
/// with each declared field at its DSL `= <default>` value. Re-running the
/// compiler overwrites this file; the intent is that hand edits between
/// compiler runs represent a user's balance-tuning snapshot, and once a
/// schema change lands the user merges their tune-deltas back onto the
/// regenerated file.
pub fn emit_config_toml(blocks: &[ConfigIR]) -> Result<String, EmitError> {
    let mut out = String::new();
    writeln!(out, "# GENERATED by dsl_compiler from the `config` blocks in assets/sim/.").unwrap();
    writeln!(out, "# Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`.").unwrap();
    writeln!(out, "# Hand edits to this file are appropriate for TUNING — changes will").unwrap();
    writeln!(out, "# be read at runtime via Config::from_toml. Do NOT change the set of").unwrap();
    writeln!(out, "# fields here; that bumps the config schema hash. Change values freely.").unwrap();
    writeln!(out).unwrap();

    // Stable ordering: sorted by block name. Field order within a block
    // preserves source order (the emitted Rust struct layout depends on it,
    // and humans editing the TOML find it easier to match the DSL source).
    let mut sorted: Vec<&ConfigIR> = blocks.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));

    for (i, c) in sorted.iter().enumerate() {
        if i > 0 {
            writeln!(out).unwrap();
        }
        writeln!(out, "[{}]", snake_case(&c.name)).unwrap();
        for f in &c.fields {
            let lit = render_toml_default(&f.ty, &f.default)?;
            writeln!(out, "{} = {}", f.name, lit).unwrap();
        }
    }
    Ok(out)
}

// ---------------------------------------------------------------------------
// Helpers — type rendering, literal rendering, header
