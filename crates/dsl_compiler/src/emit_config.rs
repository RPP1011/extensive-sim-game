#[cfg(test)]
mod tests {
    use super::*;
    use crate::ast::Span;
    use crate::ir::ConfigFieldIR;

    fn cfg_block(name: &str, fields: Vec<(&str, IrType, ast::ConfigDefault)>) -> ConfigIR {
        ConfigIR {
            name: name.into(),
            fields: fields
                .into_iter()
                .map(|(n, ty, def)| ConfigFieldIR {
                    name: n.into(),
                    ty,
                    default: def,
                    span: Span::dummy(),
                })
                .collect(),
            annotations: vec![],
            span: Span::dummy(),
        }
    }

    #[test]
    fn struct_emits_field_per_entry() {
        let block = cfg_block(
            "combat",
            vec![
                ("attack_damage", IrType::F32, ast::ConfigDefault::Float(10.0)),
                ("attack_range", IrType::F32, ast::ConfigDefault::Float(2.0)),
            ],
        );
        let out = emit_config(&block, Some("assets/sim/config.sim")).unwrap();
        assert!(out.contains("pub struct CombatConfig {"));
        assert!(out.contains("pub attack_damage: f32,"));
        assert!(out.contains("pub attack_range: f32,"));
        assert!(out.contains("attack_damage: 10.0_f32,"));
        assert!(out.contains("attack_range: 2.0_f32,"));
        assert!(out.contains("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]"));
        assert!(out.contains("#[serde(default)]"));
    }

    #[test]
    fn int_and_bool_defaults_render_correctly() {
        let block = cfg_block(
            "misc",
            vec![
                ("limit", IrType::U32, ast::ConfigDefault::Uint(32)),
                ("negative", IrType::I32, ast::ConfigDefault::Int(-1)),
                ("flag", IrType::Bool, ast::ConfigDefault::Bool(true)),
            ],
        );
        let out = emit_config(&block, None).unwrap();
        assert!(out.contains("limit: 32u32,"));
        assert!(out.contains("negative: -1i32,"));
        assert!(out.contains("flag: true,"));
    }

    #[test]
    fn mod_rs_has_aggregate_config_struct() {
        let combat = cfg_block(
            "combat",
            vec![("attack_damage", IrType::F32, ast::ConfigDefault::Float(10.0))],
        );
        let movement = cfg_block(
            "movement",
            vec![("move_speed_mps", IrType::F32, ast::ConfigDefault::Float(1.0))],
        );
        let out = emit_config_mod(&[combat, movement]);
        assert!(out.contains("pub mod combat;"));
        assert!(out.contains("pub mod movement;"));
        assert!(out.contains("pub use combat::CombatConfig;"));
        assert!(out.contains("pub use movement::MovementConfig;"));
        assert!(out.contains("pub struct Config {"));
        assert!(out.contains("pub combat: CombatConfig,"));
        assert!(out.contains("pub movement: MovementConfig,"));
        assert!(out.contains("pub fn from_toml("));
        assert!(out.contains("pub enum ConfigError"));
    }

    #[test]
    fn toml_default_table_roundtrip_shape() {
        let combat = cfg_block(
            "combat",
            vec![
                ("attack_damage", IrType::F32, ast::ConfigDefault::Float(10.0)),
                ("attack_range", IrType::F32, ast::ConfigDefault::Float(2.0)),
            ],
        );
        let movement = cfg_block(
            "movement",
            vec![("move_speed_mps", IrType::F32, ast::ConfigDefault::Float(1.0))],
        );
        let out = emit_config_toml(&[movement, combat]).unwrap();
        // Sorted: combat before movement.
        let combat_pos = out.find("[combat]").expect("combat table");
        let movement_pos = out.find("[movement]").expect("movement table");
        assert!(combat_pos < movement_pos);
        assert!(out.contains("attack_damage = 10.0"));
        assert!(out.contains("move_speed_mps = 1.0"));
    }

    #[test]
    fn empty_blocks_still_produce_valid_config_struct() {
        let out = emit_config_mod(&[]);
        assert!(out.contains("pub struct Config {}"));
        assert!(out.contains("pub fn from_toml("));
    }
}
