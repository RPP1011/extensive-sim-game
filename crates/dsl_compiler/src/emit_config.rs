//! Rust + TOML emission for `config` declarations.
//!
//! Every `config <Name> { ... }` block produces:
//!
//! 1. A per-block Rust struct `<Name>Config` under
//!    `crates/engine_rules/src/config/<snake>.rs` carrying one field per
//!    declared entry plus a `Default` impl baking in the DSL's default
//!    values. `serde::{Deserialize, Serialize}` is derived so the aggregate
//!    `Config` type round-trips through `toml::from_str` / `to_string`.
//! 2. An entry in the aggregate `crates/engine_rules/src/config/mod.rs`,
//!    which re-exports each per-block struct and bundles them on a single
//!    `pub struct Config`. `Config::default()` walks the per-block defaults;
//!    `Config::from_toml(path)` reads + parses a `default.toml`.
//!
//! Alongside the Rust output we emit `assets/config/default.toml` carrying
//! the authored default values. The TOML is what a user edits to tune
//! balance at runtime; re-running the compiler OVERWRITES this file, so
//! hand edits there are only safe when the DSL source is the ultimate
//! source of truth (same "generated file, edit the source" contract as
//! every other emission target).
//!
//! The emitter is deliberately mechanical — `writeln!` into a `String`, no
//! macros, one Rust struct per block, one `[block]` table per block in the
//! TOML. Reviewers should be able to skim the output directly.

use std::fmt::Write;

use crate::ast;
use crate::ir::{ConfigIR, IrType};

// ---------------------------------------------------------------------------
// Errors
// ---------------------------------------------------------------------------

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum EmitError {
    Unsupported(String),
}

impl std::fmt::Display for EmitError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            EmitError::Unsupported(s) => write!(f, "config emission: {s}"),
        }
    }
}

impl std::error::Error for EmitError {}

