fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(out, "// GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
    writeln!(out).unwrap();
}

fn emit_config_error(out: &mut String) {
    writeln!(out, "/// Loader error for [`Config::from_toml`]. Kept tiny so `engine_rules`").unwrap();
    writeln!(out, "/// doesn't need `thiserror` / `anyhow`; the two underlying error types").unwrap();
    writeln!(out, "/// (`std::io::Error` on the file read, `toml::de::Error` on parse) are").unwrap();
    writeln!(out, "/// wrapped by hand-emitted `From` impls.").unwrap();
    writeln!(out, "#[derive(Debug)]").unwrap();
    writeln!(out, "pub enum ConfigError {{").unwrap();
    writeln!(out, "    Io(std::io::Error),").unwrap();
    writeln!(out, "    Parse(toml::de::Error),").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl From<std::io::Error> for ConfigError {{").unwrap();
    writeln!(out, "    fn from(e: std::io::Error) -> Self {{ ConfigError::Io(e) }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl From<toml::de::Error> for ConfigError {{").unwrap();
    writeln!(out, "    fn from(e: toml::de::Error) -> Self {{ ConfigError::Parse(e) }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl std::fmt::Display for ConfigError {{").unwrap();
    writeln!(out, "    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {{").unwrap();
    writeln!(out, "        match self {{").unwrap();
    writeln!(out, "            ConfigError::Io(e) => write!(f, \"config io error: {{e}}\"),").unwrap();
    writeln!(out, "            ConfigError::Parse(e) => write!(f, \"config parse error: {{e}}\"),").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl std::error::Error for ConfigError {{}}").unwrap();
    writeln!(out).unwrap();
}

fn emit_config_impl(out: &mut String, _blocks: &[&ConfigIR]) {
    writeln!(out, "impl Config {{").unwrap();
    writeln!(out, "    /// Read + parse a TOML file into a fully-populated `Config`.").unwrap();
    writeln!(out, "    /// Unknown tables are ignored by `serde`'s default behaviour;").unwrap();
    writeln!(out, "    /// missing fields fall back to their `Default` values via the").unwrap();
    writeln!(out, "    /// per-block `#[serde(default)]` attribute on this struct.").unwrap();
    writeln!(out, "    pub fn from_toml(path: &std::path::Path) -> Result<Self, ConfigError> {{").unwrap();
    writeln!(out, "        let s = std::fs::read_to_string(path)?;").unwrap();
    writeln!(out, "        let cfg = toml::from_str(&s)?;").unwrap();
    writeln!(out, "        Ok(cfg)").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

/// Produce the Rust type name for an `IrType` constrained to the config
/// primitive surface: bool / iN / uN / f32 / f64 / String. Anything outside
/// that surface is rejected here (config blocks don't allow nested structs
/// or arrays in this milestone).
fn rust_type_name(ty: &IrType) -> Result<&'static str, EmitError> {
    Ok(match ty {
        IrType::Bool => "bool",
        IrType::I8 => "i8",
        IrType::U8 => "u8",
        IrType::I16 => "i16",
        IrType::U16 => "u16",
        IrType::I32 => "i32",
        IrType::U32 => "u32",
        IrType::I64 => "i64",
        IrType::U64 => "u64",
        IrType::F32 => "f32",
        IrType::F64 => "f64",
        IrType::String => "String",
        other => {
            return Err(EmitError::Unsupported(format!(
                "config field type {other:?} is outside the permitted primitive surface \
                 (bool/iN/uN/f32/f64/String)"
            )))
        }
    })
}

/// Render a default literal for the Rust `Default::default` body. Reconciles
/// the declared type with the parsed literal so `u32 = 10` is written as
/// `10u32`, and `f32 = 1.0` stays `1.0_f32`. Strings use Rust's debug form
/// so embedded quotes / escapes round-trip.
fn render_default(ty: &IrType, def: &ast::ConfigDefault) -> Result<String, EmitError> {
    match (ty, def) {
        (IrType::Bool, ast::ConfigDefault::Bool(b)) => Ok(if *b { "true".into() } else { "false".into() }),
        (IrType::F32, ast::ConfigDefault::Float(v)) => Ok(format!("{}_f32", render_float(*v))),
        (IrType::F64, ast::ConfigDefault::Float(v)) => Ok(format!("{}_f64", render_float(*v))),
        (IrType::F32, ast::ConfigDefault::Int(v)) => Ok(format!("{}_f32", render_float(*v as f64))),
        (IrType::F64, ast::ConfigDefault::Int(v)) => Ok(format!("{}_f64", render_float(*v as f64))),
        (IrType::F32, ast::ConfigDefault::Uint(v)) => Ok(format!("{}_f32", render_float(*v as f64))),
        (IrType::F64, ast::ConfigDefault::Uint(v)) => Ok(format!("{}_f64", render_float(*v as f64))),
        (IrType::String, ast::ConfigDefault::String(s)) => Ok(format!("String::from({s:?})")),
        (int_ty, ast::ConfigDefault::Uint(v)) if is_integer(int_ty) => {
            Ok(format!("{}{}", v, int_suffix(int_ty)))
        }
        (int_ty, ast::ConfigDefault::Int(v)) if is_integer(int_ty) => {
            Ok(format!("{}{}", v, int_suffix(int_ty)))
        }
        (ty, def) => Err(EmitError::Unsupported(format!(
            "type {ty:?} is not assignable from default literal {def:?}"
        ))),
    }
}

/// Render a default literal for the TOML table. TOML booleans are bare
/// `true`/`false`, integers are bare decimal, floats always carry a decimal
/// point or exponent (same rule as the Rust emitter), strings use TOML's
/// basic-string form with the standard backslash escapes.
fn render_toml_default(ty: &IrType, def: &ast::ConfigDefault) -> Result<String, EmitError> {
    match (ty, def) {
        (IrType::Bool, ast::ConfigDefault::Bool(b)) => Ok(if *b { "true".into() } else { "false".into() }),
        (IrType::F32 | IrType::F64, ast::ConfigDefault::Float(v)) => Ok(render_float(*v)),
        (IrType::F32 | IrType::F64, ast::ConfigDefault::Int(v)) => Ok(render_float(*v as f64)),
        (IrType::F32 | IrType::F64, ast::ConfigDefault::Uint(v)) => Ok(render_float(*v as f64)),
        (IrType::String, ast::ConfigDefault::String(s)) => Ok(toml_string(s)),
        (int_ty, ast::ConfigDefault::Uint(v)) if is_integer(int_ty) => Ok(format!("{v}")),
        (int_ty, ast::ConfigDefault::Int(v)) if is_integer(int_ty) => Ok(format!("{v}")),
        (ty, def) => Err(EmitError::Unsupported(format!(
            "type {ty:?} has no TOML representation for default literal {def:?}"
        ))),
    }
}

fn is_integer(ty: &IrType) -> bool {
    matches!(
        ty,
        IrType::I8
            | IrType::U8
            | IrType::I16
            | IrType::U16
            | IrType::I32
            | IrType::U32
            | IrType::I64
            | IrType::U64
    )
}

fn int_suffix(ty: &IrType) -> &'static str {
    match ty {
        IrType::I8 => "i8",
        IrType::U8 => "u8",
        IrType::I16 => "i16",
        IrType::U16 => "u16",
        IrType::I32 => "i32",
        IrType::U32 => "u32",
        IrType::I64 => "i64",
        IrType::U64 => "u64",
        _ => "",
    }
}

fn render_float(v: f64) -> String {
    let s = format!("{v}");
    if s.contains('.') || s.contains('e') || s.contains('E') || s == "inf" || s == "-inf" || s == "NaN" {
        s
    } else {
        format!("{s}.0")
    }
}

/// Emit a TOML basic string. Minimal escapes: `\"`, `\\`, `\n`, `\t`, `\r`.
/// Anything outside printable ASCII goes through as-is (TOML is UTF-8).
fn toml_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            other => out.push(other),
        }
    }
    out.push('"');
    out
}

// ---------------------------------------------------------------------------
// Naming — snake_case / struct name
// ---------------------------------------------------------------------------

fn struct_name(block_name: &str) -> String {
    // DSL authors use lower_snake block names (`combat`, `movement`, ...);
    // the emitted struct gets a PascalCase + `Config` suffix so downstream
    // call sites read `CombatConfig` / `MovementConfig`.
    let pascal = pascal_case(block_name);
    format!("{pascal}Config")
}

fn snake_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 4);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            for lower in ch.to_lowercase() {
                out.push(lower);
            }
            prev_upper = true;
        } else {
            out.push(ch);
            prev_upper = false;
        }
    }
    out
}

fn pascal_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len());
    let mut upper_next = true;
    for ch in name.chars() {
        if ch == '_' || ch == '-' {
            upper_next = true;
        } else if upper_next {
            for u in ch.to_uppercase() {
                out.push(u);
            }
            upper_next = false;
        } else {
            out.push(ch);
        }
    }
    out
}

