fn emit_header(out: &mut String, source_file: Option<&str>) {
    match source_file {
        Some(path) => writeln!(out, "// GENERATED by dsl_compiler from {}.", path).unwrap(),
        None => writeln!(out, "// GENERATED by dsl_compiler.").unwrap(),
    }
    writeln!(
        out,
        "// Edit the .sim source; rerun `cargo run --bin xtask -- compile-dsl`."
    )
    .unwrap();
    writeln!(out, "// Do not edit by hand.").unwrap();
}

/// Canonical capability field order — when no entity declares a
/// `capabilities: Capabilities { ... }` block we fall back to this shape.
/// Mirrors `dsl/spec.md` §2.1.
const CANONICAL_CAPABILITY_ORDER: &[&str] = &[
    "channels",
    "languages",
    "can_fly",
    "can_build",
    "can_trade",
    "can_climb",
    "can_tunnel",
    "can_marry",
    "max_spouses",
    // Task 177 — deer-herding flee bias. Falls through the generic
    // bool emitter arms in `capability_field_type` /
    // `render_capability_value`, so no type/render edits are needed
    // here; listing it in the canonical order lets the emitter keep
    // producing the field when an entity omits it (pre-177 test
    // fixtures that never set it take the `false` default).
    "herds_when_fleeing",
];

/// Pull the capability field order from the first entity that has a
/// `capabilities: Capabilities { ... }` block. If none do, return the
/// canonical order. The emitted struct's field order matches this — so
/// reordering fields in the DSL source reorders the struct layout.
fn capability_field_order(entities: &[&EntityIR]) -> Vec<String> {
    for e in entities {
        if let Some(fields) = find_capabilities_fields(e) {
            let names: Vec<String> = fields.iter().map(|f| f.name.clone()).collect();
            if !names.is_empty() {
                // Ensure `languages` is present — legacy engine wants it in
                // the struct even when entities only declared `channels`.
                // If missing, splice it in after `channels`.
                let mut out = names.clone();
                if !out.iter().any(|n| n == "languages") {
                    let insert_at = out
                        .iter()
                        .position(|n| n == "channels")
                        .map(|i| i + 1)
                        .unwrap_or(0);
                    out.insert(insert_at, "languages".to_string());
                }
                return out;
            }
        }
    }
    CANONICAL_CAPABILITY_ORDER
        .iter()
        .map(|s| s.to_string())
        .collect()
}

/// The Rust type for each known capability field. Fields the emitter
/// doesn't recognise default to `bool` — callers should flag any mismatch.
fn capability_field_type(field: &str) -> &'static str {
    match field {
        "channels" => "ChannelSet",
        "languages" => "SmallVec<[LanguageId; 4]>",
        "max_spouses" => "u8",
        // All remaining known capability fields are bools per spec §2.1.
        "can_fly" | "can_build" | "can_trade" | "can_climb" | "can_tunnel" | "can_marry" => "bool",
        _ => "bool",
    }
}

/// Look up the `capabilities` field on an entity. Returns the inner struct-
/// literal fields, or `None` if the entity has no `capabilities` block.
fn find_capabilities_fields(entity: &EntityIR) -> Option<Vec<EntityFieldIR>> {
    for f in &entity.fields {
        if f.name == "capabilities" {
            if let EntityFieldValueIR::StructLiteral { fields, .. } = &f.value {
                return Some(fields.clone());
            }
        }
    }
    None
}

/// Render one `CreatureType::X => Self { <field>: <val>, ... }` value for
/// the given capability field and the entity's capability block.
///
/// When the entity omits a field, we substitute a default (`false` / `0` /
/// empty). This matches the pre-milestone engine behaviour — fields not
/// mentioned in `Capabilities::for_creature(X)` kept their type defaults.
fn render_capability_value(
    field: &str,
    caps: Option<&[EntityFieldIR]>,
    all_names: &[&str],
) -> String {
    let value = caps.and_then(|fs| fs.iter().find(|f| f.name == field));
    match field {
        "channels" => render_channel_list(value, all_names),
        "languages" => render_language_list(value, all_names),
        "max_spouses" => render_int_value(value, "0"),
        "can_fly" | "can_build" | "can_trade" | "can_climb" | "can_tunnel" | "can_marry" => {
            render_bool_value(value, "false")
        }
        _ => render_bool_value(value, "false"),
    }
}

/// Render a `channels` list like `smallvec![CommunicationChannel::Speech,
/// CommunicationChannel::Testimony]`. Accepts either a bare single value
/// (`channels: PackSignal`) or a list (`channels: [Speech, Testimony]`).
/// Splits across multiple lines when the single-line form would exceed
/// rustfmt's 100-column width (measured relative to the `channels:` field
/// indent, which is 16 columns deep inside a `for_creature` match arm).
fn render_channel_list(value: Option<&EntityFieldIR>, all_names: &[&str]) -> String {
    let Some(field) = value else {
        return "SmallVec::new()".into();
    };
    let names = collect_variant_names(&field.value, all_names);
    if names.is_empty() {
        return "SmallVec::new()".into();
    }
    let parts: Vec<String> = names
        .iter()
        .map(|n| format!("CommunicationChannel::{}", n))
        .collect();
    render_smallvec_literal(&parts, /* field_indent */ 16, /* field_label_len */ "channels: ".len())
}

/// Render a `languages` list like `smallvec![LanguageId::COMMON]`.
fn render_language_list(value: Option<&EntityFieldIR>, all_names: &[&str]) -> String {
    let Some(field) = value else {
        return "SmallVec::new()".into();
    };
    let names = collect_variant_names(&field.value, all_names);
    if names.is_empty() {
        return "SmallVec::new()".into();
    }
    let parts: Vec<String> = names
        .iter()
        .map(|n| format!("LanguageId::{}", language_const(n)))
        .collect();
    render_smallvec_literal(&parts, 16, "languages: ".len())
}

/// Emit a `smallvec![...]` literal, preferring single-line but splitting
/// across lines with trailing-`,` when the single-line form would exceed
/// rustfmt's 100-column max-width.
fn render_smallvec_literal(parts: &[String], field_indent: usize, field_label_len: usize) -> String {
    let single = format!("smallvec![{}]", parts.join(", "));
    // Width of the whole field-line: `    {indent}name: {single},`.
    let line_len = field_indent + field_label_len + single.len() + 1 /* trailing comma */;
    // rustfmt's 100-column default; a conservative split at 99 keeps us
    // below rustfmt's "too wide" trigger.
    if line_len < 99 {
        return single;
    }
    let continuation = " ".repeat(field_indent + 4);
    let closer = " ".repeat(field_indent);
    let mut out = String::from("smallvec![\n");
    for p in parts {
        out.push_str(&continuation);
        out.push_str(p);
        out.push_str(",\n");
    }
    out.push_str(&closer);
    out.push(']');
    out
}

/// DSL `Common`, `Draconic`, ... → `LanguageId::COMMON`, `DRACONIC`, ... —
/// SHOUTY_SNAKE_CASE of the PascalCase source ident.
fn language_const(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 2);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            out.push(ch);
            prev_upper = true;
        } else {
            for u in ch.to_uppercase() {
                out.push(u);
            }
            prev_upper = false;
        }
    }
    out
}

fn render_bool_value(value: Option<&EntityFieldIR>, default: &str) -> String {
    let Some(field) = value else {
        return default.into();
    };
    match &field.value {
        // The parser's type_ref path can consume `true` / `false` as type
        // names; detect and convert.
        EntityFieldValueIR::Type(IrType::Named(n)) if n == "true" || n == "false" => n.clone(),
        EntityFieldValueIR::Expr(e) => match &e.kind {
            IrExpr::LitBool(b) => if *b { "true" } else { "false" }.into(),
            _ => default.into(),
        },
        _ => default.into(),
    }
}

fn render_int_value(value: Option<&EntityFieldIR>, default: &str) -> String {
    let Some(field) = value else {
        return default.into();
    };
    match &field.value {
        EntityFieldValueIR::Expr(e) => match &e.kind {
            IrExpr::LitInt(v) => v.to_string(),
            _ => default.into(),
        },
        _ => default.into(),
    }
}

/// Pull out the PascalCase names of the variants in a field value. Accepts:
/// - A single bare type (`channels: PackSignal`) — one name.
/// - A list (`channels: [Speech, Testimony]`) — N names.
/// - Anything else — empty.
///
/// `entity_ref_names` maps `EntityRef` indices back to source names. It's
/// needed because `Wolf` inside `prey_of: [Wolf]` resolves to
/// `IrExpr::Entity(EntityRef(_))` whenever `Wolf` is a declared entity — the
/// resolver prefers that over `EnumVariant`. Pass `&[]` when no entity
/// context is available.
fn collect_variant_names(
    value: &EntityFieldValueIR,
    entity_ref_names: &[&str],
) -> Vec<String> {
    match value {
        EntityFieldValueIR::Type(IrType::Named(n)) => {
            // `channels: PackSignal` — bare type resolved as Named.
            vec![n.clone()]
        }
        EntityFieldValueIR::Type(IrType::EntityRef(r)) => {
            // `creature_type: Wolf` — Wolf is a declared entity.
            entity_ref_names
                .get(r.0 as usize)
                .map(|n| vec![(*n).to_string()])
                .unwrap_or_default()
        }
        EntityFieldValueIR::List(items) => items
            .iter()
            .filter_map(|e| expr_variant_name(e, entity_ref_names))
            .collect(),
        _ => Vec::new(),
    }
}

/// Extract a variant name from an expression node when possible. Covers
/// `EnumVariant` (uppercase idents that didn't resolve to a declared symbol)
/// and `Entity(EntityRef)` (the case where an entity name appears inside a
/// list, e.g. `preys_on: [Wolf]` where `Wolf` is a declared entity).
fn expr_variant_name(e: &IrExprNode, entity_ref_names: &[&str]) -> Option<String> {
    match &e.kind {
        IrExpr::EnumVariant { variant, .. } => Some(variant.clone()),
        IrExpr::Entity(r) => entity_ref_names
            .get(r.0 as usize)
            .map(|s| (*s).to_string()),
        _ => None,
    }
}

/// Walk every entity's `predator_prey` block and build the symmetric closure
/// of hostile `(CreatureType, CreatureType)` pairs.
///
/// `entity X { predator_prey: PredatorPrey { prey_of: [Y], preys_on: [Z] } }`
/// produces `(X,Y)`, `(Y,X)`, `(X,Z)`, `(Z,X)`. Self-pairs are excluded. The
/// output is deduplicated and sorted by the declaration order of the two
/// creatures; this keeps the emitted `matches!` arm order stable.
fn collect_hostility_pairs(agents: &[&EntityIR], all_names: &[&str]) -> Vec<(String, String)> {
    use std::collections::BTreeSet;

    // Order map: canonical declaration-order index per entity name.
    let order: std::collections::HashMap<&str, usize> = agents
        .iter()
        .enumerate()
        .map(|(i, e)| (e.name.as_str(), i))
        .collect();

    // Collected pairs, keyed on (idx_a, idx_b, name_a, name_b) so sorting is
    // purely by declaration order — ties broken by names for determinism.
    let mut pairs: BTreeSet<(usize, usize, String, String)> = BTreeSet::new();

    for entity in agents {
        let Some(pp) = find_predator_prey(entity) else {
            continue;
        };
        let pp_fields = match &pp.value {
            EntityFieldValueIR::StructLiteral { fields, .. } => fields,
            _ => continue,
        };
        for pp_field in pp_fields {
            let other_names = collect_variant_names(&pp_field.value, all_names);
            for other in &other_names {
                if other == &entity.name {
                    continue; // no self-hostility
                }
                // Skip unknown references — the emitted `matches!` arm needs
                // both creatures to exist in the CreatureType enum.
                if !order.contains_key(other.as_str()) {
                    continue;
                }
                insert_symmetric(&mut pairs, &order, &entity.name, other);
            }
        }
    }

    pairs
        .into_iter()
        .map(|(_, _, a, b)| (a, b))
        .collect()
}

fn insert_symmetric(
    out: &mut std::collections::BTreeSet<(usize, usize, String, String)>,
    order: &std::collections::HashMap<&str, usize>,
    a: &str,
    b: &str,
) {
    let ia = order[a];
    let ib = order[b];
    out.insert((ia, ib, a.to_string(), b.to_string()));
    out.insert((ib, ia, b.to_string(), a.to_string()));
}

fn find_predator_prey(entity: &EntityIR) -> Option<&EntityFieldIR> {
    entity.fields.iter().find(|f| f.name == "predator_prey")
}

fn snake_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len() + 4);
    let mut prev_upper = false;
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 && !prev_upper {
                out.push('_');
            }
            for lower in ch.to_lowercase() {
                out.push(lower);
            }
            prev_upper = true;
        } else {
            out.push(ch);
            prev_upper = false;
        }
    }
    out
}

