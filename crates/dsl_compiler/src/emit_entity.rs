/// Emit one stub file per entity. The real `CreatureType` / `Capabilities`
/// content lives in the aggregate `mod.rs`; each per-entity file is a
/// header-only comment so `rustc` has something coherent to look at and the
/// xtask's `prune_stale` stays consistent with the other declaration kinds
/// (each kind emits one file per declaration plus an aggregator).
pub fn emit_entity(entity: &EntityIR, source_file: Option<&str>) -> Result<String, String> {
    let mut out = String::new();
    emit_header(&mut out, source_file);
    writeln!(out).unwrap();
    writeln!(
        out,
        "// Entity `{}` — an Agent-root creature. The `CreatureType::{}`",
        entity.name, entity.name
    )
    .unwrap();
    writeln!(
        out,
        "// variant, per-variant `Capabilities::for_creature(...)` arm, and"
    )
    .unwrap();
    writeln!(
        out,
        "// hostility-matrix contribution live in this directory's `mod.rs`."
    )
    .unwrap();
    writeln!(out).unwrap();
    let snake = snake_case(&entity.name);
    writeln!(out, "// This file is intentionally near-empty — see `mod.rs`.").unwrap();
    writeln!(
        out,
        "#[allow(dead_code)]\npub(super) const __ENTITY_{}: &str = \"{}\";",
        snake.to_uppercase(),
        entity.name
    )
    .unwrap();
    Ok(out)
}

/// Emit the aggregate `entities/mod.rs`. Contains the full `CreatureType`
/// enum + impl and the `Capabilities` struct + `for_creature` — everything
/// the engine needs to compile against `engine_rules::entities`.
pub fn emit_entity_mod(entities: &[EntityIR]) -> String {
    let mut out = String::new();
    writeln!(out, "// GENERATED by dsl_compiler. Do not edit by hand.").unwrap();
    writeln!(out, "// Regenerate with `cargo run --bin xtask -- compile-dsl`.").unwrap();
    writeln!(out).unwrap();

    let agents: Vec<&EntityIR> = entities
        .iter()
        .filter(|e| matches!(e.root, ast::EntityRoot::Agent))
        .collect();
    // EntityRef indices resolve against the order in `comp.entities` — mirror
    // it here so `IrExpr::Entity(EntityRef(i))` can round-trip to the source
    // name inside `prey_of: [Wolf, ...]` lists.
    let all_names: Vec<&str> = entities.iter().map(|e| e.name.as_str()).collect();

    // Per-entity stub modules (one per declaration, Agent-root or otherwise)
    // — pruned and rewritten on every compile. Non-agent roots still get a
    // stub so reviewers can grep entity names.
    if entities.is_empty() {
        writeln!(out, "// No `entity` declarations in scope yet.").unwrap();
        return out;
    }
    let mut sorted: Vec<&EntityIR> = entities.iter().collect();
    sorted.sort_by(|a, b| a.name.cmp(&b.name));
    for e in &sorted {
        writeln!(out, "pub mod {};", snake_case(&e.name)).unwrap();
    }
    writeln!(out).unwrap();

    if agents.is_empty() {
        writeln!(
            out,
            "// No Agent-root entities declared — CreatureType + Capabilities omitted."
        )
        .unwrap();
        return out;
    }

    // Imports: ChannelSet / CommunicationChannel / LanguageId live in
    // `engine_rules::types`. SmallVec is the `languages` storage; the
    // `smallvec!` macro builds the per-variant literals.
    writeln!(
        out,
        "use crate::types::{{ChannelSet, CommunicationChannel, LanguageId}};"
    )
    .unwrap();
    writeln!(out, "use smallvec::{{smallvec, SmallVec}};").unwrap();
    writeln!(out).unwrap();

    emit_creature_type(&mut out, &agents, &all_names);
    writeln!(out).unwrap();
    emit_capabilities(&mut out, &agents, &all_names);

    out
}

