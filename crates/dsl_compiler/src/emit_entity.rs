fn emit_creature_type(out: &mut String, agents: &[&EntityIR], all_names: &[&str]) {
    // Source-order discriminants. Reordering entities in the DSL source
    // reorders the discriminants; that's a schema bump because the wire
    // layout changes (state_hash covers entity field layouts at
    // milestone 6).
    writeln!(
        out,
        "#[derive(Copy, Clone, Default, Eq, PartialEq, Ord, PartialOrd, Hash, Debug)]"
    )
    .unwrap();
    writeln!(out, "#[repr(u8)]").unwrap();
    writeln!(out, "pub enum CreatureType {{").unwrap();
    for (i, e) in agents.iter().enumerate() {
        if i == 0 {
            writeln!(out, "    #[default]").unwrap();
        }
        writeln!(out, "    {} = {},", e.name, i).unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl CreatureType {{").unwrap();

    // `ALL` — dense list, declaration order.
    writeln!(out, "    pub const ALL: &'static [CreatureType] = &[").unwrap();
    for e in agents {
        writeln!(out, "        CreatureType::{},", e.name).unwrap();
    }
    writeln!(out, "    ];").unwrap();
    writeln!(out).unwrap();

    // `name()` — debug string matching the variant name.
    writeln!(out, "    pub fn name(self) -> &'static str {{").unwrap();
    writeln!(out, "        match self {{").unwrap();
    for e in agents {
        writeln!(
            out,
            "            CreatureType::{} => \"{}\",",
            e.name, e.name
        )
        .unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    // `is_hostile_to` — dense `matches!` over the symmetric closure of each
    // entity's predator/prey lists. Self-hostility excluded. Always symmetric.
    let hostility_pairs = collect_hostility_pairs(agents, all_names);
    writeln!(
        out,
        "    /// Pairwise hostility predicate. Symmetric closure of the"
    )
    .unwrap();
    writeln!(
        out,
        "    /// `predator_prey.{{prey_of, preys_on}}` lists in `assets/sim/entities.sim`."
    )
    .unwrap();
    writeln!(out, "    pub fn is_hostile_to(self, other: CreatureType) -> bool {{").unwrap();
    if hostility_pairs.is_empty() {
        writeln!(out, "        let _ = other;").unwrap();
        writeln!(out, "        false").unwrap();
    } else {
        writeln!(out, "        matches!(").unwrap();
        writeln!(out, "            (self, other),").unwrap();
        // rustfmt formats or-patterns with leading `|` on continuation lines
        // — match that so emitted output survives `rustfmt` unchanged.
        for (i, (a, b)) in hostility_pairs.iter().enumerate() {
            if i == 0 {
                writeln!(
                    out,
                    "            (CreatureType::{}, CreatureType::{})",
                    a, b
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "                | (CreatureType::{}, CreatureType::{})",
                    a, b
                )
                .unwrap();
            }
        }
        writeln!(out, "        )").unwrap();
    }
    writeln!(out, "    }}").unwrap();

    writeln!(out, "}}").unwrap();
}

