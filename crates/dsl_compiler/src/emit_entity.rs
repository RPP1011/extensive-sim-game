/// A flattened `(entity_name, field_name, field_type_tag)` tuple that feeds
/// `schema_hash::state_hash`. Kept here because only the emitter knows how
/// to project the IR shape into a stable layout signature.
///
/// `field_type_tag` is a short ASCII description — `"bool"`, `"u8"`,
/// `"ChannelSet"`, `"SmallVec<LanguageId;4>"`, `"Named:<ident>"`, etc. The
/// state_hash folds these bytes verbatim; renaming a tag is a schema bump.
pub fn schema_hash_input(entities: &[EntityIR]) -> Vec<SchemaHashRow> {
    let mut rows: Vec<SchemaHashRow> = Vec::new();
    for e in entities {
        let root = match e.root {
            ast::EntityRoot::Agent => "Agent",
            ast::EntityRoot::Item => "Item",
            ast::EntityRoot::Group => "Group",
        };
        rows.push(SchemaHashRow {
            entity: e.name.clone(),
            root: root.to_string(),
            field: "_root".into(),
            tag: format!("root:{}", root),
        });
        for f in &e.fields {
            append_field_rows(&e.name, root, &f.name, &f.value, &mut rows);
        }
    }
    rows
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct SchemaHashRow {
    pub entity: String,
    pub root: String,
    pub field: String,
    pub tag: String,
}

fn append_field_rows(
    entity: &str,
    root: &str,
    prefix: &str,
    value: &EntityFieldValueIR,
    rows: &mut Vec<SchemaHashRow>,
) {
    match value {
        EntityFieldValueIR::Type(t) => {
            rows.push(SchemaHashRow {
                entity: entity.into(),
                root: root.into(),
                field: prefix.into(),
                tag: type_tag(t),
            });
        }
        EntityFieldValueIR::StructLiteral { ty, fields } => {
            rows.push(SchemaHashRow {
                entity: entity.into(),
                root: root.into(),
                field: prefix.into(),
                tag: format!("struct:{}", type_tag(ty)),
            });
            for sub in fields {
                let sub_prefix = format!("{}.{}", prefix, sub.name);
                append_field_rows(entity, root, &sub_prefix, &sub.value, rows);
            }
        }
        EntityFieldValueIR::List(items) => {
            rows.push(SchemaHashRow {
                entity: entity.into(),
                root: root.into(),
                field: prefix.into(),
                tag: format!("list:{}", items.len()),
            });
        }
        EntityFieldValueIR::Expr(_) => {
            rows.push(SchemaHashRow {
                entity: entity.into(),
                root: root.into(),
                field: prefix.into(),
                tag: "expr".into(),
            });
        }
    }
}

fn type_tag(t: &IrType) -> String {
    match t {
        IrType::Bool => "bool".into(),
        IrType::I8 => "i8".into(),
        IrType::U8 => "u8".into(),
        IrType::I16 => "i16".into(),
        IrType::U16 => "u16".into(),
        IrType::I32 => "i32".into(),
        IrType::U32 => "u32".into(),
        IrType::I64 => "i64".into(),
        IrType::U64 => "u64".into(),
        IrType::F32 => "f32".into(),
        IrType::F64 => "f64".into(),
        IrType::Vec3 => "vec3".into(),
        IrType::String => "String".into(),
        IrType::AgentId => "AgentId".into(),
        IrType::ItemId => "ItemId".into(),
        IrType::GroupId => "GroupId".into(),
        IrType::QuestId => "QuestId".into(),
        IrType::AuctionId => "AuctionId".into(),
        IrType::EventId => "EventId".into(),
        IrType::AbilityId => "AbilityId".into(),
        IrType::Named(n) => format!("Named:{}", n),
        IrType::EntityRef(r) => format!("EntityRef:{}", r.0),
        IrType::EventRef(r) => format!("EventRef:{}", r.0),
        _ => format!("{:?}", t),
    }
}

