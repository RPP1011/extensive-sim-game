fn emit_capabilities(out: &mut String, agents: &[&EntityIR], all_names: &[&str]) {
    // Struct definition. Field order matches the DSL declaration order in
    // the first entity's `capabilities` struct literal, falling back to the
    // canonical order from `dsl/spec.md` §2.1 if no entities define it.
    let cap_fields = capability_field_order(agents);
    writeln!(
        out,
        "/// Per-agent capability bundle — what the agent *can* do, structurally."
    )
    .unwrap();
    writeln!(out, "/// `dsl/spec.md` §2.1 is authoritative.").unwrap();
    writeln!(out, "#[derive(Clone, Debug)]").unwrap();
    writeln!(out, "pub struct Capabilities {{").unwrap();
    for f in &cap_fields {
        let ty = capability_field_type(f);
        writeln!(out, "    pub {}: {},", f, ty).unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // `for_creature(ct)` — per-variant default builder.
    writeln!(out, "impl Capabilities {{").unwrap();
    writeln!(
        out,
        "    /// Spawn-time capability defaults for each creature type."
    )
    .unwrap();
    writeln!(out, "    pub fn for_creature(ct: CreatureType) -> Self {{").unwrap();
    writeln!(out, "        match ct {{").unwrap();
    for e in agents {
        let caps = find_capabilities_fields(e);
        writeln!(out, "            CreatureType::{} => Self {{", e.name).unwrap();
        for f in &cap_fields {
            let val = render_capability_value(f, caps.as_deref(), all_names);
            writeln!(out, "                {}: {},", f, val).unwrap();
        }
        writeln!(out, "            }},").unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
}

