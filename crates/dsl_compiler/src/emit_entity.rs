//! Entity emission (milestone 6).
//!
//! Every `entity` declaration of root `Agent` contributes one variant to the
//! aggregate `CreatureType` enum and one arm to `Capabilities::for_creature`.
//! Predator/prey lists (`preys_on` + `prey_of`) feed the symmetric closure
//! behind `CreatureType::is_hostile_to`.
//!
//! Emission target: `crates/engine_rules/src/entities/`. The aggregate
//! `mod.rs` carries the full `CreatureType` enum, the `Capabilities` struct,
//! `for_creature`, and `is_hostile_to` — all in one file, because every
//! Agent-root entity contributes to the same enum + impls and they would
//! just re-export each other otherwise. Per-entity stub files sit alongside
//! as named bookmarks; their body is a pointer comment.
//!
//! The engine consumes the emitted output the same way it consumes `Event`:
//! `engine::creature` becomes a re-export shim over `engine_rules::entities`.
//!
//! The emitter is deliberately mechanical — `writeln!` into a `String`, no
//! macros / traits. Reviewers should be able to skim the output without
//! tracing any abstractions back to the emitter.

use std::fmt::Write;

use crate::ast;
use crate::ir::{EntityFieldIR, EntityFieldValueIR, EntityIR, IrExpr, IrExprNode, IrType};

