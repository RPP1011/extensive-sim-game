//! Campaign state for headless simulation.
//!
//! All types are `Clone + Debug` for MCTS tree search (cheap state cloning,
//! deterministic stepping). The game world advances via fixed 100ms ticks.

use serde::{Deserialize, Serialize};

use super::config::CampaignConfig;

/// Fixed tick duration in milliseconds, matching the combat sim.
pub const CAMPAIGN_TICK_MS: u32 = 100;

// ---------------------------------------------------------------------------
// Top-level state
// ---------------------------------------------------------------------------

/// Complete campaign state as a plain, Clone-able struct.
///
/// Designed for MCTS: cheap to clone, deterministic to step.
/// No Bevy ECS — all state is explicit fields.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CampaignState {
    // --- Time ---
    /// Monotonic tick counter (increments by 1 each step).
    pub tick: u64,
    /// Total elapsed game time in milliseconds (`tick * CAMPAIGN_TICK_MS`).
    pub elapsed_ms: u64,

    // --- Guild ---
    pub guild: GuildState,

    // --- World ---
    pub overworld: OverworldState,
    pub factions: Vec<FactionState>,
    pub diplomacy: DiplomacyMatrix,

    // --- Units ---
    pub adventurers: Vec<Adventurer>,
    pub parties: Vec<Party>,

    // --- Quests ---
    /// Available requests on the guild board (not yet accepted).
    pub request_board: Vec<QuestRequest>,
    /// Accepted, in-progress quests.
    pub active_quests: Vec<ActiveQuest>,
    /// Completed quest history (bounded ring buffer in practice).
    pub completed_quests: Vec<CompletedQuest>,

    // --- Battles ---
    pub active_battles: Vec<BattleState>,

    // --- Progression ---
    /// Unlocked abilities/buffs. Effect-property vectors, NOT identity-indexed.
    pub unlocks: Vec<UnlockInstance>,
    pub progression_history: Vec<ProgressionEvent>,

    // --- Choices ---
    /// Pending choice events requiring player decision.
    /// If deadline passes, the `default_option` is auto-selected.
    pub pending_choices: Vec<ChoiceEvent>,

    // --- Hook state ---
    /// Tracks which quest hooks have fired and when.
    #[serde(default)]
    pub hook_state: super::quest_hooks::HookState,

    // --- Bonds ---
    /// Adventurer bond matrix. Key = (min_id, max_id), value = bond strength 0-100.
    #[serde(default)]
    pub adventurer_bonds: std::collections::HashMap<(u32, u32), f32>,

    // --- Narrative ---
    pub event_log: Vec<CampaignEvent>,
    pub npc_relationships: Vec<NpcRelationship>,

    // --- RNG ---
    /// LCG state for deterministic randomness. Isolated from combat sim RNG.
    pub rng: u64,

    // --- ID counters ---
    pub next_quest_id: u32,
    pub next_party_id: u32,
    pub next_battle_id: u32,
    pub next_unlock_id: u32,
    pub next_event_id: u32,

    // --- Combat mode ---
    pub combat_mode: CombatMode,

    // --- Progression queue ---
    /// Content generated by the LLM, cached until the next rest event.
    /// Items are added immediately when triggers fire, presented at rest.
    #[serde(default)]
    pub pending_progression: Vec<PendingProgression>,
    /// Whether the guild is currently resting (no quests dispatched, recovery active).
    #[serde(default)]
    pub resting: bool,
    /// Ticks since last rest (drives fatigue and rest prompts).
    #[serde(default)]
    pub ticks_since_rest: u64,

    // --- Campaign phase ---
    /// Current phase of the campaign.
    pub phase: CampaignPhase,

    /// Available starting packages. Loaded from data files.
    /// Emptied after the player chooses one.
    pub available_starting_choices: Vec<StartingChoice>,

    // --- Player character ---
    /// The player's created character. Built up during character creation.
    #[serde(default)]
    pub player_character: Option<PlayerCharacter>,

    /// Queue of backstory events to present during character creation.
    #[serde(default)]
    pub creation_event_queue: Vec<super::backstory::BackstoryEvent>,

    // --- Buildings ---
    /// Guild building upgrade tiers (0–3 each). Provides passive bonuses.
    #[serde(default)]
    pub guild_buildings: super::systems::buildings::GuildBuildings,

    // --- Configuration ---
    /// All tunable balance parameters. Systems read from this.
    pub config: CampaignConfig,

    // --- LLM content generation ---
    /// Optional LLM config for generating abilities/classes via Ollama.
    /// When None, falls back to template generators.
    #[serde(skip)]
    pub llm_config: Option<super::llm::LlmConfig>,
    /// Shared content store for LLM request/response logging and dedup.
    /// Arc so it survives Clone (BFS branches share the same store).
    #[serde(skip)]
    pub llm_store: Option<std::sync::Arc<super::llm::ContentStore>>,
    /// Optional VAE model for content generation (replaces LLM + templates).
    /// Arc so cloned states share the same weights.
    #[serde(skip)]
    pub vae_model: Option<std::sync::Arc<super::vae_inference::ContentVaeWeights>>,

    // --- Rival guild ---
    /// AI-controlled competing guild. Activates at tick 2000.
    #[serde(default)]
    pub rival_guild: RivalGuildState,

    // --- New systems state ---
    #[serde(default)]
    pub trade_routes: Vec<TradeRoute>,
    #[serde(default)]
    pub caravans: Vec<Caravan>,
    #[serde(default)]
    pub next_caravan_id: u32,
    #[serde(default)]
    pub next_route_id: u32,
    #[serde(default)]
    pub retired_adventurers: Vec<RetiredAdventurer>,
    #[serde(default)]
    pub mentor_assignments: Vec<MentorAssignment>,
    #[serde(default)]
    pub civil_wars: Vec<CivilWarState>,
    #[serde(default)]
    pub rumors: Vec<Rumor>,
    #[serde(default)]
    pub war_exhaustion: Vec<WarExhaustion>,
    #[serde(default)]
    pub spies: Vec<Spy>,
    #[serde(default)]
    pub next_spy_id: u32,
    #[serde(default)]
    pub available_mercenaries: Vec<MercenaryCompany>,
    #[serde(default)]
    pub hired_mercenaries: Vec<MercenaryCompany>,
    #[serde(default)]
    pub black_market: BlackMarketState,
    #[serde(default)]
    pub resource_nodes: Vec<ResourceNode>,
    #[serde(default)]
    pub gathering_boosts: std::collections::HashMap<usize, u64>,
    #[serde(default)]
    pub migrations: Vec<MigrationWave>,
    #[serde(default)]
    pub next_migration_id: u32,
    #[serde(default)]
    pub active_festivals: Vec<Festival>,
    #[serde(default)]
    pub rivalries: Vec<Rivalry>,
    #[serde(default)]
    pub chronicle: Vec<ChronicleEntry>,
    #[serde(default)]
    pub nemeses: Vec<Nemesis>,
    #[serde(default)]
    pub faction_favors: Vec<FactionFavor>,
    #[serde(default)]
    pub favor_requests: Vec<FavorRequest>,
    #[serde(default)]
    pub site_preparations: Vec<SitePreparation>,
    #[serde(default)]
    pub next_site_id: u32,
    #[serde(default)]
    pub council_votes: Vec<CouncilVote>,
    #[serde(default)]
    pub next_vote_id: u32,
    #[serde(default)]
    pub diseases: Vec<Disease>,
    #[serde(default)]
    pub next_disease_id: u32,
    #[serde(default)]
    pub prisoners: Vec<Prisoner>,
    #[serde(default)]
    pub next_prisoner_id: u32,
    #[serde(default)]
    pub captured_adventurers: Vec<u32>,
    #[serde(default)]
    pub propaganda_campaigns: Vec<PropagandaCampaign>,
    #[serde(default)]
    pub monster_populations: Vec<MonsterPopulation>,
    #[serde(default)]
    pub visions: Vec<Vision>,
    #[serde(default)]
    pub next_vision_id: u32,
    #[serde(default)]
    pub loans: Vec<Loan>,
    #[serde(default)]
    pub credit_rating: f32,
    #[serde(default)]
    pub victory_condition: VictoryCondition,
    #[serde(default)]
    pub victory_progress: VictoryProgress,
    #[serde(default)]
    pub named_npcs: Vec<NamedNpc>,
    #[serde(default)]
    pub artifacts: Vec<Artifact>,
    #[serde(default)]
    pub next_artifact_id: u32,
    #[serde(default)]
    pub difficulty_scaling: DifficultyState,
    #[serde(default)]
    pub regional_cultures: Vec<RegionalCulture>,
    #[serde(default)]
    pub dungeons: Vec<Dungeon>,
    /// Crafting resource stockpile.
    #[serde(default)]
    pub resources: std::collections::HashMap<ResourceType, f32>,

    // --- Extended system trackers ---
    /// Aggregate counters for the ~30 campaign subsystems.
    /// Updated by step logic; consumed by tokens, clustering, and value estimation.
    #[serde(default)]
    pub system_trackers: SystemTrackers,
}

// ---------------------------------------------------------------------------
// Extended system trackers
// ---------------------------------------------------------------------------

/// Aggregate counters for campaign subsystems that feed the token/clustering/value
/// pipeline. Each field is a cheap scalar maintained by the step logic. Defaults
/// to zero so old serialized states remain compatible.
#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct SystemTrackers {
    // --- Espionage ---
    /// Number of active spies embedded in factions.
    pub spy_count: u32,
    /// Total intel points gathered across the campaign.
    pub total_intel_gathered: f32,
    /// Average cover level of active spies (0-100).
    pub mean_spy_cover: f32,

    // --- Mercenaries ---
    /// Currently hired mercenaries across all active quests/battles.
    pub mercenaries_hired: u32,
    /// Total aggregate mercenary combat strength.
    pub total_mercenary_strength: f32,
    /// Average loyalty of hired mercenaries (0-100).
    pub mean_mercenary_loyalty: f32,

    // --- Black market ---
    /// Heat level from illicit activity (0-100). High heat attracts enforcement.
    pub black_market_heat: f32,
    /// Total profit from black market deals.
    pub black_market_profit: f32,
    /// Number of currently active black market deals.
    pub active_deals: u32,

    // --- Prisoners ---
    /// Captured enemy combatants held by the guild.
    pub prisoner_count: u32,
    /// Captured adventurers (our side) held by enemies.
    pub captured_adventurer_count: u32,

    // --- Loans ---
    /// Total outstanding debt from loans.
    pub total_debt: f32,
    /// Credit rating (0-100). Higher = better loan terms.
    pub credit_rating: f32,

    // --- Rumors ---
    /// Number of active unverified rumors.
    pub active_rumor_count: u32,
    /// Number of rumors that have been investigated.
    pub investigated_rumor_count: u32,

    // --- Civil wars ---
    /// Number of active civil wars between factions.
    pub active_civil_war_count: u32,
    /// Whether the guild is involved in any civil war.
    pub guild_civil_war_involvement: bool,

    // --- Diplomacy agreements ---
    /// Number of active trade agreements.
    pub trade_agreement_count: u32,
    /// Number of active non-aggression pacts.
    pub non_aggression_pact_count: u32,
    /// Number of active mutual defense treaties.
    pub mutual_defense_count: u32,
    /// Gold per tick from active trade agreements.
    pub active_trade_income: f32,

    // --- Rival guild extended ---
    /// Reputation gap (rival - player). Positive = behind.
    pub rival_reputation_gap: f32,
    /// Power gap (rival - player). Positive = behind.
    pub rival_power_gap: f32,
    /// Rate at which rival competes for the same quests (0-1).
    pub rival_quest_competition_rate: f32,

    // --- Caravans ---
    /// Number of active trade caravan routes.
    pub active_caravan_routes: u32,
    /// Total income from caravan trade.
    pub caravan_trade_income: f32,
    /// Number of caravans raided this campaign.
    pub caravans_raided: u32,

    // --- Retirement ---
    /// Number of adventurers who have retired.
    pub retired_count: u32,
    /// Sum of legacy bonuses from retired adventurers.
    pub total_legacy_bonuses: f32,

    // --- Monster ecology ---
    /// Total monster population across all regions.
    pub total_monster_population: f32,
    /// Highest aggression level among monster groups.
    pub highest_monster_aggression: f32,

    // --- Festivals ---
    /// Number of currently active festivals.
    pub active_festival_count: u32,
    /// Total festivals attended this campaign.
    pub festivals_attended: u32,

    // --- Backstory ---
    /// Fraction of adventurers that have backstory content (0-1).
    pub has_backstory_ratio: f32,
    /// Number of rival faction connections from backstories.
    pub rival_faction_connections: u32,

    // --- Mentorship ---
    /// Number of active mentor-mentee pairings.
    pub active_mentorship_count: u32,

    // --- Rivalries ---
    /// Number of active adventurer rivalries.
    pub active_rivalry_count: u32,
    /// Average intensity of active rivalries (0-100).
    pub mean_rivalry_intensity: f32,

    // --- War exhaustion ---
    /// Maximum war exhaustion level across all factions at war with us.
    pub max_war_exhaustion: f32,

    // --- Chronicle ---
    /// Total chronicle entries written.
    pub chronicle_entry_count: u32,
    /// Number of recent tragedy events in the chronicle.
    pub recent_tragedy_count: u32,

    // --- Deeds ---
    /// Total heroic deeds earned by adventurers.
    pub total_deeds_earned: u32,

    // --- Population ---
    /// Total regional population in guild-controlled territory.
    pub total_population: f32,
    /// Average civilian morale across controlled regions.
    pub mean_population_morale: f32,
    /// Total tax income from controlled populations.
    pub total_tax_income: f32,

    // --- Site preparation ---
    /// Total preparation level across quest sites.
    pub total_site_preparation: f32,
}

#[derive(Clone, Debug, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum CombatMode {
    /// Predict outcomes via small MLP (microseconds). Used during MCTS.
    Oracle,
    /// Run full deterministic combat sim (milliseconds). Used for evaluation.
    FullSim,
    /// Run tactical sim bridge with generated abilities (~0.1ms).
    TacticalSim,
}

