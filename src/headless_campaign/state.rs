/// An adventurer in the guild. Maps from existing `HeroCompanion`.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Adventurer {
    pub id: u32,
    pub name: String,
    /// Archetype from hero_templates (e.g. "knight", "ranger", "mage").
    pub archetype: String,
    pub level: u32,
    pub xp: u32,

    // --- Combat stats summary ---
    pub stats: AdventurerStats,
    pub equipment: Equipment,
    pub traits: Vec<String>,

    // --- Condition ---
    pub status: AdventurerStatus,
    /// 0–100.
    pub loyalty: f32,
    /// 0–100.
    pub stress: f32,
    /// 0–100.
    pub fatigue: f32,
    /// 0–100. ≥90 = incapacitated.
    pub injury: f32,
    /// 0–100.
    pub resolve: f32,
    /// 0–100.
    pub morale: f32,

    // --- Assignment ---
    /// Which party this adventurer belongs to, if any.
    pub party_id: Option<u32>,
    /// NPC relationship score to the guild (−100 to 100).
    pub guild_relationship: f32,

    // --- Leadership ---
    /// Leadership role providing passive buffs to the guild/faction.
    /// Unlocked through progression. Buff is active while adventurer is
    /// alive and not on a quest.
    #[serde(default)]
    pub leadership_role: Option<LeadershipRole>,
    /// Whether this is the player character.
    #[serde(default)]
    pub is_player_character: bool,
    /// Which faction this adventurer belongs to (None = guild).
    #[serde(default)]
    pub faction_id: Option<usize>,
    /// If traveling to join a faction (Sleeping King mechanic).
    #[serde(default)]
    pub rallying_to: Option<RallyTarget>,
    /// Unit tier progression status.
    #[serde(default)]
    pub tier_status: super::unit_tiers::UnitTierStatus,

    /// History tags accumulated from gameplay. Used to bias ability generation
    /// so abilities reflect the adventurer's actual journey.
    /// e.g., {"solo": 10, "blight_prevention": 3, "region_defense": 5}
    #[serde(default)]
    pub history_tags: std::collections::HashMap<String, u32>,

    // --- New system fields ---
    /// Generated backstory — birthplace, motivation, flaw, personal quest hook.
    #[serde(default)]
    pub backstory: Option<super::systems::backstory::Backstory>,
    /// Legendary deeds earned through achievements.
    #[serde(default)]
    pub deeds: Vec<LegendaryDeed>,
    /// Hobbies developed during idle downtime.
    #[serde(default)]
    pub hobbies: Vec<HobbyProgress>,
    /// Health status with respect to diseases.
    #[serde(default)]
    pub disease_status: DiseaseStatus,
}

/// A leadership role that provides passive buffs while the adventurer is active.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct LeadershipRole {
    pub title: String,
    pub buff: LeadershipBuff,
}

#[derive(Clone, Debug, Serialize, Deserialize)]
pub enum LeadershipBuff {
    /// Adds flat military strength to the faction.
    MilitaryStrength(f32),
    /// Multiplier on faction attack power.
    AttackMultiplier(f32),
    /// Reduces control loss from enemy attacks.
    DefenseBonus(f32),
    /// Passive gold income per tick.
    GoldIncome(f32),
    /// Faster adventurer recovery.
    RecoveryRate(f32),
    /// Improved quest reward scaling.
    RewardMultiplier(f32),
    /// Better recruitment chances.
    RecruitBonus(f32),
    /// Improved faction relationship drift.
    DiplomacyBonus(f32),
    /// Reduces adventurer stress accumulation.
    MoraleAura(f32),
}

/// An adventurer traveling to rally to a faction (for crisis mechanics).
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct RallyTarget {
    /// Faction they're traveling to join.
    pub faction_id: usize,
    /// Position they're heading to.
    pub destination: (f32, f32),
    /// Travel speed (tiles/sec).
    pub speed: f32,
}

#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct AdventurerStats {
    pub max_hp: f32,
    pub attack: f32,
    pub defense: f32,
    pub speed: f32,
    pub ability_power: f32,
}

#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct Equipment {
    pub weapon: Option<u32>,
    pub offhand: Option<u32>,
    pub chest: Option<u32>,
    pub boots: Option<u32>,
    pub accessory: Option<u32>,
}

#[derive(Clone, Debug, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum AdventurerStatus {
    Idle,
    /// In the candidate pool for a quest, not yet dispatched.
    Assigned,
    /// En route to quest location.
    Traveling,
    /// At quest location, executing.
    OnMission,
    /// In active battle.
    Fighting,
    /// Recovering from injury, unavailable.
    Injured,
    Dead,
}

#[derive(Clone, Debug, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum EquipmentSlot {
    Weapon,
    Offhand,
    Chest,
    Boots,
    Accessory,
}

